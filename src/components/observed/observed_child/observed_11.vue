<p><strong>2.</strong><strong>有聲出版</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有聲出版13張專輯，其中國立傳統藝術中心及臺北市立國樂團各有3張專輯、桃園市國樂團有2張專輯。</p>
<p>（1）國立傳統藝術中心</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國立傳統藝術中心本年度發行3張DVD專輯，其中1套為數位光碟與專書《2019臺灣音樂年鑑》同名，另2套為演出實況，其一為2019/6/23舉辦的《五代同堂話國樂－2019年世代之聲 臺灣族群音樂紀實系列》，發行《世代之聲－臺灣族群音樂紀實系列X－五代同堂話國樂》，由林昱廷教授導聆國樂在臺灣70年來的發展歷史，並由國樂耆老、臺北市光復國小國樂團、介壽國中國樂團、臺藝大國樂系國樂團、采風樂坊等跨世代國樂團體演奏代表作品，呈現臺灣國樂發展的軌跡。另一張實況DVD專輯為臺灣國樂團與九天民俗技藝團，2018年代表力作「陣頭傳奇」劇場音樂會，發行《NCO臺灣國樂團+九天民俗技藝團/陣頭傳奇》DVD。</p>
<p>（2）桃園市國樂團</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃園市國樂團有2張專輯出版，分別為《絲竹雅韻》及《桃園四季》。《絲竹雅韻》專輯融合「古典、民間、地方音樂」，收錄13首絲竹合奏作品，以精緻的音色呈現國樂的神韻；《桃園四季》則與金曲作曲家李哲藝先生攜手合作，收錄6首作品，除與標題同名的《桃園四季》第一到第四樂章外，另有《四月雨二胡幻想協奏曲》及《龍潭映象交響詩》2首樂曲。傳遞閩南、客家、原住民及新住民的多元文化，以桃園四季風情，奏響臺灣新樂章。</p>
<p>（3）臺北市立國樂團</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團本年度亦有3張專輯出版，分別為《山地印象》、《淬煉<strong>－</strong>瞿春泉作品精選》及《集合吧！國樂森友會》。《山地印象》為臺北市立國樂團與德國古典樂知名大廠「拿索斯唱片」（Naxos Records）合作，於國際市場推出的音樂專輯，由小提琴家林昭亮擔任協奏。北市國與林昭亮攜手開拓了國樂演出從華人文化區進入國際市場。專輯收錄作曲家關迺忠的《山地印象》、以及喬爾．霍夫曼（Joel HOFFMAN）的第二號小提琴協奏曲《春之雪》與《鸚鵡螺的對稱》共3首作品。《山地印象》原為板胡協奏曲改為小提琴協奏曲版本，描寫阿里山《霧中神木》、《山林火車》與《歡慶豐年》的遞進意象。《鸚鵡螺的對稱》則是霍夫曼的首部國樂創作，引用了漢代舞謠《相和歌》的曲式，來經營美麗的對稱時間結構；《春之雪》是作曲家霍夫曼特地為林昭亮與北市國量身打造的作品，曲中呈現西方作曲家對國樂結構邏輯的觀察與思考。</p>
<p>（4）其他專輯</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;陳家崑《揚》、盧美智《留．轉～鋼琴與民族器樂的邂逅》、陳伊瑜與四象箏樂團《存在》、台北柳琴室內樂團《恣意放歌》及賴宜絜的《箏樂自然》古箏創作專輯。陳家崑的《揚》可說是橫跨百年的二胡音樂發展簡史的演奏專輯，收錄古琴移植二胡演奏曲《關山月》、劉天華創作的《除夕小唱》等。《留．轉～鋼琴與民族器樂的邂逅》雖以鋼琴為主，但是紀錄的是一首首的國樂經典，留下過去與歷史的累積，創作出民族器樂與鋼琴和鳴的新作，轉出承先啟後。盧美智邀請了多位名家，包括臺北市立國樂團首席王銘裕（二胡）、臺南藝術大學中國音樂系副教授黃俊錫（古箏）、香港中樂團演奏家吳曼翎（阮、柳琴）、前臺南藝術大學中國音樂系客座教授侯廣宇（笛、簫）、前高雄市國樂團笙首席蔡輝鵬（笙）及擅長演唱各地民歌的李釆恩（民族聲樂），以1套2張專輯邀請民族器樂、聲樂名家打造出15首動人的樂章。《存在》是由作曲家李哲藝、彭郁雯、藤井俊充創作的6首樂曲，並由古箏演奏家陳伊瑜、四象箏樂團及數位演奏家合力完成的專輯，希望以動人浪漫的旋律，純淨療癒的聲音，撫慰動盪的心靈，儲存這個時代的聲音。《恣意放歌》是台北柳琴室內樂團為成立25週年而發行之專輯，樂團承載了彈撥樂發展的使命，每年均委託作曲家創作全新樂曲以創造彈撥音樂之生命，本專輯集結2010-2018年委託作曲家蘇文慶、劉至軒、張永欽、何立仁、陳能濟、劉德海創作、改編的9首樂曲錄製而成。《箏樂自然》是賴宜絜的古箏創作專輯。畢業於夏威夷大學民族音樂學博士的賴宜絜，與臺、日、美籍作曲家合作，運用古箏特有的清新風格與音韻色彩，引領聽者感受大自然的動靜之美。2020年有聲出版資料彙整如表12：</p>
