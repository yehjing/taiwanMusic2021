<p><strong>（二）文字與有聲出版</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年因為疫情的影響，上半年音樂活動自3月6日以後全數取消，直至6月份以後才逐漸復甦，展演活動雖然減少，但是學術活動明顯增加。國樂領域的出版形式，主要分為文字與有聲兩大類。其中文字部分包括專書、樂譜；有聲出版以樂團和個人發行的專輯為主。</p>
<p><strong>1.</strong><strong>文字出版</strong></p>
<p>（1）書籍</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;專書、樂譜中專書有5本，分別為林昱廷（2020）〈2019年度國樂活動觀察與評介〉專文、張偉萱（2020）《陳暘《樂書》音樂哲學之研究》、陳雯主編（2020）《遺落的天音 古琴論述文集》、林慧寬（2020）《現代音樂中琵琶的演奏技法：以鍾啟榮《夢．亭》、李志純《魂迴夢轉》、董昭民《遊園京韻》為例》及楊軍（2020）《京劇嗩吶曲牌知多少？》。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;〈2019年度國樂活動觀察與評介〉專文是林昱廷以2019年度國樂領域的生態、國人創作及整體發展的趨勢與議題等面向所提出的觀察紀錄與評介報告，彙整收錄於張儷瓊主編的《2019臺灣音樂年鑑》（林昱廷，2020）；《陳暘《樂書》音樂哲學之研究》是輔仁大學張偉萱依2017年博士論文基礎修改出版（張偉萱，2020）；《遺落的天音 古琴論述文集》此文集由陳雯主編，收錄2017年至2019年由台灣琴會主辦，蘭陵琴會協辦之古琴活動，包括國立臺灣戲曲學1場研討會，3場古琴研習營，邀請兩岸三地古琴名家如唐健垣、謝俊仁、戴曉蓮、楊青、梅士軍、成紅雨、張清治、陳雯、孫俊彥等14位名家文稿集結成書，內容談論古琴的律呂、調性、打譜、創作，不同琴派如梅庵、廣陵、川派的風格特點，古琴大家如成公亮、張子謙、管平湖的藝術成就等等，呈現當代琴學風貌（陳雯，2020）；文化大學林慧寬老師的《現代音樂中琵琶的演奏技法：以鍾啟榮《夢．亭》、李志純《魂迴夢轉》、董昭民《遊園京韻》為例》，藉由3首樂曲探討琵琶演奏技法的運用（林慧寬，2020）及院楊軍老師主編的《京劇嗩吶曲牌知多少？》，簡易說明了嗩吶曲牌的基本用法（楊軍，2020）。</p>
<p>（2）樂譜</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;樂譜部分計有揚琴、二胡、笛、簫4項樂器，分以紙本及USB型式出版。紙本有3套，分別為陳思伃主編的《2017-2019臺灣揚琴創作曲集》，林一鳳的《流行二胡教材樂譜精選集第7冊》及陳中申的《一個簫郎－陳中申洞簫作品精選》，由鄭立彬主編，TCO發行。TCO另以USB型式發行2套樂譜，分別是《胡琴作品精選》及《笛與簫作品精選》。以上曲譜都是由臺灣作曲家創作或改編，而5套樂譜中，TCO即佔了3套，TCO本著肩負教育推廣、培育國樂人才之義務，近年來集結臺灣多位作曲家的創作或改編作品，以USB型式儲存及販售，至今已發行《國樂秘笈捌佰式－樂團片段精選》（2018）、《琵琶、阮咸作品精選》（2019），加上2020年發行的《胡琴作品精選》與《笛與簫作品精選》，對於國樂作品樂曲曲量，可謂殫思竭慮十分用心。2020年書、譜出版彙整如表8：</p>
