<p><strong>1.</strong><strong>育藝深遠－藝術教育啟蒙</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;TCO是全臺擁有最多附設團隊的職業國樂團，為了讓更多樂友能夠有一個合奏排練演出的團體，依不同年齡及專業差異，設立了市民國樂團、教師國樂團、學院國樂團、青年國樂團、青少年國樂團、少年國樂團等6個國樂團；同時為了演出的需要，設立了合唱團。這些團隊組成龐大的TCO家族，除了正式大型音樂會的需求外，也分擔了TCO展演、推廣及交流的任務。2020年完成了7場演出，也擔任26場「文化就在巷子裡」的推廣音樂會活動。同時配合九年一貫教育「人文與藝術」課程的規劃演出「育藝深遠－藝術教育啟蒙方案」以「國樂森友會」為題演出10場，總計9,851人進場欣賞（TCO，2020）。</p>
<p><strong>2.</strong><strong>國樂頻道－藝術教育普及化</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;NCO以「國樂頻道」為題，設計了《聽音樂說故事：白賊七傳奇》、&nbsp;《那個年代VS這個年代》、《樂說臺灣風情：小迪遊臺南》等3套曲目，在樂團的排練場所，以演出、導聆及互動方式呈現。從8月開始一直到11月中旬，總計演出56場，由於場地的因素，每場人數限制在100人（NCO，2020）。</p>
<p><strong>3.</strong><strong>校園巡迴－推廣國樂，服務偏鄉</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國今年的推廣音樂會仍是採用樂團進入校園演出的方式，受到疫情影響，今年的校巡活動一直延到10月才開始進行，一共演出22場，外加1場社區演出（桃市國，2020）。桃市國為了服務在地的國小，尤其是偏鄉、資源較不足的學校，讓學子們能夠有機會近距離接觸國樂樂器，並欣賞優美的樂音。依舊將樂團分為A、B兩組，以通俗、趣味性高的曲目為主，帶給學子們一個知性與感性之旅。</p>
<p><strong>4.</strong><strong>全民美育旗艦計畫－藝教宣導，藝同玩樂</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="bkt">國立臺灣藝術大學獲教育部支持，組成專責小組辦理表演藝術推廣系列活動「全民美育旗艦計畫」，以「藝術教育宣導」、「偏鄉學校推廣」、「慈善關懷」、「跨領域科技劇場」等四大方向主軸，結合國樂、舞蹈、音樂、戲劇等多元跨領域表演藝術與產官學的力量，在全臺各地及澎湖金門馬祖等離島，主動推送各類優質表演藝術，進行全民環島藝文播種，期許傳遞給全民生活美育，及優質跨領域藝術饗宴走入民眾生活。</span>（國立臺灣藝術大學，2020）</p>
