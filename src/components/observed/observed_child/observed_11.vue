<h3><strong>貳、國人創作與發表</strong></h3>
<h4><strong>一、全年度的國樂創作</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當代國樂的創作是國樂持續發展的重要因素，鼓勵臺灣作曲家持續為臺灣這塊土地留下當代的音響紀錄是國樂發展必須面對的課題。2020年受到疫情的影響，許多演出場次被迫取消或延後，許多新創作品也因而未能如期發表。在如此艱困的環境下，國人在國樂創作方面雖不如2019年的成績，委創樂曲有81首，包含1首套曲；編曲或移植有4首，共計8首（詳如附錄）。</p>
<p><strong>（一）委創的來源</strong></p>
<p><strong>1.</strong><strong>委創來源的主體－職業樂團、民營樂團</strong></p>
<p>（1）職業樂團</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;往年由於職業樂團年度演出的需要，再加上每年都有穩定的預算，因此，在委託創作上的數量，都會是年度創作的主要來源。2020年受到疫情影響，許多演出不得不取消或延期，相對於原本委創的樂曲也無從發表，甚為可惜！期待全球疫情能夠早日受到控制，讓人們的生活恢復往常的光景，文化的交流得以重起。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年五大職業國樂團都有委創樂曲，其中TCO全年共有7首委創樂曲，其中國人創作6首，因疫情而未能發表的樂曲有5首，其中國人創作的有4首；NCO全年共有5首委創樂曲，4首為國人創作，其中1首《臺灣音樂世紀豐華》套曲，邀請4位作曲家共同完成12首樂曲，內含4首純器樂和8首歌曲；桃市國全年共有7首委創樂曲，6首為國人創作；高市國全年只有1首委託國人創作的樂曲，沒有2019年的整場新創作樂曲音樂會；南市國今年有1首委創樂曲，由於樂團正逢創團20周年慶，因而邀請蘇文慶為團慶音樂會創作樂曲。總計2020年職業樂團共委託創作了21首樂曲（含1首套曲），其中國人創作18首，約佔85.71%。受到疫情影響而未能發表的樂曲有5首，其中國人創作有4首。今年職業樂團所委託國人創作的樂曲僅有18首，佔整體的22.22%，相較2019年的46首，佔整體44.66%，有明顯的大幅下滑現象。若加入因疫情而未能發表的4首，整年度也只有22首國人創作樂曲，主要原因是TCO的委託數量減少，加上高市國沒有舉辦作品發表會導致。</p>
<p>（2）民營樂團</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年民營樂團受到疫情的衝擊，在前半年的演出大幅減少，但是在臺灣疫情受到良好的控制後，下半年的演出活動逐漸恢復正常。2020年民營樂團委創的單位有：台南樂集、三個人、小巨人絲竹樂團、台灣揚琴樂團、晨園琴箏社等11個團體。一共委託創作25首，約占整體的30.86%。其中台南樂集有6首、三個人有5首；台灣揚琴樂團有3首；小巨人、當代藝林、晨園琴箏社各2首，其餘5個團隊都有1首委託創作。由於民營樂團的經費較不充裕，在委創方面，相對需要公部門的補助。在這25首新創樂曲中，有15首是由國藝會補助；臺北市、金門文化局各補助1首。2020年的委創樂曲在數量上，相較2019年國人創作18首，增加了7首樂曲，顯見公部門的補助對於鼓勵國人創作達到了實質的效益。</p>
<p><strong>2.</strong><strong>委創來源的枝幹－社會團體、專業系所</strong></p>
<p>（1）社會團體</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年在社會團體的部分，除了國樂學會與台灣揚琴發展協會兩個社會團體持續關注國人創作的推展面向，國樂學會委約了10首國人創作；揚琴協會則委約了2首樂曲。此外今年剛創會的臺灣笛簫協會，在笛演奏家陳中申與笛簫專業人士共同努力下，於10月4日舉辦創會音樂會，特別委創了2首樂曲；中華民國現代音樂協會舉辦了一場琵琶獨奏會，委創了3首樂曲。總計，4個團體在2020年共委託國人創作了17首樂曲，占整體的20.99%。更多社會團體投入在國人的創作，對國樂的發展具有實質的意義。在社會團體的部分，國藝會補助有16首，是創作經費的重要來源，也因著國藝會挹注經費，使得國樂的創作得以持續發展。</p>
<p>（2）專業系所</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在大專校院委創樂曲方面，2020年有3首，佔總體的3.7%。其中2首由國立臺灣師範大學民族音樂研究所客座教授Dr. Hsuan Chang Kitano擔任指導的即興作品；1首是臺藝大國樂系委託熊仁岳新創的樂曲《紡》，12月1日在衛武營國家藝術文化中心演出。臺藝大國樂系今年同樣受到疫情影響，原本已經委創完成的2首樂曲，已延至2021年演出。</p>
<p><strong>3.</strong><strong>委創來源的綠葉－個人邀約與創作</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年個人邀約、創作的作品有18首，佔總體的22.22%。其中楊杰儒延續她2019年的創作發表，今年同樣發表了5首樂曲；張婉俞在個人音樂會中，發表3首新創樂曲；陳麗晶在個人琵琶音樂會中委約1首樂曲，在她和謝從馨兩人的聯合音樂會中委約2首琵琶與打擊樂的重奏曲；陳乙萱在她碩士畢業音樂會中，委約了1首新創樂曲；吳奇峯在個人音樂會委約1首樂曲，曲名《白蛇幻想組曲》共分4個部分，12個段落；劉宇儐自行創作與演出1首新作品。另外，笛簫協會發表4首屬於個人邀約、創作的樂曲，在他們創會的首演音樂會中。</p>
<p><strong>（二）委創的內容</strong></p>
<p><strong>1.</strong><strong>以聲繪形，栩栩如生</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台南樂集為了推廣音樂，同時關注生態議題，以揚琴為主奏，舉辦了一場名為「我們的島，飛天鑽地」的揚琴專題音樂會。委創了5首樂曲，以臺灣特有的動物譜曲，此5種動物分別為：臺灣蝶蛾、櫻花鉤吻鮭、石虎、藍鵲與雲豹。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;張婉俞《雲的思念》是為雲豹而寫，<span class="bkt">借鏡繪畫上的點描手法，將選擇後的音高及音程當作音色，並有限度的讓鋼琴與揚琴對話</span>。（台南樂集，2020，頁9）樂曲一開始鋼琴以低沉的重音為穩固低音，而右手的高音則雙音、多音進行，兩者之間的音高相差四個八度以上，讓整體音響聽起來有種迷濛、霧化感。揚琴快速擊板時的聲響彷似雲豹匆促的腳步，此作品中每一個聲響都是一道色彩，將瀕絕雲豹的模樣一塊塊的拼貼出來。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;作曲者暨演奏者吳家榆《長尾山娘》（別稱臺灣藍鵲），用小樂隊的形式演奏，分別有鋼琴、小提琴與打擊。在揚琴主奏下，鋼琴襯職的伴奏著，而小提琴補足整體聲線較為點狀的缺陷，並於樂段轉換時擔任重要銜接角色，<span class="bkt">此曲在各個樂段分別描繪藍鵲的各種面貌，面對敵人的攻擊、優雅的飛行姿勢&hellip;&hellip;等</span>。（台南樂集，2020，頁11）後半段樂曲中加入輕快的打擊樂器讓整首樂曲產生躍動感，動聽之外，其旋律亦平易近人，讓人記憶猶深。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;謝惠如《拾虎》<span class="bkt">以石虎與牠的里山好朋友為故事展開</span>。（台南樂集，2020，頁12）樂曲第一段為三拍子的圓舞曲，旋律輕快明亮，讓人聯想到石虎與其好友開心的暢遊嬉戲，生活無憂無慮。然而曲風突變，揚琴以強烈不規則重拍，輔以樂隊不和諧的音程進行，象徵石虎遭遇路殺、毒藥等緊張的氛圍。在最後樂段，作曲者回到最初輕快悠揚的主題旋律，而後加強、加重旋律，甚至加快速度，讓樂曲激昂的結束。<span class="bkt">此曲是作曲者既企盼石虎能肆意活著，又祈願人們能關注這些快停止的生命而作</span>。（台南樂集，2020，頁12）</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;任重《弄火》<span class="bkt">為台灣原生種蝶蛾所作樂曲，樂曲敘述蝶與蛾的一生皆是向光而行，樂曲共分五個小樂段，破蛹、生命、與火對舞、凋零、即將失去生命</span>。（台南樂集，2020，頁15）作曲家以動態描繪性的手法，結合目前揚琴比賽的選曲中，擅長發揮揚琴炫技功能的創作手法去譜寫。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;刁鵬《櫻花鉤吻鮭》<span class="bkt">以一隻名叫小櫻的鮭魚，牠為了繁衍下一代而奮力洄游為發想</span>。（台南樂集，2020，頁16）全曲給人的感覺極具動感，鋼琴以同樣節奏的低音躍動著，如溪水不停地沖刷，而揚琴則是鮭魚小櫻，在流暢的旋律中奮勇向上，雖然小櫻一度遇上危險（擱淺溪邊），旋律也一轉慢板，以大提琴為主奏，奏出悠長的旋律線，很是生動的刻劃出鮭魚小櫻心中的掙扎，究竟要放棄還是再奮力一搏？然而最終仍是一躍回到溪水中繼續上游，此曲加入大提琴的低重音，一則補足揚琴先天低音的不足，二來大提琴的低音讓樂曲的聲響達至最佳平衡。</p>
<p><strong>2.</strong><strong>傳統取材，賦予新聲</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年來國人在創作取材方面，大多會以在地的傳統音樂、歌謠等做為核心素材。如：劉至軒的第三號音樂會序曲《慶》，旋律融入北管曲調音型；張婉俞為琵琶與弦樂四重奏創作的《恬靜世界》，引用《老六板》中的五聲音階用音E A D C G 作為琵琶的核心音高；何立仁為桃市國創作的二胡協奏曲《客風弦韻》，以傳統客家八音的旋律片段為素材發展而成；黎俊平為中華國樂團創作的《鐘鼓樓》，以南管四大名譜之首《四時景》作為核心素材；鄭光智為中華國樂團創作的《梅花操》，吸取南管套曲《梅花操》的片段旋律，加以拼湊與融合；林子倫為中華國樂團創作的《鬼祭盛會》，取自北管曲牌古路【風入松】作為旋律素材創作而成。這些素材都經過作曲家的巧思，運用現代創作手法，透過旋律、節奏、音色等的變化，賦予了嶄新的風貌與音響。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;劉至軒的第三號音樂會序曲《慶》<span class="bkt">樂曲由單主題貫串，旋律融入北管曲調音型，於不同調式中遊走產生色彩變化。和聲上刻意避開三和弦，改用疊置四度的音響效果，營造熱鬧歡慶的氣氛。頑固節奏與擊樂音色組合同時帶有當代流行及傳統的音樂風格，而後段主題再現時則搭配北管的聲響元素，使整首作品有如於各時代中穿梭</span>。（TCO青少年國樂團，2020，頁2）在層層舖疊的節奏引導下，單主題旋律伴隨調式轉換產生色彩變化，先是嗩吶吹管的領奏，弦樂接棒，伴隨打擊樂。樂曲進行接近中段時，轉由打擊樂主奏，器樂僅以拍打琴身及胡琴弓桿敲打琴筒伴奏，長度接近2分鐘。接著單主題旋律再現，並由嗩吶巧妙過渡到傳統的北管音樂出現，曲終在打擊與樂隊的競奏下歡快的結束。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;黎俊平《鐘鼓樓》<span class="bkt">樂曲以南管四大名譜之首《四時景》作為核心素材，描繪大自然四季的風景，在骨幹音呈上以序列音列的手法做為結構序碼，透過樂器在傳統演奏上的手法，呈現南管音樂的風格特性</span>。（國樂學會，2020，頁11）描述著鐘鼓樓從過去座落在繁華地區，至今成為文化遺產所帶給人們的省思。作曲家運用了南管的《四時景》作為音樂素材，保留了南管支聲複音的織體，加入不同音程的堆疊，賦予音樂新的音響表現。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;鄭光智《梅花操》<span class="bkt">吸取原曲梅花操的片段旋律，加以拼湊與融合，試圖使用現代化配器聲響融合南管特色，以及傳統支聲複音的作曲技法，揉和出新穎的色彩，展現出當代技術與聲響特色</span>。（國樂學會，2020，頁12）將傳統音樂支聲複音的特性保留下來，加入和弦伴奏豐富其音樂聲響。</p>
<p><strong>3.</strong><strong>樂團行銷，城市為名</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國自2016年創團以來，致力於建立樂團自有且具在地特色的樂曲，邀請作曲家以桃園為題創作樂曲，累計至今已達10首。其中2首樂曲尚未發表，其餘8首中，除1首為新加坡作曲家王辰威的作品《世外桃園》外，其餘7首皆為國人創作。2020年8月16日在參與竹塹國樂節10周年慶的演出，以「桃園．炫」為題發表了2首委創樂曲，陳樹熙的《桃園二景》與曾翊玹的《TAOYUAN：CAPRICCIO桃園暢想曲》。陳樹熙的《桃園二景》分為〈走馬看花逛老街〉、〈埤塘〉（群鴨戲水）兩個樂章，此曲運用了客家八音的素材，創作出一首描繪桃園客家文化的作品。〈走馬看花逛老街〉的樂章運用了客家八音的《流水》為基底；〈埤塘〉（群鴨戲水）則運用了客家八音《鵝公叫鵝母》為素材。作曲家還將按壓黃色小鴨所發出的聲響安排在第二樂章的末段，為作品增添趣味性。曾翊玹的《TAOYUAN：CAPRICCIO桃園暢想曲》<span class="bkt">使用兩大主題旋律相互呼應，對照出桃園這片土地與生活在這的人民，以及兩者交會所經歷的過去與現在，來期盼這片桃花園，能開出絢爛的未來</span>。（桃市國，2020，頁4）</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國多年來的努力，期盼能為樂團留下優質的保留曲目，以城市為名委創具有鮮明在地形象的樂曲，成為樂團行銷的最佳利器，同時也為樂團提供了許多優質的品牌故事。</p>
<h4><strong>二、國人作品專場發表會</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020國人作品專場發表會，在團體方面有： NCO、國樂學會、臺灣笛簫協會等；個人方面有：郭靖沐、楊杰儒等。以下就各場發表會提出觀察報告：</p>
<p><strong>1.</strong><strong>臺灣國樂團「臺灣音樂世紀豐華」發表會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團在10月1日演出的「臺灣音樂世紀豐華」音樂會，原是文化部為了東京奧運會所特別設計推廣臺灣文化活動之一。以臺灣音樂史發展脈絡為主軸，由前國立傳統藝術中心主任吳榮順擔任導聆，分成4個段落來陳述。一、孕育傳統之聲；二、戰火中的遺落；三、蟄伏後的復甦；四、島國新視界。每個段落都有3首樂曲，由梁啟慧、范揚景、林心蘋、王乙聿4位作曲家完成整場音樂會的曲目。內容包含了各個族群的音樂，以音樂來述說臺灣不同時期的景象，最後一段就是以文化融合作為出發點，結合現代國樂團、原住民音樂的演出，傳達臺灣音樂的活力與新方向。這場的製作與主旨非常鮮明，透過導聆更能讓觀眾進入到聯想的層面，別具意義！</p>
<p><strong>2.</strong><strong>國樂學會國樂創作聯合發表會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂學會從2007年開始，每年舉辦國人的創作發表會，至今總計舉辦了13場（2013年停辦），總計創作了133首樂曲。每年邀請的作曲家都是經過創作委員會的討論而定，邀請了吳宗憲、林子倫、倪珩均、洪千惠、徐瑋廷、熊仁岳、黎俊平、劉至軒、鄭光智與廖明勛等國內青中年作曲家10人。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年參與創作的人員與往年相比，變動較大；首次參與這聯合發表會的人員有3位；在創作手法與表現形式上，較往年豐富，有幾首作品運用國樂作品中較少見的創作手法，包括現代創作技法、十二音列等；絲竹樂團的編制與配器，也有不同的組合；多數作品運用了臺灣在地素材，或以臺灣的文化風情作為主題。整體而言，對這次的創作樂曲的多元、各具特色，觀眾反應熱烈；樂界人士都給予正面肯定。學會建立的創作發表平臺，提供了作曲家實踐樂曲的夢想，更能藉著演出的反饋，提升自己創作的能量，更讓臺灣的國樂能蓬勃發展，並且建立臺灣在地的風格特色。</p>
<p><strong>3.</strong><strong>臺灣笛簫協會創會發表會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;笛簫協會於2019年，由陳中申、吳宗憲等臺灣笛簫專業人士為了推展笛簫藝術而成立，並於2020年10月3日起於臺中、臺北、高雄與花蓮等4地，舉辦4場創會音樂會「笛聲四起．簫震八方」。每場的演出人員未盡相同，演出曲目也隨演奏家而有所變異。臺北場次是4 場音樂會新創樂曲發表的核心場次，有2首委創樂曲，吳宗憲的《笛．語》與蔡欣微的《話笛點簫》。另外首次發表的樂曲有陳中申改編自河北民間音樂笛子二重奏《蝶雙飛》；蔡欣微笛曲《航》；廖錦麟笛曲《寂靜的夜》。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《笛．語》是一首笛子二重奏，由吳宗憲與陳中申擔任演奏。樂曲在以描繪性的手法，有如初戀情人在分分合合中，最後找到彼此互動模式，回歸平靜。《話笛點簫》是以1把G調洞簫、3支笛子，分別為A調梆笛、D調曲笛及G調大笛一起演奏，加上1位打擊成為五重奏的編制演出。曲中使用陳中申研發的半音笛及首次在音樂會露面的陳氏半音簫合作演出，由賴苡鈞、張君豪、蔡宜靜、林克威擔任笛簫、郭以芯擔任打擊演奏。樂曲分為8段，此曲跌宕起伏速度變化極大，因此由陳中申擔任指揮，中段為客家山歌素材表現，讓樂曲增添不少親切感。《話笛點簫》與「畫龍點睛」名稱相似，寓意笛簫協會在臺灣宛如「畫龍點睛」般成為笛簫藝術的推手。</p>
<p><strong>4.</strong><strong>郭靖沐作品發表會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="bkt">【箏新視野III】藉由重奏來探索「關係」這個議題；樂器與人、人與人、想法與操作、給予與接收、震動與感動等等</span>。（郭靖沐，2020，頁5）這段出自演奏家個人在節目冊中所傳達的訊息，可以明確地表述他在規劃音樂會內容的思維，也期盼透過他的演奏，能夠讓所有參與的觀眾都能去思考<span class="bkt">作曲家在生命中的覺察與體會，這些美好的發現，將化為無形的聲波分享與你</span>。（郭靖沐，2020，頁5）</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;郭靖沐的古箏演奏能力，絕對是箏樂界的翹楚，他勇於嘗試不同類型的樂曲，他也不斷思索當代箏樂的發展。經過這些年的思考與沉澱，他規劃出這一系列的音樂會，<span class="bkt">希望透過策劃、創作、演出，讓音樂更貼近人、人性、生命</span>。（郭靖沐，2020，頁23）</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;【箏新視野III】箏樂音樂會由5首委創樂曲所組成，第5首作品是郭靖沐自己的創作《無聲地釋放》，實驗的精神似乎更顯而易見，這是一首寫給無雁柱箏的作品，曲長近11分鐘。樂曲運用音量的變化結合演奏技法，再使用電扇綁上1條繩子，穿過古箏的弦與弦之間，然後離場，讓電扇發出近似先前的聲響，1分鐘後：繩子斷了，風扇停了，掌聲響起，同時象徵著表演的結束。</p>
<p><strong>5.</strong><strong>楊杰儒作品發表會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2018年起楊杰儒舉辦了首次的創作發表「柳琴60快樂！！」後，連續2年都持續舉辦個人創作發表，2020年創作發表會的標題「弄樂」，演出曲目以楊杰儒創作作品為主，其它包含改編、移植等樂曲。她的創作仍以推廣為主軸，自去年嘗試不同樂器的創作後，燃起對其它器樂的寫作興趣，透過不同的樂器特性發展出不一樣的樂曲特色，因此在今年更加入二胡及笙的創作，望能透過這次的試驗產生不同的曲趣。</p>
<h3><strong>參、值得討論的趨勢與議題</strong></h3>
<h4><strong>一、</strong><strong>2019</strong><strong>年趨勢的持續追蹤觀察</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;要掌握趨勢的脈動，需要藉由長期持續觀察。本文回顧2019年提出教育成效彰顯，「國樂人才輸出」與「國樂樂譜借用平台」成效兩個議題追蹤觀察。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣培養的國樂演奏人才，在各地華人樂團都有很好的發展，他們合作的態度與精湛的技術，都受到當地的肯定。但在輸出的同時，也開始有部分的人員回流。有的因為家庭因素，有的因為生涯規劃的改變，有的因為生活適應、思鄉。不論何種原因，其實人員流動對樂團而言都是正常的現象。2020年由於受到疫情的衝擊，全世界表演藝術幾乎都停擺，特別在染疫風險較大地區的臺灣演奏家們，多少都會增加他們返臺避疫的意願。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「國樂樂譜借用平台」持續穩健在發展中，是否能突破現有在國樂合奏及絲竹樂合奏樂譜的借用範圍，擴展到各項獨奏樂器的樂譜借用，是需要持續努力的方向。</p>
<h4><strong>二、</strong><strong>2020</strong><strong>觀察的議題</strong></h4>

<p><strong>1.「國樂樂譜借用平台」服務</strong></p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自2016年成立了「國樂樂譜借用平台」，5年來已趨向穩定發展，作曲家授權的樂曲逐漸增加。2020年一共借出28位作曲家的樂曲，其中20位是國人，8位非國人；總計借出的樂曲有288首，國人的作品有239首；非國人的作品有49首。借譜平台主要針對每年的學生音樂比賽指定曲目提供正確、授權的作品，長年來已經累積不少數量，因此，逐漸擴展到其他的使用範疇，讓作曲家的作品能夠推廣，也能獲得必要的尊重。</p>
<p><strong>2.</strong><strong>疫情中的國樂生態</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;突如其來的武漢肺炎（COVID-19），打亂了國樂團隊既定的年度計畫，尤其是一年僅有一場音樂會的團隊而言，更是極度挫折！慶幸臺灣的疫情控制在疫情爆發後3個月，逐漸受到控制，國樂的活動才在有限度下漸漸回復。樂團在排練時，以壓克力板放在每一位吹管樂的左右與前側，來防止口沫的飛濺，而指揮與其他團員必須全程配戴口罩，作為防護的機制。音樂會演出的當下，當疫情較為嚴峻時，除了部分管樂團員外，其餘都配戴口罩演出，而觀眾必須以實名制，在票根上留下個人姓名、電話號碼，並量測體溫後，才能進場聆聽音樂。為了在室內保持社交距離，觀眾席次有以梅花座或減少席次的方式，降低進場人數，相對票房收入大大減少。TCO率先在3月21日舉辦了全臺第一場網路直播音樂會，也帶動了國樂團現場表演直播的風潮；6月13日在中山堂演出的「那疫年，我們的音樂比賽&hellip;T_T|||」，是疫情解封以來臺灣的第一場售票演出音樂會，也是TCO的第四場網路線上直播音樂會，之後國樂的活動才逐漸恢復榮景。</p>
<h3><strong>結語</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年國樂的演出活動在疫情籠罩中度過，全年的創作數量有81首國人作品及4首編配的樂曲，另外還有至少寫作6首已創作完成卻因疫情影響必須延後發表的樂曲。今年職業樂團的委創樂曲大幅減少，是總量減少的關鍵，但是以今年的景況，81首的新創樂曲，也算是不錯的成績。這些作品的創作理念與手法，關注在我們所生活這塊土地上的多元樣貌，以不同的表現形式與聲響，述說臺灣美麗動人的故事。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因疫情影響，讓樂團有機會運用網路線上直播來分享美麗動聽的音樂，不僅撫慰了受到疫情干擾生活的不安情緒，同時也打開了樂團行銷的另一扇門，可說是「塞翁失馬，焉知非福！」</p>
<p class="bkt"><strong>後記：</strong></p>
<p class="bkt">&nbsp;&nbsp;&nbsp;&nbsp;在疫情初發之際，當下完全無法預測2020國樂的生態會有何發展。雖然，演出活動一一被取消或改期，但是，我們的團隊仍舊兢兢業業地收集所有資料，最終才能順利完成年鑑國樂類的觀察。在此除了感謝各單位提供的寶貴資料外，也要謝謝任重、郭靖沐等願意無私的分享創作的理念與手法。當然最重要的還是要感謝我們堅強的團隊：林青儀、萬智懿、吳沛諭及蔡佩娟四位助理的努力。沒有妳們全年全心投入與詳實紀錄蹲點場次，這篇報告是無法完成的。在此，向妳們致上誠摯的謝意，完成蹲點的榮耀，全歸於妳們！</p>

