<p><strong>（二）比賽與檢定</strong></p>
<p><strong>1.</strong><strong>比賽：</strong></p>
<p>（1）全國學生音樂比賽</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全國學生音樂比賽共分為團體、個人兩大類，參賽人員須為在籍之學生。共分為國小、國中、高中職以及大專組，其次再分為A、B兩組：A組為就讀音樂班、科、系、所者，B組則為就讀非音樂班、科、系、所者。除團體項目B組不得跨A組參賽外，個人項目並不禁止B組的學生跨A組比賽。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;108學年度全國學生音樂比賽團體項目中，屬於國樂類別的有：國樂合奏、絲竹室內樂合奏；個人項目中，屬於國樂類別的則有：高胡獨奏、二胡獨奏、中胡獨奏、笛獨奏、簫獨奏、笙獨奏、嗩吶獨奏。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度音樂比賽決賽因受疫情影響，教育部於2月24日宣布團體項目停辦，並發給「108學年度全國學生音樂比賽決賽參賽資格證明書」。除此之外，亦規劃在下半年擇期舉辦「109年度好優Show學生藝團活動」，給予決賽參賽團隊一個展演舞臺。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;個人項目雖如期舉行，但取消頒獎典禮以及場內觀摩，僅評審、大會工作人員、參賽者與陪同人員得以入場。入場時需量體溫，並全程佩戴口罩。由於3月份正值疫情高峰期，為了讓學生能夠安心比賽，同時也避免學生染疫。主辦單位國立臺灣藝術教育館投下了龐大的人力與物力，在每個比賽場地都建構了一個緊急救護站，全程由醫護人員駐守。比賽場地定時消毒，並於中場休息期間，使用紫外線燈殺菌，這一切的作為，都是為了維護大家的安全，更避免群聚感染。遇到體溫超過38.5度的參賽者，即禁止進入比賽會場，但為了維護他們的權益，在所有比賽結束後，另行提供一次機會，讓當下體溫過高的學生，能夠一展身手。</p>
<p>（2）專業器樂大賽</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;職業樂團所舉辦的比賽受到疫情影響，原先TCO、TSO兩團訂於3月28日至31日合辦的「2020臺北國際指揮大賽」比賽，原先公告延至10月底，然而，疫情持續擴展，到了秋冬時期，更是新的一波疫情高峰期。有鑒於國外參賽者都無法如期來臺參加比賽，最後決定取消此次的比賽。NCO則在疫情較能控制之際，舉辦了揚琴新秀選拔的項目，也是今年僅有的專業大賽。</p>
<p>《揚揚得藝》2020 NCO器樂大賽－揚琴新秀選拔</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;NCO的全國器樂選秀活動，是為選拔國內優秀之器樂演奏人才，提供展現技藝的平臺，透過公開舉辦的選秀活動，讓優秀的新生代演奏人才有嶄露頭角的機會，賽後並安排相關的音樂展演活動，擴大比賽效應並喚起大眾對傳統樂器的再認識及重視。從第一屆嗩吶選秀至今，已陸續舉辦7種器樂選秀。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次的揚琴大賽，初賽是以DVD送審，共有30位青年好手參加，經審查結果，錄取10名進入實體面試的複賽。複賽以帷幕的方式進行，選出李艾霏、杜宛霖、陳亭豫、張哲瑋與郭盈含等5位優勝者進入決賽。最終由杜宛霖奪得第一名的殊榮，她同時獲得最佳魅力舞台獎；第二名則有此次參賽的5人中，唯一的男性張哲瑋獲得，他同時獲得最佳人氣獎。</p>
<p>（3）民間器樂大賽</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;受到疫情影響，國樂學會原本以為必須停辦的器樂大賽，在疫情得到控制後，立即展開大賽的籌備計畫。共舉辦北、南區兩場次，總計有1,003人報名，其中北區有828人，內含吹管組220人、擦弦組176人、彈撥組429人；南區178人，包括吹管組33人、擦弦組36人、彈撥組109人。參與的人數較2019年的885人，有明顯增加。</p>
<p><strong>2.</strong><strong>檢定</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;考級檢定提供一般學習國樂的人士，在學習過程中一個檢核的平臺，讓他們能夠藉由外部機制檢核的結果，了解自己學習的成效，以此循序漸進學習，達到自我訂定的目標。2020年國樂學會在全臺共舉辦25場次，報名人數905人（含110人透過網路檢定），缺考人數8人，延考人數209人，不合格人數67人，合格人數504人。延考的原因，主要都是因著疫情影響所致。</p>
