    <h3><strong>前言</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年對全球而言，是一個艱困的年度。自從武漢肺炎（COVID-19）蔓延全球後，人類所有的活動都受到嚴重的影響。臺灣因著2003年SARS的經驗，以致政府能夠快速反應，提出策略，而民眾的自覺與配合，更是防疫成功的重要因素。雖然受到疫情影響，導致今年2月中旬至6月中旬的演出活動被迫取消、延期或改為網路直播。但由於疫情管控得宜，在採取實名制、配戴口罩及量測體溫的規範下，於6月14日開始逐步恢復正常演出活動。全年度的國樂創作數量有81首新創（內含1首套曲）與4首改編作品，另外有5首（4首為國人創作）已創作完成，卻因疫情而未能發表的作品。以下將從國樂領域的生態與發展及國人創作與發表兩面向來分析與評述，茲分述如下：</p>
<h3><strong>壹、國樂領域的生態與發展</strong></h3>
<h4><strong>一、疫情對國樂生態影響之觀察</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年因著突如其來的疫情，打亂了所有國樂團體的既有規劃，全國學生音樂比賽國樂合奏與絲竹室內樂合奏等項目的停賽，更造成國樂基層教師頓時失去主要的收入來源，影響之大，無法計數！從2月15日，桃園市國樂團（以下簡稱桃市國）與高雄市國樂團（以下簡稱高市國）聯合舉辦的「雙城樂會賀新春」音樂會受到疫情影響停辦開始，一直到6月21日演出活動取消的達到45場次、推廣活動取消有11個場次，主要為中華民國國樂學會（以下簡稱國樂學會）所舉辦的考級檢定活動。延期舉辦的演出活動有15個場次、推廣活動延期的有3個場次，其中包含臺北市立國樂團（以下簡稱TCO）與臺北市立交響樂團（以下簡稱TSO）聯合舉辦的「2020臺北國際指揮大賽」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;為了降低疫情所帶來的衝擊，國立藝術教育館推出的「109年度好優Show學生藝團活動」補助方案，讓部分學校樂團能夠在疫情趨緩後舉辦音樂會。文化部也推出了藝文振興紓困辦法，提供藝文團體申請，以彌補應疫情所產生的部分營運缺口。TCO為了持續國樂的表演活動，首先於3月13日運用Facebook網路直播的方式，展開了「TCO小學堂」一系列個別樂器的演出與教學活動；3月21日更在YouTube頻道開啟了第一場網路直播線上音樂會「望春風」，獲得熱烈的迴響。高市國與桃市國也在5月後以網路直播方式演出，這也是因應疫情所帶來的變通方式，也開啟了國樂活動展現的另一個管道。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂學會面對疫情而接續取消了10場考級檢定，在考量到考生的需求下，開創出網路檢定的模式，提供考生上傳影音檔，做為評分的依據，總計有110人參與檢定，也已成為日後檢定考試的方式之一。</p>
<h4><strong>二、展演活動</strong></h4>
<p><strong>（一）公部門樂團</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣以公部門經費成立的國樂團有職業與非職業兩類，職業樂團有以公務機關及基金會兩種不同營運的模式；非職業樂團則由客委會、市公所、文化中心等不同機關所支持成立。</p>
<p><strong>1.</strong><strong>職業樂團</strong></p>
<p>（1）臺北市立國樂團</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;TCO身為臺灣最具規模的職業國樂團，在2020年3月5日由於外籍音樂家確診武漢肺炎的消息發布後，政府立即宣布取消近期所有演出活動，國際邀演也全面延後的訊息。TCO於3月6日在官網及粉絲頁發布取消3月8日至5月2日的13場演出，並將「2020臺北國際指揮大賽」活動延期，最終取消；3月30日再次發布第二波節目取消公告，取消了5月3日至5月24日的10場演出，共計23場的演出，最終全年共計取消或延期44場（包含6場中國巡演）（TCO，2020）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;TCO為了思考音樂家的社會責任，運用網路直播的策略，快速轉型為線上活動，以音樂來撫慰不安的人心，領頭走出表演藝術的困境。率先在3月21日舉辦了全臺第一場網路直播音樂會，也帶動了國樂團現場表演直播的風潮。6月13日更締造全球第一場全席售票音樂會，為藝文界點亮了希望的明燈，也撫慰了群眾不安的心情。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;面對疫情，TCO推出了「TCO小學堂」及「TCO小學堂青年演奏家系列」和「廣場藝術節」等活動。前兩者是邀請樂團演奏家在臉書粉絲專頁以直播分享各個樂器的心得，共計44場。「廣場藝術節」是TCO與臺北市立交響樂團見疫情已逐漸趨緩，聯手規劃的大型戶外展演活動。自5月29日至6月21日連續4周之週五、六、日傍晚，於臺北市中山堂廣場隆重舉行。期盼藉由戶外演出，振興表演藝術產業，逐步恢復過往榮景。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;雖然受到疫情的影響，TCO在2020年還是推出了19場的演出，其中跨界的音樂會有5場。另外，附設團隊有7場演出，個人獨奏會有1場，合計26場的音樂會。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年委託創作有12首，國人創作10首，非國人創作2首，受到疫情影響，其中5首尚未發表（1首為非國人創作），因此，國人創作只有6首。相較2019年的27首委創樂曲，23首由國人創作大幅減少。</p>
<p>（2）臺灣國樂團</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團（以下簡稱NCO）隸屬於文化部轄下的傳統藝術中心，是唯一隸屬於中央的國樂團。2020年受到疫情影響，演出活動明顯減少，在國內的展演活動有8場，其中包含應邀參與竹塹國樂節的演出1場；揚琴新秀選拔1場；推廣音樂會56場（NCO，2020）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;NCO面對疫情而產生的對應策略是開放影音資源，有「NCO雲端音樂盒」及「NCO ON AIR 新秀舞台」兩部分。「NCO雲端音樂盒」提供了《北管印象》、《玉山之巔》、《蓬瀛狂想》、《繁花》、《聽香》等5首，選自歷年演出的經典樂曲，讓愛樂的朋友們能夠在線上觀看。「NCO ON AIR 新秀舞台」則以樂團近年新進的10位團員，各自演出獨奏曲的選段，一則提供這些團員有個舞臺的磨練機會，另一則可提供樂友們觀賞他們精湛的演出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;12月19日在臺灣戲曲中心大表演廳所演出的「破繭重生」音樂會，是江靖波擔任NCO音樂總監以來第一場總監專場音樂會。音樂會以江文也的《臺灣舞曲》開場，囊括臺灣老中青三代作品，蘊含繼往開來的期許。音樂會中最主要的作品是王乙聿的《破繭重生》，這是他的第二號交響曲，為長達50分鐘的大作，共分為4個樂章。由於作品有相當豐富的層次，要如何完整的呈現，在在考驗指揮與樂團的功力與默契。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年委託創作的樂曲有5首（1首套曲），其中國人創作4首，非國人創作1首。10月1日「臺灣音樂世紀豐華」音樂會，係文化部特別為東京奧運規劃，為了推廣臺灣文化的活動之一。因此在內容設計上，以臺灣音樂發展史為主軸，由各個族群作為切入點。全場音樂會分為4個段落，每個段落由3首樂曲組成，一共12首樂曲，其中4首為國樂團演奏，其餘8首為樂團伴奏的歌曲，分別由梁啟慧、范揚景、林心蘋與王乙聿等4位作曲家創作與編配，在前國立傳統藝術中心主任吳榮順的開場導聆下，更能迅速抓住節目所要傳達的概念與想法。在2個多小時的節目中，帶領觀眾綜覽臺灣音樂數百年的發展，是一檔別具意義、相當用心的製作。</p>
<p>（3）桃園市國樂團</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃園市國樂團（以下簡稱桃市國）在2020年演出共舉辦13場，其中專題音樂會5場、桃園國樂節4場、竹塹國樂節1場、1場獨奏音樂會、1場作品專場音樂會及戶外演出1場。推廣音樂會中，校園巡迴演出有22場，社區演出1場，對於樂團在地生根及國樂推廣，有實質的效益。同樣受到疫情影響，桃市國於2月15、16日取消了與高市國的聯合音樂會，是目前觀察到全臺首場取消的國樂音樂會。直到5月因著疫情稍稍減緩，開始了線上音樂會的演出。6月14日開放現場觀眾採實名制入場欣賞，同時線上轉播的音樂會。日後逐漸恢復正常的演出，但為了防止疫情傳染，在進場人數上，除了採實名制外，並嚴格管控人數，這是因應疫情而不得不的權宜之計（桃市國，2020）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國今年的委託創作共有7首，其中6首是國人創作，1首是非國人創作。有3首樂曲在竹塹國樂節發表，2首樂曲在桃園國樂節發表，另有1首樂曲是與藝企合作的音樂會上發表。陳樹熙的《桃園二景》、曾翊玹的《TAOYUAN：CAPRICCIO桃園暢想曲》都是以桃園為題的作品，這正是桃市國的策略。每年邀請作曲家以「桃園」為題，為桃市國量身創作樂曲，2016年以來已經累積了8首，對形塑樂團的自有形象有絕對的助益。</p>
<p>（4）臺南市民族管絃樂團</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺南市民族管絃樂團（以下簡稱南市國）2020年的大型音樂會共有3場，其中1場是參與竹塹國樂節10週年的演出。推廣音樂會中，全臺特教學校的巡演有17場，臺南市在地學校的巡演活動有12場，配合公務的演出活動有22場，合計51場（南市國，2020）。南市國是全臺唯一至全臺特教學校演出的國樂團，以樂團現有20餘人編制，機動性強，適合校園的演出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年的委託創作有1首，係配合樂團20週年慶「喜悅臺南．靚樂二十」的活動，委請作曲家蘇文慶先生創作合奏曲《靚樂．二十》，以傳統吹打喜慶風格，洋溢著團慶歡樂的氣氛。</p>
<p>（5）高雄市國樂團</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;高雄市國樂團（以下簡稱高市國）今年所舉辦的音樂會共計16場，包含年度音樂會9場；因應疫情改線上音樂會有2場；戶外音樂會2場；參與竹塹10週年演出1場；附設青少年國樂團演出2場。11月份是高市國專門為兒少們所舉辦的推廣音樂會，對象以高雄市的中小學為主，2019年一共舉辦20場，今年受疫情影響僅舉辦6場。在委託創作方面，同樣受到疫情影響，全年只有委創1首作品，係作曲家鄭伊里為「簧之狂響」音樂會創作《生命是無止盡的流動》（高市國，2020）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年臺灣五大職業樂團面對疫情，不得不取消既定的演出計畫，但也因此開啟了線上音樂會的演出形式。透過網路的平臺，讓更多人能夠接觸到國樂的演出，對樂團而言，未嘗不是一種新的體驗。6月14日由桃市國、高市國各在南北舉行音樂會，讓觀眾進場欣賞演出，國樂的演出活動才逐漸恢復正常。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在新創作品中，共有21首（1首套曲），其中國人創作有18首，國人創作比例達85.71%。相較2019年的55首新創作品，雖然少了34首，但是綜觀全球疫情嚴峻導致封城、鎖國的現況，臺灣還能有此成績，值得欣慰！</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;為了防止疫情的傳播，所有入場聽眾都必須填妥姓名和電話，並量測體溫，且全程攜帶口罩。除此，在疫情較為嚴峻時，臺上的演出人員，除了管樂聲部外，也要攜帶口罩演奏，形成了臺上難得一見的奇特景象。</p>
<p><strong>2.</strong><strong>非職業樂團</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由公部門支持成立的非職業樂團有，臺中市客委會成立的臺中市客委會國樂團；基隆市文化中心成立的基隆市國樂團；彰化市公所成立的彰化市立國樂團；苗栗縣文化觀光局成立的苗栗縣立國樂團等，每年都有定期的演出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;苗栗縣立國樂團是公部門所成立的非職業樂團中，首先開啟在7月4日於苗北藝文中心演藝廳舉辦了「幻響」音樂會的演出，邀請李英擔任指揮。臺中市客委會國樂團則在12月12日假國立臺灣交響樂團演奏廳舉行「國樂與西樂的美麗相遇」音樂會。這2個樂團都是以推廣客家文化為主體的團隊，透過音樂來展現客家的精神與文化風貌。</p>
<p><strong>（二）民營樂團</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的民營樂團為數不少，也是國樂活動在民間活躍的主要動力來源。以下僅就小巨人絲竹樂團、新竹青年國樂團、台南樂集等3團，在2020年的展演活動提出觀察報導。</p>
<p><strong>1.</strong><strong>小巨人絲竹樂團</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;小巨人絲竹樂團為國藝會2020 TAIWAN TOP演藝團隊，演出共12場，含推廣系列演出4場，絲竹室內樂系列2場，兩岸薪傳系列2場，國際與兩岸交流3場，網路音樂會1場。因疫情取消7場，其中3月27日的場次已延至7月31日演出，6月14日場次將延至2021年3月21日演出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國際與兩岸交流部分，小巨人絲竹樂團於1月31日至2月2日至加拿大參加「PuSh國際表演藝術節」跨國現代音樂、現代舞共同製作《飛白》演出3場。原定2020年9月參加墨西哥克雷塔羅國際音樂節、Visiones Sonoras藝術節及墨西哥城「國際新音樂論壇」的4場巡迴表演延至2021年；預計10月參加波蘭新藝術合成器藝術節（NeoArte Synthesizer of Arts Festival）的2場表演，亦延至2021年。今年的委託創作有2首，分別為鄭光智創作的《雪霜薺麥》及徐瑋廷創作的《傾聽我說》（小巨人絲竹樂團，2020）。</p>
<p><strong>2.</strong><strong>新竹青年國樂團</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;新竹青年國樂團（以下簡稱竹青）是具有極高演奏水準的民營樂團，2020年持續獲選為新竹市傑出演藝團隊。2020年竹青一共演出6場，其中4場屬於竹塹國樂節、2場「竹青之星」音樂會。在暑假期間舉辦1場竹塹國樂營（竹青，2020）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年正逢竹塹國樂節10周年慶，在疫情受到控制下，擴大舉辦活動。因著疫情的因素，今年沒有邀請國外的演出團體，全由臺灣的表演團隊擔綱。其中五大職業樂團是國樂節的核心團隊，竹青更是肩負起開、閉幕音樂會的重任，其餘來自臺灣各地及在地的團隊與學校社團參與活動，熱鬧非凡，也讓愛樂者在疫情期間，有一個抒發情感的出口。總計有18場的節目，其中2場是專題演講。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;竹青由來自臺灣各大專院校菁英所組成，素質佳，認同度高，已經連續多年獲選為新竹市傑出演藝團隊的第一名。目前正積極規劃樂團的轉型，邁向職業團隊而努力。</p>
<p><strong>3.</strong><strong>台南樂集</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台南樂集成立於2014年，主要發展方向為1.藝術性的「年度製作」2.公益性的「關懷演出」3.偏鄉紮根「育成計畫」4.累積樂團專屬作品的「委約創作」，並於2018-2020年連續榮獲「臺南市傑出演藝團隊」。今年的演出有2場，8月22日「鷲嶺傳奇」與12月19日「我們的島，飛天鑽地」，並至2間學校進行推廣音樂會的演出（台南樂集，2020）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;委創樂曲有6首，其中5首都在「我們的島，飛天鑽地」音樂會中發表。邀請臺灣青年作曲家為臺灣特有的動物譜曲，此5種分別為：臺灣蝶蛾、櫻花鉤吻鮭、石虎、藍鵲與雲豹。演出的反應熱烈，讓觀眾不僅能欣賞音樂，更能藉由音樂會的提醒，愛惜我們在地珍貴的資源！</p>
<p><strong>（三）個別樂器族群的表演生態</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在臺灣除了以國樂團的形制營運外，近年來以族群樂器成立的團體，逐年增加中。以古箏組成的團體有：大觀箏樂團、台北正心箏樂團、風雅頌古箏樂團、谷方當代箏界等約22個單位；以彈撥樂器為主體的有：台北柳琴室內樂團、琵琶雅集、台中琵琶樂團等；以揚琴為主體的有：台灣揚琴樂團、蝴蝶琴室揚琴樂團；以打擊樂為主體的有：天鼓擊樂團、紘采樂集等；還有以笙、胡琴或者不同樂器組合的形式，形制多元且有優質的演出表現，為臺灣的展演活動增添色彩。</p>
<p><strong>1.</strong><strong>台灣揚琴樂團</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台灣揚琴樂團是臺中市的傑出演藝團隊，由揚琴演奏家鐘泉波成立。2020年總計27場演出，其中9場為售票演出，18場為推廣性質演出。受到疫情的影響，樂團蟄伏了半年之久，在僅有的半年期間，樂團繳出了27場的驚人數字，顯見在團長鐘泉波的帶領下，樂團兼具專業及強大的活動力。今年的委託創作有3首，都在12月26年度音樂會「花現琴韻II－台灣揚琴合奏作品之夜」演出。曲目分別為何立仁創作的《雨港寫意》，羅正楎創作的《山居秋暝》及陳傳禹創作的《浪嶼》。作曲家迎合台灣揚琴樂團的宗旨：創作作品兼併傳承與創新，以本土素材，探索臺灣音樂文化，創造揚琴演奏新聲為使命（台灣揚琴樂團，2020）。這些新創作樂曲，為台灣揚琴樂團創造出不同的樣貌，也讓觀眾一睹揚琴合奏編制在不同作品中的展現。</p>
