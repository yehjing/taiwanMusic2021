    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;於8月起，名人堂花園大飯店以「名人堂Live
      Show」為主題，策畫了一連串的表演藝術節目，每週安排不同樂團進行演出，如海風樂團、西尤島樂團、CMO樂團、連珮如與漩指樂團等，樂團在圓形的劇場舞臺中央進行演出，位於環繞舞臺觀眾席的民眾可自由飲食，同時也能欣賞到精采豐富的音樂演出，滿足不同感官的需求。10月起則以「微醺之夜－音樂現場」為主題於每週五、六安排音樂演出，除了前述所列出之樂團外，也邀請到跨越語言與族群的大地之歌樂團，進行最新音樂專輯《行樂千山－Gaia
      Formosa》的現場發表會。
    </p>
    <h4><strong>四、觀光、產業與音樂展演的結合</strong></h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;戶外的展演與觀光的結合，為下半年音樂展演活動的方向之一。疫情期間民眾無法出國，國內的觀光活動因此更加興盛。在發展國內觀光產業之餘，樂團同時受邀至觀光景點、活動進行演出，讓音樂能跳脫制式的演藝空間與形式，更加融入民眾的生活，不再被束之高閣。於各縣市舉辦的在地藝術節，打破舞臺上、舞臺下之間的藩籬，增加了不同領域從業者之間的交流機會，也讓表演藝術不只能是被展示與欣賞，更實際與社會產生互動關係。隨著國內疫情趨緩，藝文活動的舉辦也逐漸解禁，然而，下半年的展演方式仍以戶外大型活動為主，為了豐富節目內容，不同音樂類型的樂團齊聚一堂，展現出樂團的特色，同時也互相交流，並不會侷限在單一的音樂類型。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，在端午連假中由文化部與中華文化總會於總統府前廣場舉辦的「藝文大匯演」<sup>5</sup>，匯聚了音樂、舞蹈、戲劇等不同領域共60多組的表演團隊，目的是希望透過將藝文工作者帶回舞臺，重新喚起表演藝術界的活力，並提振大眾參與藝文活動的信心。活動會場同時也有各式各樣農產品相關展售，讓民眾能同時以實際行動支持農業、藝文產業。以異國音樂文化為創作素材的海馬樂團受邀演出，這場演出除了是疫情影響的幾個月之後，樂團的第一場公開演出，也是樂團第一次在總統府前廣場將音樂分享給觀眾。樂團以《旅人．出走》作為起點，在《草山呼麥》中透過馬頭琴與呼麥的聲響將蒙古草原的壯觀景色呈現在觀眾面前，隨後又翻過阿爾泰山脈來到神祕的《香料王國》中亞、土耳其，並透過《聖戰》期許大家都能打完面對疫情的這場戰役，最後一首曲子《旅人》用雙嗩吶模仿蘇格蘭風笛的聲音，前進涼爽的蘇格蘭高地；穩定持續的大鼓聲猶如旅人愛冒險的心，一步步往前走；蘇格蘭風笛象徵遠方的風及希望的光，大家最終都會循著旅人的步伐找到屬於各自的回家之路。海馬樂團以多首精彩曲目，將嗩吶、琵琶、胡琴，搭配上蒙古的馬頭琴與呼麥，以及來自世界各地的打擊樂器，透過各種樂器交織出的聲響，用音樂帶著觀眾擺脫疫情的限制，飛離潮濕悶熱的臺灣，優游於世界各地。透過不同樂器的聲響描寫蒙古、中亞、蘇格蘭等景色，目的是為了在無法出國的時期，讓民眾能夠擺脫疫情的限制，隨著音樂環遊世界。
    </p>
    <h3><strong>貳、疫情對於臺灣世界音樂發展上的衝擊</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在今年初，中國武漢爆發了武漢肺炎疫情並且擴散至世界各地。在嚴峻的疫情影響下，許多國家皆採用限制出入境的方式來避免疫情持續擴散。而限制出入境除了導致全球經濟陷入停滯狀態，國際間的藝文交流活動也同樣受到衝擊；絕大部分有邀請海外演奏家或樂團的音樂相關活動皆必須延期甚或取消。而在臺灣方面，儘管防疫上取得重大成就，但在疫情之初也同樣因諸多防疫措施，直接或間接地致使諸多室內音樂活動取消。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;世界音樂在臺灣的推廣，不只是需要臺灣對世界音樂愛好者努力的學習與持續不斷地發展，每年藉由邀請海外知名演奏家或團體前來開設講座或者舉辦音樂會，更能有效且直接地闡述世界音樂文化的原真性與多元性。而這些原本已經常態化的推廣模式，卻因武漢肺炎疫情的蔓延與惡化，導致許多來自海外的交流與演出的中斷與停滯，甚至受到直接衝擊與影響。
    </p>
    <h4><strong>一、疫情對臺灣世界音樂發展面向之衝擊</strong></h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;武漢肺炎疫情對臺灣世界音樂發展所帶來的影響，最明顯的即體現於部分在推廣、傳習世界音樂的大專院校。以2020年的情況而言，諸如國立臺灣大學音樂學研究所、國立臺北藝術大學傳統音樂學系以及國立臺南藝術大學民族音樂學研究所等相關系所，皆有定期邀請海外專家學者、展演團隊前來進行世界音樂及其跨界展演之相關講座、工作坊或音樂會。由於大專院校作為高等教育及學術場域，所策劃之活動仍必須具有一定程度的教育及學術性質。因此，在規劃異文化音樂相關活動時，往往會聘請經歷豐富且具有相當知名度的專家學者。為此，在防疫政策的限制下，除了相關活動之次數有著明顯的下降之外，實際執行此類活動者也幾乎以臺灣在地的演奏團隊為主。此外，活動的時間也幾乎都集中在下半年，臺灣已確實地將疫情防堵後才開始逐一執行。在此之前，部分大專院校則會以系所內的師生為主體來規劃相關活動。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大專院校之外，也有部分專職於異文化音樂推廣、展演的在地世界音樂團體，因疫情衝擊而使得過去所習慣的推廣模式受到影響，而造成影響的原因主要來自兩個面向：針對出入密閉式公共場所進行管制及室內容納人數限制；入境居家隔離規定及各國出入境限制。以第一個面向而言，除了諸多密閉式音樂廳或者展演空間，配合政策選擇以停止營業或取消大多數展演活動的方式來避免人潮聚集之外，部分表演者及團體也會配合防疫政策決議自主取消活動。這對於那些每年皆會以音樂廳為主要場地辦理一至二場年度重點演奏會的團體而言，無疑是不小的打擊。而從第二個面向來看，各國出入境管制所導致的國際間交流的停滯，則影響到音樂家例行前往他國交流之行程，或者是在地世界音樂團體於疫情期間，對於邀請海外演奏家或者樂團前來也會有許多顧忌。上述原因對臺灣在地世界音樂團體造成的影響，尤以日本邦樂為主要推廣對象的樂團及協會－華之和音、台灣廣原會（圖3）以及臺灣雅三弦會－所受到的衝擊最不容小覷。因為舉辦邦樂音樂會的場所，目前仍以音樂廳為主，這不僅使日本音樂在離開母國至異地後，仍能維持本身音樂之特質，並建立其在異國文化中的能辨識度。而上述樂團及協會又基本繼承了日本邦樂文化中的師徒制度，儘管在非日本的異國發展，也仍維持著與母國強韌的聯繫，並且受到流派的規則與慣習所制約。為此，透過邀請他們的「師父」及「師兄」定期在臺灣演出及授課，除了能維持日本邦樂在臺灣的特質外，也能藉由與文化發源地的連結。而疫情的爆發間接使得原先的推廣策略無法持續進行，已習慣於這類模式的在地世界音樂團體則變得無所適從，最終止得選擇停止運作直到疫情緩和或結束。
    </p>
