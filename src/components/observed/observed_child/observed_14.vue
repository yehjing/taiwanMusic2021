    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一年一度的「臺北市中山堂廣場音樂節」<sup>1</sup>於4月份登場（圖1），以開放式的戶外舞臺作為展演空間，邀請不同類型的音樂團體進行演出。作為開場主題的「傳藝跨界」，由「聲動樂團」及「絲竹空爵士樂團」揭開序幕。聲動樂團在本場演出共帶來9首原創歌曲，融合豐富多元的民族樂器，除了臺灣傳統樂器二胡、中阮、柳琴、鑼鼓，還有中東手鼓、印度笛（bansuri）、口簧琴、手碟鼓（Rav），搭配人聲的演唱與肢體的律動，將這塊土地蘊含的豐沛生命力，創作成一首首的樂聲和歌唱。絲竹空爵士樂團在本場演出共帶來8首不同風格的曲目，並以爵士樂的演奏方式與節奏旋律重新詮釋大家耳熟能詳的歌曲。音樂節的其他主題分別為「天籟古典」、「自由流行」及「炫技電音」。雖然主辦方並沒有提供線上觀賞表演的途徑，但部分樂團會自行透過粉絲專頁等線上平臺進行直播，讓無法到場的樂迷也能同步觀賞。在活動現場，觀眾之間、觀眾與舞臺間的距離因防疫措施而必須拉開，減少彼此之間的互動，但各個樂團仍透過音樂傳遞熱情並表達對醫護人員的感謝。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5月起，全聯善美的文化藝術基金會於國立傳統藝術中心舉辦為期2個月的「大聲音樂季」<sup>2</sup>，每週平日、假日分別邀請共28個臺灣的獨立樂團，其中還包含了金曲獎、金音獎、金馬獎等入圍與得主樂團。演出場地位於半戶外的臨水劇場，演出進行的同時，也能夠接觸到大自然；而假日場次透過臉書直播的方式，民眾就算無法親臨現場，也能讓更多人參與這個音樂盛會，欣賞臺灣多元的音樂文化。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;線上直播音樂會雖然打破了時間與空間的限制，但無法完全滿足觀眾對於現場觀賞體驗的需求。因此，隨著6月份臺灣疫情解封之際，各縣市政府也陸續舉辦大型藝文活動，結合在地物產市集，活絡藝文與經濟的發展。6月底首先舉辦的是為期1個月的嘉義藝術節，以文化局、博物館、嘉義製材所、阿里山森林鐵路車庫園區周邊場域與音樂、戲劇、舞蹈等表演藝術做連結。擔任開場演出團體之一的聲動樂團，樂團為觀眾帶來多首樂團原創樂曲，而本次演出也是在疫情解封後，聲動樂團的第一場公開演出。在樹林環繞的會場中為觀眾帶來自創的樂曲，而以大自然聲響為素材的樂曲讓觀眾能在解封之際更貼近大自然。演出場地周圍樹林環繞，舞臺後方不時經過的阿里山線火車，也為樂曲增添了環境聲響，讓觀眾有不同以往的聆聽經驗。
    </p>
    <h4><strong>二、多樣化的展演場域與形式</strong></h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;具有獨特音樂靈感的高雄鼓道場<sup>3</sup>，以來自街頭與國際上的交流和旅行作為創作的能量，音樂元素包含了澳洲Didgeridoo、日本太鼓與非洲鼓，並搭配人聲吟唱，試圖突破大眾對於打擊樂的框架與想像。樂團經常於高雄的愛河、旗津等河畔景點、市集活動進行演出及舉辦工作坊，並與加一吉他、Stereo平衡訊號等樂團進行交流共同演出。此外，樂團於「中華民國109年國慶酒會」中演出作品《燈光》，與來賓分享樂團的創作。高雄鼓道場認為，街頭是能見度最高、感受度最直接的大舞臺，希望能在這個每天行人往返的地方展現街頭音樂的魅力。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以擊樂為基底，加入生活創意、臺灣在地傳統元素調味，呈現多元的演出形式的鼓玩擊樂團，運用專業演奏手法將樂曲創意地揉合再生，並融合臺灣在地文化，演繹豐富多元的打擊樂，傳達出「一天的日常，事件的發生，總是伴隨著各種聲響」的概念。樂團多次於礁溪長榮鳳凰酒店演出，並在暑假期間舉辦打擊樂營隊進行推廣。此外，樂團也在國立故宮博物院南部院區舉辦的「邂逅南院－故宮下午茶」活動中進行演出，並加入手碟（Handpan）一同演奏，增加打擊樂的豐富度。在10月份由桃園市政府客家事務局主辦的「愛客音樂展演」中，鼓玩擊樂團與客家創作音樂人－劉慧卿、小丑街舞表演團體－缺席舞團3個不同領域的表演團體跨界共同演出，觀眾同時是觀賞者也是表演者，打破了表演者與觀眾之間的界線。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;嘗試在「非典型展演空間」進行演出的藍眼淚樂團，以古箏、鋼琴、合成器、氛圍電吉他、電子聲響為編制，融合了古典、現代、傳統、電子等音樂，並搭配多媒體、影像與聲響設計，將自然聲響與場域互動視為創作手段的核心。2月底，樂團在寶藏巖防空洞進行了為期兩天的實驗音樂計畫「聲軌製造」，以戰爭、自由為主題，透過器樂與電子聲響，創造出另類的聽覺體驗。5月份，樂團延續了過去在花蓮慶修院、多羅滿夜間賞鯨航班執行的「空間聲響計畫」，首次將樂團的音樂帶進美術館，透過當下的氛圍與環境聲響的結合，創造出特殊的感受。8月份，樂團在澎湖舉行了為期13天、演出10天的音樂計畫「島嶼上的人」，透過4種演出型態及5個非典型展演空間，與在地的店家合作，結合歷史建物、電影映畫、眷村文化、文學，拓展了音樂展演的場域空間，為聽眾創造出不同的感官體驗，探索聲響運用的可能性。樂團也在雲林虎尾鎮的虎尾建國眷村展出「人生故事屋」，將音樂裝置結合歷史現場，讓民眾能體驗到眷村的文化。
    </p>
    <h4><strong>三、地方性的藝文活動與樂團</strong></h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8月份臺南市政府在河樂廣場規劃一系列的活動，其中包含樂團與市集的「樂團輕輕唱
      X
      米街遊河樂市集」，樂團在河樂廣場東側水域上的舞臺表演，民眾可隨意坐在池邊斜坡、迴廊或佇立於地面層觀賞，舞臺前方除了規畫讓民眾席地而坐的區域，另有「吃米不知米街」市集，以各類手作品牌為特色。於「樂團輕輕唱」節目中演出的3個樂團都各具特色，分別是融合多種器樂聲響與風格的甜約翰Sweet
      John、為臺灣土地創作的Cicada樂團、由阿美族青年組成，透過實驗音樂、原住民古調、爵士音樂及各類型曲風關注生命、環境與人文議題的CMO樂團（圖2）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由宜蘭人組成的「A
      Root同根生樂團」以在地文化作為創作素材，用音樂描寫宜蘭的地景，刻劃出北管、泰雅、客家等臺灣音樂素材的當代光景。暑假期間，樂團主要於宜蘭觀光景點及相關活動進行演出，如宜蘭國際童玩藝術節、宜蘭中興文化創意園區「藝術自造祭」、蘭陽博物館「2020蘭博四季音樂節」，樂團也與不同類型的表演藝術團體合作：8月份，樂團受邀至桃園大溪於「大溪大禧」中與當地的新勝飛龍團跨界共同演出；1月份與馬戲類型演出團體Eye
      Catching
      Circus創造焦點合作，在《春祈．年年好》節目中進行配樂，共同賦予臺灣傳統藝術新的生命。樂團也於第三十一屆傳藝金曲獎頒獎典禮表演節目－《薪聲．祈福》中與青年傑出戲曲演員及傳藝中心駐團演訓習藝生共同聯演。《薪聲．祈福》源自北管的新創扮仙作品〈鬧三仙〉，傳達人神之間的親近與共生，也象徵對神明祈求平安及祝福的心願，而樂團與傳統戲劇的跨界合作，讓新世代傳統藝術傳承者將其青春豐富的能量與生命力注入傳統藝術，展現臺灣各劇種薪傳希望。
    </p>
