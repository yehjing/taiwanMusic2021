    <h3><strong>前言</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年的新型冠狀病毒（COVID-19，簡稱武漢肺炎）不但對於全世界的健康、政治、經濟、社會、文化產生極大的衝擊，對於表演藝術領域而言，也改變了整體表演藝術的生態。臺灣在此次疫情的控管上有著極為傑出的表現，在政府與百姓的相互配合之下，成為全世界疫情管控的表率，而受到世界各國的肯定。雖然，臺灣已經將疫情所造成的傷害減到最低程度，但也因為疫情的關係，導致臺灣百姓的生活方式受到很大的衝擊與改變。當然，這樣的衝擊與改變對於臺灣音樂環境的發展也產生極大的影響，不可否認地，許多的演出被迫取消或延期、部分國外團體無法來臺、演出方式從現場改為網路進行等，這些現象都在在地改變了臺灣表演藝術的生態體系與演出環境，而線上演出也開啟了表演藝術的另一種可能性。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這樣的改變對於跨界、世界與新移民音樂而言衝擊應該是特別嚴重，終究這幾種類型的音樂型態本身與國外的連結十分密切，許多演出必須依靠來自海外的資源，當海外演出人員無法來臺的情況下，也就只能依靠大量的臺灣本地團隊現場演出或者以網路方式來進行。
    </p>
    <h3><strong>壹、疫情下的跨界音樂演出與特性</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不同形貌、甚至是不同範疇之各種文化之間的交流，經常需要耗費漫長的時間，而在這段過程中，這些音樂文化其內涵也必然會互相接觸、衝突、吸收，進而衍伸出不一樣的形貌，甚至形成一種新的形式，並受到人們的接納與應用，這段音樂文化交流的過程正也是跨界音樂發展的特性。因此，「跨界」並非一個特定的結果，也因此我們或許也可以說「音樂的跨界、融合、重組其核心的價值與特性就在於過程的呈現。」
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在疫情持續地影響之下，民眾不僅無法出國、甚至表演者欲規劃過去常態舉辦的室內展演活動都有相當的難度。人群聚集的活動大幅度減少，藝文活動也連帶受到影響。在這樣的情況下，線上直播音樂會逐漸興起，提供表演者另類的展演方式，也讓觀眾獲得不同的觀賞體驗。無法進行線上直播演出的團體，也會透過錄製影片的方式，與聽眾分享音樂創作並透過網路進行互動。然而，線上展演的方式仍然缺少臨場觀看的氛圍，因此，隨著下半年國內疫情趨於穩定，藝文活動從暑假起逐漸回復常態，戶外的展演活動也陸續增加，地方政府在防疫的同時也積極活絡藝文活動的發展。隨著暑假的到來，各個文化相關場域如博物館、美術館等邀請不同樂團進行演出，讓民眾能夠同時接觸到不同領域的藝文展演，進一步吸引人潮。
    </p>
    <h4><strong>一、線上音樂會、大型展演活動</strong></h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年年初，由於武漢肺炎的影響，為減少人群聚集、降低疫情擴散的風險，許多大型藝文活動因此暫停舉辦或延期；為了能讓民眾不用出門也能觀賞到藝文表演，網路成為藝文表演者突破重圍的利器。有別於以往的現場演出，線上音樂會、直播等方式成為藝文表演者新的展演媒介。透過科技的輔助，各式展演活動得以打破時空的限制，吸引更多的觀眾參與其中。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自今年4月起，趨勢教育基金會策劃了「2020趨勢說演劇場－胤錞彈說國樂」系列節目，邀請音樂家陳胤錞擔任音樂總監，規劃安排了10場YouTube直播線上音樂會，於每場次音樂會做完整介紹與說明。除了以國樂樂器組成的「紘采樂集」、「樂x5
      胡琴重奏」之外，也邀請許多不同音樂類型的演出團體，如演奏手碟的「蒐SoulDays
      Studio」、曾獲傳藝金曲獎兩項入圍的「海馬樂團」、由宜蘭音樂人組成、融合爵士即興與世界音樂的「A
      Root同根生樂團」、以臺灣歌仔調或是童謠民歌為創作素材的「木子mooz」、以當代思維重塑傳統音樂感官體驗的「三個人樂團」、以即興演奏為主軸的「隨心所欲樂團」、融合運用臺灣各種傳統聲景的「栢樂座」。在線上音樂會的過程中，樂團並非單純演出，而是穿插樂器介紹及樂曲解說，讓民眾能夠認識到不同的音樂文化，同時也向觀眾傳遞了對音樂的想法與創作的理念。
    </p>
