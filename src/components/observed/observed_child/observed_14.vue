      Camara）<sup>8</sup>的家族背景便為世襲的音樂世家，能演奏非洲木琴（Balafon）及豎琴（Kora）、非洲金貝鼓（Djembe）等等。因為臺籍妻子，舞者李芳伶到幾內亞學習非洲舞而認識毛慕沙，婚後毛慕沙第一次出國即來到臺灣，在北部地區教授非洲鼓，並與李芳伶成立「聲唄（Senbe）鼓舞樂團」推廣非洲鼓樂與舞蹈藝術。來自日本的新住民志甫一成<sup>9</sup>是專業的三味線演奏者，2011年日本311震災後來到臺灣，開始三味線的演奏、教學與推廣，並認識臺籍妻子，在屏東、高雄的新住民市集常可見志甫一成表演三味線，妻子美緒老師則在攤位繪畫的搭擋。出生於牙買加，留學於英國的新住民聖潔豐與臺籍妻子聖謝承芳定居於屏東，平常擺攤販售自製的牙買加傳統食物，當有新住民活動時也一起熱心參與。聖潔豐混合中英文，在節奏動感的音樂或英文老歌如"Singing
      in the
      Rain"中即興演唱，鼓勵人在不如意的環境中也要開心。此外兩人也舉辦各式使在地人開始關心公共事務、國際議題的活動，試圖影響人不只顧自己的生活，更能關心周遭及國際的議題。
    </p>
    <p><strong>（二）新住民二代</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;越來越多新二代在臺灣公眾領域的展演嶄露頭角，其中有些人是透過新住民的文化展演活動登臺表演，新二代們越來越能夠以表演藝術形態來展現自己父母原來國家的文化。透過屏東新住民市集上臺表演的有越南新二代劉威廷，和媽媽上臺一起唱越南歌。印尼新二代林華鈺、林華軒姐妹自小習舞，在媽媽指導下配合印尼傳統音樂進行印尼傳統舞蹈演出。加拿大新二代查以樂流利演唱西方流行歌曲。泰國新二代林炘蓉、林家妤姐妹回到媽媽的母國泰國學習泰國樂器，並時常一起在屏東多元文化市集中表演泰國木琴與揚琴合奏。參加相同計畫，學習泰國揚琴的新二代程湘閑則固定在高雄新住民多元文化市集演出。專業的新二代表演者則有菲律賓新二代呂曉倩、呂曉清姐妹組成的團體「呂曉曉」，姐妹二人在臺灣各綜藝節目、歌唱比賽均表現兩眼，平時也在定點駐唱，並常受邀至新住民相關活動演出。
    </p>
    <p><strong>（三）在臺外籍移工、學生與志工</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在新移民文化展演中時常可見到許多外籍表演者，這群表演者或為移工、學生、志工等身份。若新移民文化活動中缺少這一部份人參與，可能無法成就如此多元精彩的成果。外籍勞工是這一群體中人數最多的。印尼移工團體Singo
      Barong
      Taiwan成員來自印尼東爪哇省，在日常工作之餘，以來自印尼的樂器、舞蹈面具等展演東爪哇著名的Reog舞，並常受邀至各式印尼文化活動演出。此外也有短期來臺留學的學生，例如文藻外語大學邀請來臺唸書的越南學生，同時也是歌手的山姆（Sam）在高雄新住民市集演出；在淡江大學修習碩士的伊朗人米蒙德（Ali
      Montazami）在妻子Mona Kaveh
      Ahangari來臺後二人創立介紹伊朗音樂為宗旨的Mastaneh樂團，受邀至學校、政府活動中表演、講習。具有基督信仰背景的韓國好消息宣教會成立的國際青少年聯合會（IYF），幫助參與的各國青少年進行為期1年的海外志工服務體驗。該團體踴躍參加各地的新移民活動，除了表演韓國傳統與流行的音樂舞蹈與跆拳道外，也設置攤位安排韓服體驗、韓式美食分享，其志工來自韓國、秘魯、臺灣等地。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以上簡介今年活躍的一部份新移民表演者。事實上很多新移民表演者在表演活動當中，僅有主持人極為簡短的開場介紹，無法深入了解這些新移民的背景介紹。此一深度不足的問題藉由民間各類媒體對這些新移民的深度採訪而改善，但採訪的時效隨著時間流逝而變低，無法反映該表演者之後所經歷的變化。
    </p>
    <h4><strong>二、</strong><strong>常態性</strong><strong>新住民多元文化市集活動</strong></h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;過去在新移民的表演藝術活動中、大多屬於配合某些節日或臨時活動的單次性演出，然而這些年來隨著長住臺灣的新住民人數的增長，也使得新住民活動也慢慢轉向常態化的現象。這些市集結合了美食、服飾、音樂、舞蹈等形式，來呈現來自不同國家的新住民多元文化，其中以屏東「多元文化講座及創作市集」最具歷史，不但內容豐富，參與人數眾多。也由於屏東「多元文化講座及創作市集」的成功舉辦，也促使了其他縣市開始舉辦類似的活動。
    </p>
    <p><strong>（一）</strong><strong>邁向第五年的屏東新住民市集</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;新住民市集運作良好的先例應以屏東新住民市集為代表，該市集從2016年開辦，每月第二週星期天固定於屏東縣孫立人行館舉行，現由屏東縣政府委託社團法人屏東縣好好婦女權益發展協會舉辦。市集提供攤位讓新住民販賣來自家鄉的食品、料理，或自家種的農作、手工藝品等等，提供新住民與臺灣本地人交流的平臺。同時，也有政府單位做政令宣導，或有東南亞移工關懷團體介紹東南亞各國文化的攤位；活動單位並設有專人協助看管的育兒區。上述攤位分散於孫立人行館入口、庭院處，而靠近行館處為一廣場，廣場前方的石造平臺在市集過程中會有主辦單位安排的表演，同時也在行館內廣場中設有塑膠凳，遊客能坐著一邊享用美食一邊看表演。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;參加表演的團體或個人多為屏東的新住民及新二代，也有屏東在地的演出團體表演音樂或舞蹈。演出者每月時而重複，但在演出內容上會做改變。此市集會由每月固定主持表演的主持人越南籍新住民胡清嫻串場，為演出者做詳盡的介紹。有趣的是胡清嫻在表演尚未開始前，或表演暫時休息時，會拿擴音麥克風到市集各攤位跟各攤互動，市集因此充滿其介紹、笑聲的聲響。為表演而播放音樂的音響通常將音量開到極大，可傳到孫立人行館外，招攬遊客之際也烘托出市集熱鬧的氣氛。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從實際運作看來不少團體或個人，如「臺灣安克隆樂團」（前身為屏東安克隆國際親子樂團）、林華鈺與林華軒姐妹，每月都能上臺表演。這對表演者而言是穩定培養上臺經驗的機會，長期下來可看到表演者的成長。此外，在整個下午近4小時的表演時間中，有專業表演者，也有業餘表演者，甚至是孩童的演出。整體而言表演品質並非總是接近專業，而是呈現在地新住民或居民的興趣及專長。但因為活動主題為新住民市集，具新住民或新二代身份的表演佔多數，也會被安排在較前面的時段。即使沒有像後起之秀高雄新住民市集具有每月一主題的規劃，這樣豐富的展演仍然是屏東新住民市集重要的特色。
    </p>
    <p><strong>（二）</strong><strong>高雄新住民多元文化市集</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由文藻外語大學主辦，固定每個月第三個星期天在高雄橋頭糖廠「百世新天地」的高雄新住民多元文化市集（圖6），自今年3月起開始舉辦，4月時雖因武漢肺炎疫情停辦一次，仍得以在本地疫情受到控制後恢復進行。市集活動從上午11點到下午3點邀請南部地區來自各國的新住民以本國特色的文化擺攤，販售食品、藝術品或讓遊客進行文化體驗。市集在不同時段亦安排以新住民為主體的文化展演，內容除舞蹈、音樂外也有與觀眾互動性質高的啤酒大賽、魔術秀等。每月的表演也由來自不同國家的新住民擔任主持人，各人介紹表演均有不同的切入角度及語言使用。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;實際上新住民市集是文藻外語大學「藻到新力亮－文藻USR新住民新移工關懷增能計畫」的一部份，除了舉辦實體的市集，此計畫也透過臉書粉絲專頁不定時分享關於世界各國的「原來如此冷知識」，介紹擺攤的新住民朋友及其攤位特色，為網友直播市集展演，甚至能將每月預告市集舉行的中文文宣翻譯成英、越、印尼文。文藻外語大學也動員校內東南亞學系學生擺攤，學生社團如文藻印尼文化研究社、文藻嘻哈文化研究社、文藻拉丁打擊樂社亦受邀表演，也因疫情緣故安排學生為訪客量體溫、消毒等。
    </p>
