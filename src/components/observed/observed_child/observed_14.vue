    </p>
    <h4>
      <strong>二、</strong><strong>防疫行動下</strong><strong>世界音樂演出場域與平臺的改變</strong>
    </h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在防堵疫情的相關政策下，無論這些場所是否有直接受到政策的管制，在地世界音樂團體對於使用原本所習慣的展演場域產生諸多疑慮。為此，尋找並轉移至其他展演場所即成為在地世界音樂團體無可避免的途徑。以經濟活動逐步復甦的4月而言，許多大型商場規劃了特色銷售活動，而其中部分活動會以觀光或異國文化為主題。例如，4月在臺中港Mitsui
      Outlet
      Park舉辦的「和風文化」系列活動，即邀請了華之和音及津輕三味線台灣廣原會前來演出，以此點綴作為活動主軸的日本系列商品展售會。類似的活動尚有於京站時尚廣場舉辦的「High
      Fun
      日式祭典」，也邀請了津輕三味線台灣廣原會前來進行展演暨體驗活動。由於疫情的影響，人們無法出國旅遊，因此這類異國文化展售活動通常會伴隨著「即使無法出國也能感受異國風情」相關的論述進行規劃，參與演出的團體則基本依循著該脈絡，盡可能選擇可以直接體現該國文化的曲目。像是在上述以日本文化為主軸的展售會，通常會演奏知名日本古曲「さくら」或者演歌類的曲目，接著再看該團體本身係以哪項日本樂器或樂種為推廣主體，再進一步展演能夠讓現場聽眾感受到日本風情的，又同時符合該樂器、樂種之代表性曲目。以津輕三味線台灣廣原會在京站時尚廣場的展演為例，或許現場許多觀眾並不知曉津輕三味線的「津輕」所指為何，但展演者能藉由像是《岩木》、《りんご節》、《津軽甚句》、《あいや節》、《じょんから節》、《花笠音頭》、《俵つみ唄》等日本東北地區民謠，直接呈現津輕三味線音樂特質的同時，又讓觀眾能夠感受到現場「音聲」與展售會「主題」的關聯性。而華之和音在臺中港Mitsui
      Outlet
      Park的演出，則配合著現場佈置的櫻花林裝飾，直接演奏「邦樂」這種充滿濃厚日本氛圍的樂種，並配合不同作品的標題，像是呈現日本春天光景的樂曲《春の海》、《春うらら》等，藉此符合「和風文化」、「櫻花季」等主題。透過上述案例可以發現，透過與特定主題之商業類展售活動進行合作，部分在地世界音樂團體仍可於疫情期間維持他們的演出活動。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了參與展售會之外，諸如小型藝文空間、餐廳，也成為在地世界音樂團體在疫情期間規劃、推廣及展演活動時的選擇。像是以印度西塔琴（Sitar）的拉格（Raga）形式配合電腦混音進行演奏為主軸的西尤島樂團，即以「西塔琴吟遊現場」為概念，在多種不同形式的藝文空間演出。在這些藝文空間演出時，經常會伴隨著諸如新書發表會、靜態藝術展覽等活動，這些場合不僅較不需要考慮是否會有觀眾前來的問題，也較不需自行規劃防疫相關措施。而這類配合其他類型活動的音樂演出，相較於異國商品展售會類型的活動則較少直接與母國呼應，又或者，直接在音樂中融入該異文化音樂的特質、形式。以西尤島樂團的例子而言，展演內容大多是以演奏者自身的創作發想來進行規劃，像是以「睡覺時夢到自己在宇宙游泳」、「孑孓在積水中自由的游泳」為發想；或者是以過去的旅遊經驗，像是「在馬來西亞遇到大雷雨」的經驗或是過去在恆河附近居住的經驗&hellip;&hellip;等，除了「恆河」可能使人聯想到印度外，其他作品的內容構成則以創作者自身的經歷為主，除了演奏以西塔琴為主之外，基本不會使人連結至印度文化，然而作品本身的基底卻能夠符合西塔琴的拉格形式。從西尤島樂團的案例可以發現，透過在小型藝文空間進行展演，演奏者更能夠呈現自身所欲呈現，同時巧妙地活用異文化音樂元素。除了小型藝文空間外，與特定餐廳咖啡廳合作也成為其中一種模式。像是津輕三味線台灣廣原會，在原本要舉辦的7週年音樂會因疫情取消後，即改以在居酒屋定期演出，作為今年乃至長期的重點活動之一。由於場所「居酒屋」直接呼應了日本文化，因此展演內容即可直接以津輕三味線的樂曲為主，輕鬆用餐的環境也使展演者能夠安排與觀眾互動的環節，使之能夠直接體驗日本東北風土民情。而作為飲食場所也因經常會有不同的客人進出，藉著如此環境也能讓更多人接觸到津輕三味線乃至日本音樂文化。而馬儁人老師的中東鼓樂團在2020年底也開始與臺北的土耳其咖啡館合作，為土耳其咖啡香氣創造中東異國情懷。從以上的案例中我們可以看出，無論是小型藝文空間或是餐廳，其聚集人員較少、現場氣氛輕鬆、經常無需配合特定主題或地點本身即符合特定主題之性質，在疫情的影響下也會成為部分在地世界音樂團體的替代選擇之一。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;透過在社群媒體分享演奏或者是簡單的教學，則幾乎被大部分在地世界音樂團體所採用；而部分過去即有將社群媒體作為宣傳、活動分享媒介的團體，在疫情期間則部分改變了其中內容。像是安地斯樂團，過去基本以分享他們活動現場的側錄或隨錄影片為主，然而在疫情期間，大部分商業演出活動皆停擺的情況下，以往的做法變得不可行。安地斯樂團於是採取了自行錄製現場演奏的方式，讓演奏者在大自然的環境中也能展現印地安音樂與大自然之間的緊密聯繫；或者以美洲傳統樂器演奏臺灣人耳熟能詳的樂曲，再上傳至影音網站分享於社群媒體之方式，以另一種推廣模式持續進行。採用相同作法的還有華之和音，採用於以屏風為背景的空間中，演奏一些臺灣民眾相對較難接觸的邦樂作品，爾後在分享影片的貼文中針對該作品做詳細說明。而Asalato
      Taiwan則是拍攝一些節奏教學影片，或者是設計一些風趣的展演橋段並予以拍攝成影片分享至社群媒體。有些在地世界團體則會進行線上大合奏，例如津輕三味線台灣廣原會就是以線上的方式讓學員在家中也能夠參與演出。由此可知，社群媒體成為在地世界音樂群體在遭遇疫情而無法進行實際的展演活動時，發揮創意持續推廣異文化音樂的重要平臺。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從以上的案例中我們可以發現，因爲疫情而導致原本規劃的活動無法照常舉行、或者所習慣的推廣模式無法持續進行之在地世界音樂團體，會在其他不同地點或者是社群平臺，嘗試設想一些根據當前情況而臨時規劃、甚者是經過精心規劃並富有創意的方式，使得疫情期間異文化音樂之展演、推廣行動成為可能，並且對部分在地世界音樂團體而言，這些新的演出場域與方式也成為其日後能夠持續實踐或供予參照的展演、推廣形式。
    </p>
    <h4><strong>三、讓世界音樂帶您環遊世界</strong></h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有鑒於疫情導致人們無法自由出國，「偽出國」、「代替出國旅遊」成為了諸多在地世界音樂團體會引用的論述。儘管「偽出國」、「代替出國旅遊」之論述看似不顯眼，卻也成為了許多團體在當前的特殊情況下，凸顯其作為異文化音樂推廣、展演群體的代名詞。在當前無法自由出國的情況下，沒有什麼東西能比在臺灣聽到來自異國的音聲更迷人的事情了；而這樣的論述又能跟諸多旅遊活動或者商業展售會相結合。因此，在地世界音樂團體意識到，疫情期間或許「讓世界音樂帶您環遊世界」能夠成為持續推廣世界音樂文化的另一到窗口；換言之，伴隨著疫情在全世界持續擴散並影響到國際間的自由往來，「讓世界音樂帶您環遊世界」可以成為「偽出國」、「代替出國旅遊」的另一種方式。或許「讓世界音樂帶您環遊世界」僅能作為輔助，無法真正成為展演、推廣活動的核心論述，但其仍能輔助部分活動的進行，甚至能在該概念的引領下，能夠讓世界音樂與一些非直接關係的領域產生關聯性，進而進行世界音樂的持續推廣。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;世界音樂展演、推廣相關活動，雖然還不是臺灣主流的音樂文化，但對臺灣音樂的發展而言仍有著重要的地位，特別是在2020年疫情衝擊之下，世界音樂仍未停歇地於臺灣各處流動、發展。終究，世界音樂是臺灣連結世界文化的窗口與平臺，在長期的發展過程中，世界音樂也慢慢成為臺灣音樂文化不可忽略的一環。
    </p>
    <h3><strong>參、武漢疫情下但卻充滿活力的新移民音樂活動</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;與其他音樂型態一樣，2020年的臺灣新移著民活動因著武漢疫情的影響而一度消聲匿跡，但在臺灣本土疫情獲得控制後又如雨後春筍般冒出。承襲著新移民自身的多元身份差異，新移民活動的音樂展演也是多元，許多的音樂活動往往伴隨著舞蹈、服裝、飲食等文化元素複合式地交織。隨著下半年疫情的好轉，各種由政府或民間主辦的新移民表演藝術活動開始增多，各種以多元國家為主題的表演形式，增進了臺灣人與新移民的相互了解與交流。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若要提到今年新移民活動不同於以往的最明顯可感的現象，應會是受到武漢肺炎疫情流行於全球而對其活動規劃的影響。由於武漢疫情在今年春節前後爆發，臺灣因為地緣關係而首當其衝，雖然臺灣政府積極且迅速地應對，一時之間各地傳出的零星本土確診案例卻也讓人心惶惶。以每月固定舉辦的屏東及高雄新住民市集為例，疫情的影響打亂了原來規劃的步調，以致兩者在4月時的活動接連宣布暫停。自外界看來活動主辦方在當下無奈地做出此決定，並且公告的措施也是在活動前幾天才透過簡單的網路貼文告知。不論參與的新移民或當地的主辦單位，皆是相當不樂見活動的停辦。此外也有相當多預訂於此時舉行的新移民活動被迫取消或延期。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從今年4月以後臺灣暫時未見本土病例，而境外移入病例因入境旅客皆須進行十四天自我隔離而獲得有效控制，足見疫情獲得有效控制，民眾開始有出遊的信心，新移民活動也陸續恢復舉辦。但在恪守政府防疫指揮中心的指示下，許多針對性的防疫措施被主辦單位採用，室外的活動設置出入口管制、體溫量測、手部消毒、及要求配戴口罩等等；活動若是於室內進行，主辦單位還會要求實名制登記。有時這些措施雖帶來些許不便，整體而言卻為活動的復辦帶來契機。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當臺灣本土疫情暫歇，世界各地的疫情卻越演越烈，讓想出國旅遊的國人無法遂願，此時於大眾媒體上突然增加許多所謂「偽出國」的討論。原先此概念是指稱航空業安排的不出國，僅於地面乘坐飛機，或甚至由飛機繞行臺灣空域的體驗，後漸被新移民活動採用於自我宣傳上。新移民活動如高雄新住民就業多元創意市集、新竹生活節國際日等，涵蓋世界各國的食物、衣著、文化藝術等多元面向，正好讓只能國內旅遊的民眾親身體驗異國文化，由此看來實可稱偽出國的名稱貼切。各主辦單位也明瞭此時舉辦活動的特殊及珍貴，往往也在活動當下對觀眾多加強調珍惜此刻的彼此相聚。時序進入秋冬，相對於疫情在歐美等北半球國家的捲土重來，臺灣依舊未再發生本土疫情，指揮中心也公布了防疫秋冬專案，要求公共場所人須配戴口罩，以及增加入境必須附陰性證明等措施。但由幾乎每週末都有新移民相關活動看來，並未造成活動熱度的下降。
    </p>
    <h4>
      <strong>一</strong><strong>、</strong><strong>多元的新</strong><strong>移</strong
      ><strong>民身份及其展演</strong>
    </h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前居留於臺灣的新移民主要有三大類型：與臺灣人結婚獲得長期居留或國籍的新住民；以工作為目的而獲得工作簽證居住於臺灣，其中大多為工廠勞工、家庭看護，少部份以高科技人員、商業人士、大學教授等身份;為了讀書學習為目的，以學生身份在臺灣做短期居留，以及因特殊任務需求而來臺短期宗教或志工者。新移民表演者來自全球各地，帶來各國迥異的文化。來自不同國家的新移民文化有著甚大的差異性，加上他們以不同的身份、環境與資源在臺灣生活，這些也會造成他們演出型態上的不同，以致從整體角度了解新移民有其困難，但透過對新移民來臺的各種因素的了解、比較與分類，或許可以成為理解新移民族群的線索。本節將列舉新移民社群表演團體中較活躍、具代表性者做一簡單介紹，希望能兼具介紹新移民音樂的廣度及深度。
    </p>
