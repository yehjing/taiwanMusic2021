    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從細節處觀察，市集會在一次次舉辦下進行微調。由於百世新天地為一條狹長的廊道，市集安排的文化展演在頭幾次為入口處右側既有的平臺，音樂能吸引遊客，但能安排的觀眾座位空間狹小，市集談天聲亦會影響觀賞體驗。活動中期改為在廊道末端自搭舞臺進行展演，觀眾座位空間寬敞，也少市集聲，但須人工招攬市集遊客前往觀賞。後期展演又改回入口處的舞臺，而廊道末端則增設育兒區接待親子遊客。此外各新住民攤位設置每月會安排在廊道中不同位置，則主要是為了配合主辦單位為每次市集訂出的主題月而安排。各月主題以新住民來自的國家或地區設定，設定時會考慮結合該月的該國特殊節日，如5月為慶祝泰國潑水節及臺灣母親節，6月為印尼，7月為美洲，8月為馬來西亞國慶，9月為越南國慶，10月為歐洲，11月為中國。符合當月主題的新住民攤位會移至靠近廊道入口處，使外部遊客易於接觸，此外符合該月主題的展演節目也會特別多，並且安排於表演時段前段演出。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;屏東新住民市集的模式在今年被各縣市大量採用，其中多次舉辦者除了上述高雄新住民多元文化市集，還有高雄市政府勞工局自7月到10月每月一次舉辦的2020新住民就業多元創意市集。除此之外，臺北市、花蓮市、南投縣、新竹縣、新竹市、桃園市、新北市、金門縣、嘉義縣、嘉義市、基隆市等縣市均籌辦了市集及演出結合的單次活動。值得注意的是大多數活動的主辦者多為政府單位，如上述文藻外語大學主辦的「高雄新住民多元文化市集」，或阮劇團主辦的「草草戲劇節」則為少數的民間新住民相關活動。因而，多數活動開放給所有新住民報名參與，也須兼顧各國文化呈現的比例上的平衡，這或許造成了形式不拘的表演內容及鬆散無連貫性的節目安排。有的活動會將一般認為精彩的活動擺在壓軸演出，有的活動則讓後面的表演呈現較為業餘隨意，只求在市集時間內時刻都有表演即可。整體而言表演節目的規劃就是在有限的展演時間內塞入無限的活動可能。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有些活動的主題相對有凝聚性，如印尼國慶文化藝術節、龍岡米干節等，這類活動通常以國籍為主題而邀請來自該國的新住民表演。在多次舉辦的新住民活動中僅高雄新住民多元文化市集試圖為各次活動訂出國籍主題。具有主題性在對該次活動的宣傳會較容易聚焦，也能夠藉由主題的改變滿足人求新求變的新鮮感，更讓各次參與攤位及演出者有差異。不過這樣的凝聚性實際上有限，這反映在各次各國籍主題表演者並未填滿此市集表演的所有時段，而仍有其他新住民表演者，甚至本地表演者的參與；至於攤位亦然。因而國籍主題使活動更具話題性，但目前的呈現上維持多樣性的狀況仍與其它新住民活動相似。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在武漢疫苗逐漸露出曙光的此刻，2021年臺灣能否維持相對安全，重啟跟各國的實體交流，充滿不確定性。新的新住民或許短期內不會大量增加，但可以期待的是新二代逐漸躍上舞臺。往後會不會有更多常態性新住民活動？這些活動會不會有更有凝聚力的主題？都是可以探詢的。
    </p>
    <h3><strong>結語</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣社會中包含了多元的民族與文化，而這個特色也成為許多音樂創作者的靈感來源，不論是河洛、客家、原住民，亦或是來自境外的異國的音樂文化等，不同類型音樂之間的交流、融合正是呼應了臺灣多元文化的特色。除了音樂本身，無論是跨界音樂、世界音樂或新移民音樂，各類型樂團也試圖開發音樂文化與其他物件之間的連結與合作，將音樂融入在地文化之中，使音樂不再只是被欣賞的客體，而是構成文化的主體之一。這更體現了「將音樂視為文化的一部份」的觀點，使在地的文化特色得以透過音樂展現出來，並且融入日常生活當中。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年由於疫情造成全球各國之間經濟與文化的互動減少，儘管臺灣在疫情的防治方面有著十分傑出的成績，但不可否認的是，各種文化藝術活動也確實因此而受到極大的影響，使得臺灣邀請來自世界各國的表演藝術團隊大量的減少，這對於原本就與國際表演藝術活動有著極為密切關係的臺灣跨界音樂、世界音樂與新移民音樂的衝擊就更加的明顯。雖然，由於疫情的關係，許多臺灣本土樂團演出因此而取消或延遲，但也正因為邀請國外團體來臺演出的大量減少，大部分的演出活動都由本地團隊來擔任，因而凸顯了本土的跨界音樂、世界音樂與新移民音樂表演團體培養與發展的重要性。
    </p>
