    <h3><strong>壹、文化部藝文紓困撐起一片天</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部影視及流行音樂產業局在流行音樂方面按既有的六大補助機制，即人才培育、錄音製作、品牌與經紀發展、行銷推廣、跨界製作與數位化發展方面進行送案審查的補助，對於本土語言流行音樂專輯製作發行、錄音升級、扶植流行音樂產業行銷推廣進入國際市場，或是以跨產業合作方式提高臺灣原創流行音樂與泛娛樂產業做進一步連結，使得不少有意義的計畫得以進行，例如野火樂集的「Become
      The Ace」、杰德創意影音管理股份有限公司的2020 Perfect Vision「同志音樂愛情故事」等。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年成立的文化內容策進院，雖有流行音樂人陳珊妮擔任董事，但在2020疫情之年，除了一些專場的座談會外，未見對於流行音樂產業有何積極的著墨。未來文策院唯有流行音樂專業人員進駐，才能在實質上對於臺灣的發展有真正的助益。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而，除了上述中央級與其相關單位的例行性補助外，2020年的整體發展，隨時依著疫情這條紅線的變化而產生變化。2月份來臺演出的澳洲音樂家確認染疫，3月5日文化部長鄭麗君親上中央流行疫情指揮中心記者會做說明，頓時臺灣藝文界一片風聲鶴唳。許多表演或是演唱會陸續取消，遭受經濟損失的不只有臺前的音樂人，成千上萬的相關產業從業人員也是這次疫情的最大受災戶。所幸，文化部以《文化部對受嚴重特殊傳染性肺炎影響發生營運困難產業事業紓困振興辦法》為基礎，首重「防疫為重，紓困並行，振興在後」為原則，快速進行了幾項重要的補助方案，對於屬於藝文紓困領域的流行音樂界，業界的反應相當正面。包括：
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「藝文紓困1.0」，包括藝文紓困補助、紓困貸款利息補貼、行政調控、振興措施，包含短期紓困補助11億元，以及後續振興措施4億元，並將藝文產業納入經濟部的中小企業貸款及利息補貼方案。
