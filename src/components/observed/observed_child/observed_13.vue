      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;衛武營表演廳之「衛武營小時光」系列節目也會安排獨立音樂、詩歌、城市民謠等輕製作音樂節目，例如7月19日陳珊妮的「趁記憶消逝之前」，9月18日周蕙的「南回星空下Miss
      Where Live
