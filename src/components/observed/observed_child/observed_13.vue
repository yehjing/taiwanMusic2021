    <h3><strong>捌、爵士音樂</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年的爵士音樂可分為教育與相關營隊、演出活動與獎項3個部分來觀察。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在教育方面，持續在高等教育中有不錯的發展，包括輔仁大學音樂學系除既有於碩士與碩專體系招收爵士主修外，109學年度更首次招收大學部爵士組新生，而臺南藝術大學的爵士音樂學程與東華大學音樂學系爵士組招生穩定，且學生素質穩定成長，每年都有一批受過訓練的爵士音樂人，為臺灣爵士音樂界注入新的動能。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北爵士大樂隊在8月舉辦第二屆臺北爵士音樂營，160位學員年齡涵蓋9-55歲，參加人數創下新高。在全球音樂營紛紛取消的狀況下，臺灣其他各地照常舉辦的爵士音樂營，包含8月的「兩廳院爵士音樂營」，和10月由KoSwing爵士大樂團主辦的「臺南爵士基因音樂營」。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年來對於臺灣爵士音樂教育有重大貢獻的指揮家金．艾特金（Gene
      Aitken），異於往年重點式的停留臺灣，今年因疫情影響長時間待在臺灣，期間指導臺北爵士大樂隊、臺南KoSwing爵士大樂團、新竹爵士大樂團、輔仁大學爵士大樂隊、臺北美國學校等，將他對於爵士音樂的深厚功力10年來無私的貢獻給臺灣。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在演出方面，「兩廳院夏日爵士派對」在8月12-16日，於實驗劇場規劃了6場「咆哮吧！帕克精神」節目演出，為紀念查理帕克（1920-55）百年誕辰。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「臺中爵士音樂節」因疫情取消，改為線上及聖誕音樂會形式；10月的「臺北爵士音樂節」，則照常舉辦。但因國外音樂家無法參與，因此邀請多位流行藝人如許景淳、魏如萱、Leo王、艾怡良等人演出。此舉雖增加曝光度，但是否反而轉移了爵士焦點，值得觀察。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;疫情6月底解封後，爵士餐廳及酒吧的演出節目與觀眾持續增加，演出品質也隨之提升，不少國際樂手因疫情滯臺，增加了與國內樂手交流的機會。滯臺的國際爵士樂手為主的臺北職人爵士大樂隊（Taipei
      Prefessional
      Orchestra）於5月成立，並固定於Smexy音樂餐廳演出。而臺北Sappho由安和路搬遷至林森北路，享巷（Rhythm
      Alley）由古亭移至臺北東區。臺北爵士大樂隊青年團固定每個最後一週的星期二於台北藍調演出。臺北地區的Jam
      session聚集年輕優秀的樂手一同切磋學習，也發展出20-30歲的樂手新世代。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在獎項部分，於業界享有盛名的爵士鼓手黃瑞豐繼獲得金音獎後，今年又獲得金曲獎特別貢獻獎。薩克斯風手蘇聖育、鼓手林冠良、鍵盤手曾增譯合作的專輯《Ciao
      Bella》，獲得金曲獎演奏類最佳專輯獎；薩克斯風手謝明諺、吉他手李秉彥獲得金音獎最佳樂手獎；最佳爵士專輯獎，最佳爵士歌曲獎，則由歌手羅妍婷獲得。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣爵士音樂的發展，經過許多音樂人的努力，再加上近年來不少至國外學習的爵士音樂家紛紛回國貢獻所長，因此全臺各地的爵士大樂團如雨後春筍般的成立，更在2020年逐漸站穩腳步，例如台東回響樂團至新竹縣文化中心演出，臺中地區在受到多年臺中爵士音樂節的影響之後，也開始有不少爵士大樂團，例如仙人掌爵士大樂團、Midwest爵士大樂團、Illussion爵士大樂團等。甚至，爵士樂元素與樂手在流行樂界中出現的頻率有增加的趨勢，周杰倫的歌曲〈Mojito〉運用了拉丁元素，並由爵士樂手錄音；嘻哈歌手&Oslash;ZI、歌手9m88等也在錄音與現場演出中與爵士樂手合作。甚至，國防部示範樂隊於2009年成立流行樂團GR（Real
      Good）樂團，2013年更成立示範樂隊爵士樂團，讓負責國家音樂勤務的樂團，有更多的可能性。多年來已有不錯的成果，並會定期舉辦音樂會，2020年10月22日於松菸誠品表演廳演出的「爵示瑛感」，由爵士小號手魏廣晧指揮，邀請爵士鋼琴家許郁瑛共襄盛舉。顯見連軍中音樂生態都有所改變，爵士音樂與流行音樂的巨大影響性。
    </p>
    <h3><strong>玖、高等教育體系</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;要壯大臺灣的流行音樂，人才的培育是首要之務。文化部每年皆會於人才培育項目上給予補助，成為不少學校爭取的經費來源。2020年國立臺北藝術大學「IMPACT音樂學程菁英翱翔計畫」，與國立屏東大學音樂系「流行音樂人才培育－數位音創跨藝學程」等皆獲補助。但除此之外，長久之計，仍是要進入學校，做有計畫的培訓。臺灣高等教育體系，從過往以古典音樂為重，到近年來不少學府都設有流行音樂與數位配樂、音效學程與學制成為趨勢，然招生策略、師資與設備等問題，使得各學校的狀況有所消長。在各大學諸多碩士、碩專的學制競爭下，學士後學位學程皆走入歷史，例如國立臺北藝術大學學士後跨藝合創音樂學士學位學程，只維持105-108學年，110學年度將成立音樂與影像跨域學士學位學程，中原大學數位音樂應用學士後學位學程亦於107學年度結束，改為音樂產業碩士學位學程，其中設置流行與商業音樂創作組，亞太創意技術學院數位媒體設計系流行音樂與影視傳播組，因招生困難，於106學年度停招。台南應用科技大學流行音樂學士學位學程，因經營不錯，110年將改制為流行音樂系。可見得，在學校內培育流行音樂、配樂相關人才，亦是競爭激烈，就算是國立大學名校，若無法提出有利的師資與課程，招生仍會遭遇瓶頸。
    </p>
    <h3><strong>結語</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年是一個艱困之年，也是一個令人惋惜之年。9月5日打狗亂歌團團長嚴詠能，在屏東縣萬丹鄉演出時於舞臺上過世。9月16日不到40歲的小鬼黃鴻升，因主動脈剝離驟逝，引起流行音樂界諸多的嘆息。2000年至今，臺灣流行音樂正經歷實體唱片衰退、串流崛起，音樂分眾、音樂人西進、臺灣華語音樂霸主地位鬆動。諸多的挑戰，或許可以在疫情令人沉澱之時，有更多的思考，例如臺灣的流行音樂如何於在地化與國際化之間取得平衡點呢？以K-Pop表現最為亮眼的BTS防彈少年為例，他們的成功是充分做到國際化，今年9月更出版首張全英文單曲〈Dynamite〉，榮登今年最多串流收聽次數的K-Pop歌曲，並於24小時內創造了1,260萬次的串流量，在Spotify刷新紀錄。時序再往前看，近年來臺灣歌手，不論周杰倫、張惠妹、甚至樂團滅火器，標榜世界巡迴演唱會的地點，半數以上主要都集中在亞洲城市，而BTS僅半數以下在亞洲，這樣的地點選擇說明了，臺灣的流行音樂特別是華語部分，因為有中國龐大市場作為依靠，所謂的「世界巡迴」多以亞洲為最大考量，異於韓團真正對歐美的聆聽人口下功夫。所以，臺灣的國際化仍是傾向於亞洲的國際化，對於歐美聽眾的拓展與發行全英語單曲和專輯，仍是停留在不遠仍遠的規劃。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;雖有上述尚待克服的空間，但仍是有令人溫馨的驚喜出現。流行音樂雖是當下的音樂產物，但是日治時期的流行音樂因為「懷舊」的發酵，對於當今某一個消費階層，亦是有一定的吸引力。2020年在日本發行，帶有諸多文字說明的CD專輯《蓄音臺灣：1917
      to
      1943》，由蟲膠收藏者林太崴主筆歌曲解說，是戰後日本首度復刻臺灣78轉唱片，更是臺灣之外，全球第一次發行蓄音器時代的臺語專輯共23首，由Discologia音樂發行，意義非凡。洪怡芳撰寫的專書《曲盤開出一蕊花》，內容為戰前臺灣流行音樂的解析，並附有20首歌曲。上述這2筆的出版，顯示不論是流行音樂的研究亦或出版，「鄉愁」的想像仍具有一定的吸引力。類似的概念也適用於民謠歌王劉福助，他於11月21日在臺灣戲曲中心小表演廳所舉行的「斷層的聲音－劉福助」演唱會。這位歌壇長青樹，在2018年的金曲獎頒獎典禮中，與嘻哈歌手互相尬唱，讓觀眾見識到他唸歌的即興實力，而以「斷層的聲音」為題似乎在說明，年過80歲的實力派歌手依然傳承著臺語歌唱的精髓，國立傳統藝術中心「世代之聲－臺灣族群音樂紀實系列」的美好傳承，也關注在流行歌曲領域，實屬難得。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以臺灣防疫有成，屢屢登上世界媒體版面的優勢，2020年底雖然疫情未歇，面對來年考驗仍在。但是有了2020年的寶貴經驗，2021年臺灣的流行音樂定能一步一腳印地克服困境，呼應黃連煜特別創作的防疫歌曲〈Yes
      We Can〉。
    </p>
