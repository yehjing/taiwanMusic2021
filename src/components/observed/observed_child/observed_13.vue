    <h3><strong>貳、喧嘩的演唱會與不得不轉彎的線上演唱會</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;武漢肺炎在2020年給予了全世界音樂產業鍊非常沉痛的一擊，根據美國最具影響力以及最具媒體指標性的音樂媒體Pollstar，在最新12月的研究報告《Pollstar
      2020 Year-End
      Special》中指出，在全球Live演唱會產業光是2020年就損失超過300億美元的利潤，讓全球進入不可逆的黑暗時期，可見得演唱會是最慘烈的一環。臺灣雖然是防疫模範，但上半年也無法扭轉這樣的趨勢，流行音樂歌迷追星指標的演唱會，1-2月只有幾場真的如期舉行，隨著2月15日出現死亡首例，人心惶惶，多數演唱會皆是宣告延期，以減少風險。在指標性的場地臺北小巨蛋、台北國際會議中心與高雄巨蛋只有少數場次舉行，轉型線上直播演唱會成為新型態的互動，至下半年現場的演唱會才重新恢復運作，詳述如下：
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北小巨蛋，蔡依林夾著2019年《Ugly
      Beauty》專輯與紀念出道20年吸引歌迷，一連從2019年12月30、31日，延續到2020年1月1、3、4、5日，共6場，6萬6千張門票於開賣3分鐘即售罄，顯見粉絲力量大。1月12日臺視舉辦的「紅白藝能大賞」，2月1日詹雅雯、2月22、23日吳青峰、2月8日宇宙人，和2月14、15、16日的A-Lin「Passenger旅．課」演唱會後，就此畫下句點。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台北國際會議中心只有2月23日以校園歌曲為訴求的「民歌45」演唱會。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;高雄巨蛋，李宗盛繼2019年巡迴中國的「有歌之年」，於1月18日在高雄巨蛋舉行臺灣場的演唱。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;但流行音樂界似乎感受到「無法群聚」，對於整體現場演唱會產業的巨大殺傷力，在不能直接與歌迷面對面的情況下，必須積極另闢新機。4月17日劉若英首開先例，在新竹內灣大戲院舉行「陪你線上演唱會」，截至5月中旬，全球在線觀看次數累積近1.5億人之可觀紀錄，可見得完全比照一般演唱會的規格，而非陽春如在Facebook上直播或是以YouTuber的方式錄製，斥資800萬的嘗試是有其具體的果效。而收錄線上演唱會的內容，於5月25日起以《2020劉若英陪你獻上錄音專輯》上架，更可以有實質的收益。根據Livestream調查，當民眾看完活動現場直播的影片後，有67%的人會進一步購買類似音樂會或活動的門票。換句話說，觀賞線上音樂會，能激發觀眾觀看其他表演的欲望，刺激未來的演唱會銷售，所以擁有財力支持的歌手，韜光養晦之際，辦場優質的線上音樂會，絕對是加分之舉。而五月天5月31日在臺北市立體育場舉辦的線上音樂會「突然好想見到你」，更是花了1,500萬打造舞臺，將現場1.6萬個座位放滿螢光棒，代表粉絲同在的意涵，甚至在〈突然好想你〉這首歌時，出現蕭敬騰、李榮浩、毛不易等人接連合唱，就像演唱會神秘嘉賓站臺模式，深具臨場感。短短1小時的線上音樂會，累積全球4,244萬人收看，甚至五月天標榜這是成團以來，最多人「同時參加」的一場演唱會。這2場線上音樂會的成功，為焦慮的流行音樂界提供了某種示範，「玩巧思」是必備，但是軟硬體的條件，更是成敗的關鍵。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在政府有效的防疫政策與全體民眾配合之下，從4月12日至12月22日共253天無本土病例出現，所以下半年的演唱會都以實名制方式嚴格控管入場觀眾，臺灣觀眾展現高度的合作態度，讓演唱會得以順利舉行。臺語金曲歌手曹雅雯兩度延期的演唱會，終於在6月20、21日於臺北的國際會議中心舉行，這場全臺首場解封演唱會，觀眾近乎滿座，代表粉絲的忠誠度在疫情期間仍是相當堅定。接著7月11日「經典潘越雲2020演唱會」，為紀念出道40年的感恩，臺北場共演唱30首歌曲，全由她一人擔綱，沒邀請其他任何歌手，誠意十足！讓與會者見識到歌壇唱將年過60的實力。周興哲8月8日「你，好不好巡迴演唱會DELUXE」登上臺北小巨蛋，號稱是疫情解封後第一場萬人演唱會，8月9日再接一場，9月5、6日續攻高雄巨蛋，4場共4.5萬張票15分鐘售罄，成績亮麗；8月22日「Since
      5566高雄巨蛋演唱會」也不惶多讓，為南臺灣解封後首場大型演唱會，緊接著8月29日八三么「想見你想見你想見你」演唱會，現場約1萬人同時戴上八三夭特製款中衛口罩，以「疫情概念」留下獨一無二的珍貴畫面。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從2010年的開始「超犀利趴」在2020年亦不缺席，8月22、23日在臺北小巨蛋邀請歌手與獨立樂團共襄盛舉，接著9月12日詹雅雯再次攻蛋，邀請防疫指揮官衛福部長陳時中擔任防疫加場嘉賓，兩人合唱〈戀情海〉、〈想厝的人〉，引發全場「順時中」轟動，這應該是創下中央部長級官員參與大型演唱會歌唱的首例。再加上田馥甄9月25、26、27、28日，楊丞琳11月6、7、8日的場次，真是好不熱鬧！蔡依林「Ugly
      Beauty
      2020世界巡迴演唱會」於11月20、21、22、27、28、29日在高雄巨蛋舉行6場，6萬張門票也是秒殺賣完，可見蔡依林舞臺魅力異常驚人，也是在疫情之年最豐收的歌手。滅火器20週年演唱會，12月26日於高雄9號碼頭舉行，破萬人潮共襄盛舉，展現高雄最在地樂團的魅力。
