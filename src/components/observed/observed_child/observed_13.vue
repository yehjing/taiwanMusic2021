    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;10月25日蕭敬騰線上音樂會，依舊比照大型演唱會規格，配合了絢麗的燈光雷射、雙舞臺的配置、15人大型樂隊、8人弦樂團，並邀請黃子佼擔任主持人，與蕭敬騰在曲目換場間風趣地對談，也把歌迷們想問蕭敬騰的大小問題當作主題，以拉近線上的疏離感。在臺灣LINE
      Music獨家直播音質非常專業，使得這場直播音樂會的臨場感絲毫不遜色於現場演唱會！
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;原本預計舉行的跨年演唱會，因為紐西蘭機師染疫的影響，各縣市幾乎都改採線上演唱會的形式，而仍堅持照常舉行的縣市除了臺北101跨年晚會的「臺北最High新年城－2020跨年晚會」外，就屬張惠妹於臺東海濱公園舉行的跨年演唱會，共約有數萬人赴臺東追星，歌迷住宿、交通費等，為臺東帶來不少經濟效益。她並同時推出單曲〈緩緩〉，在年度交替之際，祈福未來。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;將疫情以創意發想，在戴口罩、量額溫、實名制、噴酒精之外，創造意外的亮點，以吸引粉絲共襄盛舉，實為臺灣在今年不僅是全世界少數得以舉行大型演唱會的國家，更是現場群聚、線上群聚的極致表現，透過YouTube、Facebook、LINE
      Music等平臺同步直播，把歌聲零時差的傳遞到世界各地，而聽眾也因「保持社交距離」般地臨場感下，樂於線上收看與運用留言表達支持，累積極多觀看人數，在疫情期間雖未必見得有豐厚的演出收入，但讓歌手維持人氣不下滑，亦是經營要點。在這不得不轉彎的表演形式下，為快要窒息的流行音樂演唱會注入了新的亮點，亦是搭載新的媒介與越來越進步的網速才能達成的流行音樂新型態。但是我們若以全球電音產業的預估來觀察2020年的現場演唱會，按IMS電音產業報告中的數據顯示，2019年的全球電音產值為73億美元，成長2%，但是緊接著的2020年卻是讓業界愁眉不展，因為預計在Live表演收益將下滑75%，2020年取消的音樂節與相關演出估計失掉了約890萬的樂迷。臺灣疫情控制的狀況雖然傲視全球，大家仍可自由行動，但是衝擊肯定仍是存在。因此，現場演唱會與線上音樂會兩者應是互補性質，在疫情元年是如此，緊接著的2021年或許也應是在兩者之間取得某個平衡點。
    </p>
    <h3>
      <strong>參、「未來」提前之數位制霸</strong><strong>vs.</strong><strong>實體專輯</strong>
    </h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧流行音樂的載體，從蟲膠、黑膠、卡帶到CD，歷經了半世紀的演變，但是從實體CD到數位、串流音樂，卻是時代腳步不斷快速進化的成果。國際唱片業協會（IFPI）公布的2019年《全球音樂報告》指出，全球錄製音樂市場成長了8.2%，高達202億美元，其中全球音樂串流服務的用戶超過3.41億，成長了近25%，整體營收達114億美元，以56.1%的過半比重成為趨勢，而且數位下載正持續下滑，串流成為顯學。2019年音樂串流服務成長了近25%，訂閱及下載數位音樂的總營收約佔全球錄製音樂市場總營收的50%。也就是說，付費訂閱用戶的穩定收入，讓唱片公司與數位音樂化敵為友，數位變身為主流唱片公司的營收主力。點播按月支付的Apple
      Music、KKBOX、Spotify、Amazon
      Music來聽音樂，在Facebook、Instagram上看演唱會直播，在YouTube官方頻道看歌手或愛團最新釋出的MV，這些都是網路原生世代的生活日常。依此趨勢影響所及，Podcast在今年因疫情無法演出而逐漸成為流行樂手的新寵，9m88、王若琳、韋禮安、The
      Tic
      Tac、胖虎樂團等，皆開啟自己的Podcast節目。相異於YouTube有影像與音樂，Podcast反而因某種情況下無法「使用眼睛」，而讓閱聽者回歸更單純的聲音世界，例如經營「胖虎的人生相談室」的主唱阿Ben與鼓手小雞就表示，做節目的成就感來自於以「圈內人」的角度把音樂市場的一線經驗、見識分享出去，將主題聚焦在音樂上，與粉絲分享。可見得，「圈粉」不限時間，正是趨勢。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;KKBOX集團旗下的科科農場（KKFARM），2018年1月推出「Soundscape在田」音樂發行平臺，讓加入的音樂人可以藉此把自己的作品放上全球大型的數位音樂平臺，並利用Bitmark的區塊鏈技術，讓音樂創作者可以100%握有音樂版權，且完成複雜的版稅結帳。而根據此平臺的統計，從2016年10月到2020年10月，4年間獨立廠牌的播放量成長了4.05倍。而KKBOX的2020年臺灣音樂產業報告，從2015年12月至2019年12月，4年間獨立廠牌發行的音樂播放量占比，從27%提升至40%，可以預見2020年的獨立廠牌音樂或許將貢獻50%左右的收聽量，持續2019年的觀察：「音樂的聆聽愈加社群化、分眾化」，「未來」完全掌握在年輕世代手上。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當然臺灣的流行音樂藉由音樂串流也不全然的「安全無虞」，按KKBOX的統計，華語流行音樂從2014年佔整體串流量的70%，到2020年的50%，K-POP從2014年的5%，到2020年的15%，足足成長3倍，西洋音樂逐漸下滑，日本流行音樂因為釋放海外版權，逐年回升等等的趨勢，顯見臺灣的流行音樂因串流平臺而興盛，但在臺灣串流平臺也因此得以收聽到各國流行音樂，所以競爭也更加激烈。流行音樂的消費沒有任何道德觀可言，須留心「外來勢力」的侵蝕。所以2020年武漢肺炎帶來的影響，將更全面地改變整個音樂產業，從表演形式到閱聽模式。唯有在數位串流上取得優勢，才有穩定的產業未來，這應該是必然的結論。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相異於上述的數位串流，在這蟄伏的一年，不少重量級歌手不畏疫情紛紛出版實體的CD。華語歌曲方面，周興哲在1月就展現強大的企圖心，出道僅5年24歲的他夾著之前所創下的紀錄，亦即出版的3張專輯與1張數位EP，在YouTube總點閱率超過6億，並以〈怎麼了〉MV獲得YouTube的2019年度熱門音樂影片冠軍，所有歌曲共拿下近百個數位音樂平臺的冠軍。夾著這樣的高人氣，專輯《小時候的我們》雙CD共21首歌曲，皆由他創作詞、曲、編曲與製作，頗有強勢問鼎金曲獎的氣勢，或許下一個華語小霸王已經誕生。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;頑童MJ116中的瘦子E.SO首張個人專輯《靈魂出竅OUTTA
      BODY》，淬鍊自菲律賓人煙稀少的島嶼，以他熟捻的嘻哈為基礎，串聯多元風格，呈現有別於頑童時期的歌曲精神。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;吳青峰繼2019年的個人專輯《太空人》後，於2020年又出版雙CD專輯《冊葉一：一與一》，為他曾經發表過的8首作曲和8首作詞共16首歌曲組成。在爆發被前經紀人林暐哲提告後的窘境，蘇打綠竟不能再唱自己的歌，吳青峰卻在此情況下，以密集出專輯來證明自己的音樂實力。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;睽違4年韋禮安的新專輯《Sounds of My
      Life》，嘗試以音樂來記錄生活中的微小聲音，畢業於臺大外文系的他，利用英文上的優勢，專輯中多首全英文歌曲，例如〈See
      You on Monday〉、〈Brand New〉等，企圖拓展自己多方的嘗試。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;田馥甄的專輯《無人知曉》，先在1月發出其中的第一首單曲〈懸日〉，引發粉絲對於專輯的期盼。她明顯從S.H.E.時期蛻變至個人唱腔風格的確立，以清亮空靈的音色，繼續擄獲歌迷的心。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;楊丞琳專輯《LIKE A
      STAR》，已經非常清楚點明了她在演藝界多年來，經由努力交出了不錯的成績，這張專輯的紀念性不言而喻。但像主打歌〈Bad
      lady〉，頗讓人驚訝的是從較為文藝氣息轉為動感的曲風，R&amp;B嘻哈舞曲旋律混搭電子合成器，再加上MV上的百變造型，或許楊丞琳要以異質性的風格，來呼應自己的第二十年。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在臺語歌曲方面，於荒瘠中仍有不少默默耕耘的佳作，例如相當被看好的臺語唱將許富凱專輯《拾歌》，展現他輕柔吸引人的聲線；荒山亮專輯《等待好天》中的〈長歌路遙〉，更是超越臺語歌的思維，由荒山亮主唱，尚和歌仔戲劇團梁越玲演唱的歌仔戲曲調鑲嵌於歌曲中，前為【七字連空奏】的概念，後為【霜雪調】，交替於荒山亮以英語演唱帶有即興的部分，和之後臺語緩緩道來的敘事旋律，頗有畫龍點睛的鄉愁感；黃妃專輯《色違》中的〈雪咧飛〉，在較典型的臺式旋律中，以電音融合古典的弦樂編曲，呈現搖滾抒情風的迷魅氛圍，襯托出黃妃特有的唱腔。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在客語專輯方面，《野蓮出庄》是生祥樂隊繼《圍庄》之後的作品，也是《我庄》系列的第三張專輯。林生祥以一貫的鄉土關懷，與鍾永豐合作共同著力於美濃客家庄特有的植物野蓮，在農村、土地的思維中，建構歌曲的主體性。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在原住民專輯方面，Sangpuy桑布伊推出他個人第三張專輯《得力量pulu'em》，包括9首全卑南族語創作與1首融合屏東滿州古調與卑南古調的改編歌曲，並邀請不同的編曲者為每一首歌曲量身訂做，呈現多元的豐富性。而太魯閣族姚宇謙Lowking代表的是另一個年輕世代，其首張專輯《散步到地平線》，表現出如何運用西方的R&amp;B唱腔來詮釋部落文化。各自有其特色，但是原住民歌手透過流行音樂，來強調身分認同，說族語的驕傲，則是共通的。
    </p>
    <h3><strong>肆、兵家必爭的公私立音樂獎項</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;縱然疫情，流行音樂相關的重要公私立獎項，仍是年度眾所矚目。第十五屆KKBOX風雲榜在疫情爆發之前，1月18日即在臺北小巨蛋舉行頒獎典禮，但Hito流行音樂獎則受到影響取消頒獎典禮，改於6月30日公布得獎名單。第三十一屆金曲獎頒獎典禮由往年的6月改至10月3日，臺灣原創流行音樂大獎如期於8月29日舉行，金音獎也於10月31日於臺北流行音樂中心舉行頒獎典禮。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第十五屆KKBOX風雲榜頒獎，主持人由LULU黃路梓茵擔任，風雲大使邀請以個人名義推出專輯，大獲好評的吳青峰擔任，以這屆主題「Music
      Connects
      Everything」為表演核心。年度風雲歌手包括周興哲、鄧紫棋、頑童MJ116、A-Lin、謝和弦與表演嘉賓接力演唱。獎項方面，中華電信MOD票選人氣歌手由楊丞琳獲得，華語年度歌曲累積榜則由周興哲的〈怎麼了〉，拿下點播率冠軍。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以唱片行銷售量、數位音樂下載與歌曲播出率，三大要素作為評分標準的Hito流行音樂獎，以另外一種形式製作「頒獎影片」，由主持人阿娟和Dennis以快遞員身分，將獎座送到得獎歌手手中。Hito男歌手獎由吳青峰獲得，Hito女歌手獎由魏如萱獲得，Hito團體獎由伍佰&amp;China
      Blue獲得。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;延後舉辦的金曲獎頒獎典禮，配合中央疫情指揮中心之防疫作為，入場全面採實名制，座位優先以入圍者及所屬唱片公司為主，不開放一般民眾索票及補位，入圍者更設置安全隔離區（隔板）。當天也是臺北市政府舉行的「2020臺北白晝之夜」，以「Log
      in！南港通電
      看見交界」為主題，12小時不停歇，像似為金曲獎頒獎典禮添加柴火，好不熱鬧！北流中心的營運，為南港帶來城市的翻轉，拭目以待。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第三十一屆金曲獎報名家數共有231，件數共有19,461，入圍件數163，得獎件數27，明顯少於去年。不同於過去幾年，如第二十八屆金曲獎把最佳樂團、最佳新人獎都頒給了草東沒有派對，預示華語主流樂壇逐漸為獨立音樂所主導，而第三十屆，則將最佳國語男歌手獎頒給了搖滾和饒舌男歌手Leo王，意味著饒舌當道。而今年評審團主席由知名音樂製作人陳鎮川擔任，獎項都由較文青的作品出線，像是魏如萱《藏著並不等於遺忘》打敗鄧紫棋《摩天動物園》，獲得最佳國語女歌手獎，吳青峰《太空人》打敗J.Sheon《巷子內》、張震獄《遠走高飛》，獲得最佳國語男歌手獎，堪稱2020年的最大勝利者，公私立獎項皆有極佳的斬獲。王若琳《愛的呼喚》獲得最佳國語專輯獎，持修《房間裡的大象》打敗9m88《平庸之上》、高爾宣《#osnrap》，獲得最佳新人獎。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本次金曲獎入圍8個獎項的阿爆（排灣名阿仍仍）專輯《kinakaian母親的舌頭》，打敗眾多國語、臺語、客語專輯，拿下年度專輯獎、最佳原住民專輯獎，而專輯中的〈Thank
      You感謝〉，以類福音詩歌的和聲結合原住民的唱腔，感謝所有完整她生命歷程之人，感人而真誠，獲得最佳年度歌曲獎，堪稱本屆金曲獎最令人驚艷的專輯。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;獲得最佳樂團獎的滅火器，因著2014年太陽花學運時期的〈島嶼天光〉一曲，才成為眾所皆知的樂團，其實這個早已經成軍10多年的獨立樂團，正匍匐前進，找尋一條屬於自己的音樂康莊大道。2015年，〈島嶼天光〉獲得金曲獎最佳歌曲獎的肯定，但是樂團卻因政治色彩過於濃厚，而成為商演、代言的殺手，一度難以維持樂團的營運。終在今年以《無名英雄》獲得金曲獎的肯定，專輯同名曲搖滾風中有著陣頭的意象，MV融合反送中、呼應社會上被壓迫者的吶喊，觀眾好似也看到李昂的《彩妝血祭》與編舞家林美虹的《新娘妝》之震撼，獲獎實至名歸。此專輯同時也入圍最佳臺語專輯獎、年度專輯獎、最佳專輯製作人獎，當中的〈雙城記〉更入圍年度歌曲獎，亮麗的肯定雖未必為樂團帶來許多實質的收益，但值得慶幸的是，臺灣終於出現了一個真正「獨立於利益」之外的樂團，為他們堅持的社會理念發聲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;獲得最佳臺語男歌手獎的蘇明淵為執業律師，以專輯《善良的歹人》獲獎。全部的詞曲皆由其親自創作，靈感來自於他職業上的體悟，化為歌曲，寫實而真誠。他亦是2019年原創流行音樂大獎河洛語組以〈無根的日頭花〉獲首獎者，今年亦拿到金曲獎，獎項環身，才華備受肯定。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而原住民第一代歌后盧靜子，以《尋樂》獲得最佳原住民語歌手獎，更是遲來的光環。在最佳客語歌手獎方面，其實競爭激烈，雖由米莎《戇仔船》獲獎，但是打幫你團長劉榮昌《沿路日常》、古典聲樂出身的黃珮舒《纏，一溜仔》與黃宇寒《有時有日》都是強勁對手。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;被譽為臺灣鼓王的黃瑞豐，於2016年獲得第七屆金音獎傑出貢獻獎的肯定，今年再次獲得第三十一屆金曲獎特別貢獻獎，顯見對於長久在吸引目光的歌手背後默默耕耘的樂手，其重要性逐漸被重視。綜觀金曲獎與金音獎，此類相關獎項兩者皆獲獎者，只有長青歌手文夏、掀起臺灣音樂解嚴狂潮的黑名單工作室，與以已過世身分獲獎的貝斯手郭宗韶。黃瑞豐縱橫流行音樂界與爵士音樂界超過半世紀，掌握各種流行音樂曲風，合作過的歌手不計其數，蘇芮〈一樣的月光〉堪稱經典，亦創造不少獨有的爵士鼓節奏，尤其是他的爵士鼓即興獨奏更是令人折服。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當然，金曲獎也不是沒有引起爭議的得獎作品，王若琳以專輯《愛的呼喚》拿下最佳國語專輯獎，收錄歌曲包括4首日文歌、2首粵語歌及5首國語歌，就遭到該專輯內容皆為翻唱，且國語歌曲的部分只占一半，而獎項應該頒給以創作為主的專輯之質疑。但在金曲獎的規章裡，並沒有明定翻唱專輯不能報名參賽，因此儘管王若琳得獎受到爭議，但確實沒有違反規定。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣指標性的母語音樂創作獎項－第十七屆原創流行音樂大獎在臺灣中油大樓國光廳舉行決賽暨頒獎典禮。由黃昺翔獲得河洛語組首獎、邱淑蟬獲得客語組首獎、一好．屴夯獲得原住民族語組首獎，並在9月27日於臺北華山藝文特區舉辦音樂分享會。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第十一屆金音創作獎報名件數2,727件，少於去年的2,744件。頒獎典禮於臺北流行音樂中心舉辦，由製作人陳君豪擔任評審團主席。J.Sheon拿下最佳節奏藍調歌曲、最佳專輯與最佳嘻哈歌曲獎3項大獎。阿爆以《kinakaian母親的舌頭》繼金曲獎之後再度獲得最佳專輯獎。最佳新人獎，則由9m88《平庸之上》獲得。特別貢獻獎頒給了具有25年歷史的女巫店及創辦人彭郁晶，相當具有意義。2011年曾因音樂展演空間業的規範，這間Live
      House差點無法繼續下去，這意味著，臺灣的流行音樂發展不僅是音樂本身，場地的問題也是一大考驗。後在臺北市文化局的協調下，得以繼續經營，才有今天的獲獎。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金音創作獎是文化部金曲獎之外的另一項音樂大獎，其與金曲獎的區別，按文化部的官方說法為，把金曲獎提升為明星級、國際化的音樂獎項，而金音獎則是培植創作人才的搖籃。所以，金音創作獎的目標鎖定在尋找新世代臺灣音樂的創作力，並沒有以語言來分獎項，而是著重於國內音樂生態，設立搖滾、民謠、電音及嘻哈等4類不同樂風的獎項，並從第二屆起更增設爵士及節奏藍調2類，期盼讓金音創作獎能涵跨更多元之樂風種類。另外，也設置最佳現場演出獎及最佳樂手獎，更為獎勵海外華語創作，設置「海外創作音樂獎」，相當完備流行音樂生態。2018年第九屆金音創作獎啟動為期3年的轉型計畫，該年金音創作獎首度改採主席制，由主席召集專業評審團；第十屆則調整獎項分類，包括「節奏藍調」、「另類流行」與「跨界或世界音樂」獎項，以反映當代創作現況；今年則將「海外創作音樂獎」轉換為「亞洲創作音樂獎」，音樂作品不再設限於華語作品，而是放眼亞洲各國之個人或團體報名。且本屆金音創作獎得獎獎金由10萬元提高至15萬元，藉以激勵更多元的音樂創作。然近年卻引發不少問題，例如金曲獎與金音獎2個獎項的界線越發模糊，不符文化部所訂定的前提；設有「亞洲創作音樂獎」的目的是要打開國際市場，此次邀請到國外的評審有韓國的朴仁烈與日本的龜田誠治，到底海外評審之比重要如何拿捏，才能使這個獎項更在亞洲具有地位，仍是需要深思；評審團主席在評審過程，其所扮演的主導性為何等，以上的問題，尚須文化部進一步的積極處理，讓此獎項更有益於流行音樂的發展。
    </p>
