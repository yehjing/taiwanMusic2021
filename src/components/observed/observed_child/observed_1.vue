<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;江之翠劇場《朱文走鬼》原訂4/4、4/5演出，因武漢肺炎疫情取消，改於4/4晚上19:30線上直播演出，演出現場邀請20名貴賓採梅花座入席觀賞。2020年江之翠劇場重演2007年《朱文走鬼》，仍然保留梨園戲原來的口白唱腔與身段科步，在劇情開始與結尾加入日本舞踏演員說書段落，以及在部分過場，演員採舞踏肢體行動。演出人員幾乎是原班人馬上場，後場樂師的演奏表現以及與前場演員搭配默契不錯。本場演出雖然取消售票，但保留演出以現場直播方式，讓無法到場的觀眾仍能在網路觀看演出，是很好的應變措施。當晚收看直播的觀眾人數，有500多人。直播結束，影片於YouTube頻道直播連結至4/5晚間12:00截止。</p>
<p><strong>（三）臺音講堂「聽聽吳素霞的南管藝術」從韓熙載夜宴圖聽見傳統音樂之美示範講座</strong></p>
