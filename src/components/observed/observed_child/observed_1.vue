<h4><strong>三、第二屆新綠妍萌－青少年南管音樂會</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北藝大傳音系原本預計在2020年5月舉行「第二屆新綠妍萌－青少年南管音樂會」，因為武漢肺炎疫情關係，延至2020年11月才順利舉辦，此次共襄盛舉的5個團隊為新北市正德國中南管團、新北市立達觀國中小南管社、彰化縣文化局馬興國小南管研習班、彰化縣鹿港國中南管樂團、鹿港聚英社南樂團。南管面臨少人聽、少人學的傳承推廣危機，在中小學校園推廣、成立南管社團更是艱難，並且受限於學習與練習時間不足，同學們所學樂曲有限，所以此次有少部分單位曲目重疊，就採用中場休息方式，區隔上、下半場，相同曲目彈性安排，分別在上、下半場演出，就不致於違反同場整絃曲目不重複的慣例，也避免形成拼館較勁的氛圍。本場活動也是秉持著「讓共同為南管傳承努力的學校單位與民間館閣的夥伴們，以及學習南管的兒童、青少年朋友們能有相聚認識、觀摩交流的平臺」的想法，讓大家相聚觀摩同樂。</p>
