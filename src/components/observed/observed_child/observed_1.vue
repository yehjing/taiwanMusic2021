<h4><strong>六、廈門第五批市級非物質文化遺產南音代表性傳承人卓聖翔</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;卓聖翔被廈門市文旅局認定為第五批市級非物質文化遺產南音代表性傳承人，該認定並非是臺灣的文化政策，但卓聖翔是以臺灣同胞身份被認定，所以也列入本年度紀事。卓聖翔為福建南安人，7歲受堂兄卓聖煌啟蒙學習南管，也曾到廈門集安堂、錦華閣學習，後來師從紀經畝先生、白厚先生。移居香港時，參加香港福建體育會南音戲劇組，到菲律賓時也加入國風郎君社，拜林玉謀為師學習指譜。之後，應新加坡湘靈南音社社長丁馬成邀請擔任音樂指導並專職作曲。1995年到臺灣定居，從事南管教學，曾指導串門南樂團、松山奉天宮南樂團，只是與臺灣南管館閣甚少往來。2006年臺北市兩岸南管音樂推廣協會成立，卓聖翔與發起人林素梅以及羅純禎為該協會主要成員，近幾年又積極前往廈門參與教學計畫。卓聖翔擅長琵琶彈奏與作曲，與新加坡湘靈南音社丁馬成社長合著完成新編創作曲《南管精華大全》，在臺期間，也出版多套南管錄音，如《南管曲牌大全》、《南管指譜詳析》、《水調歌頭：千里共嬋娟》。</p>
<h4><strong>七、國際展演與交流</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因受武漢肺炎疫情蔓延全球的影響，以致於2020年不少活動計畫被取消或延期。國際展演方面，2020年南管是沒有出國演出的，但在6月有一場與國際略有相關的活動。成立於紐約的國際表演藝術協會（ISPA）原已選定6月初在臺北市舉行「地區年會」，也被迫取消活動，但該機構特別將議程中最受歡迎的「新作提案」，改採網路直播方式，讓評審團精選的10個國際製作得以向全球展現，當中有6個來自臺灣的表演團隊，而與南管相關的拾念劇集《大神魃．世界之夢》與江之翠劇場《行過洛津》名列其中。6/3的直播，各個團隊向線上觀賞的會員簡報作品的內容與特色，並且回應評審團代表的提問，希望透過「新作提案」爭取國際邀演的合作機會。此外，文化部與原承辦該活動的辜公亮文教基金會合作，推出「臺灣焦點－線上展演」，安排這6個團隊與其他4個單位的演出作品，於6/8至8/30在網路播出，內容包含音樂、舞蹈、戲劇，有傳統也有現代，透過這個網路平臺，向全球展現臺灣表演藝術的多元能量，而臺灣觀眾也趁機大飽眼福。拾念劇集《大神魃．世界之夢》與江之翠劇場《行過洛津》在該平臺播放的是濃縮為30分鐘的版本。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南管國際交流部分，世界南音聯誼會原本預計在2020年舉辦「第四屆世界南音大匯演」，由馬來西亞的馬來西亞雪州瓜雪暨沙白縣福建會館承辦，活動日期為5/1到5/4，活動地點在適耕莊與吉隆玻。活動策劃組林素梅老師在2019年12月便已陸續聯絡邀請各地館閣，南聲社、閩南樂府、華聲社等館閣以及臺北藝術大學傳統音樂學系南管樂師生都接獲邀請參與。原訂2月底截止報名，不料疫情急速變化，2/17通知：因疫情影響延期，如果疫情4、5月可以平息，活動在8月底舉行。無奈，活動一延就延過了2020年。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，閩南樂府報名由中國福建省廈門市思明區文化和旅遊局主辦的「南國古韻，音你而美」第四屆「思明杯」南音大會唱活動，該活動透過線上視頻影像評審，10/10初選優秀團隊參與。10/24於廈門市思明區文化館演藝廳舉行決賽，因武漢肺炎疫情關係，活動除現場演奏之外，還開設雲端分會場連線直播境外團隊的演出影片。21個演出團隊，香港福建體育會南音社、新加坡湘靈音樂社、台北市閩南樂府、馬來西亞雪隆同安會館南音組、澳門南音社等即是以視頻連線方式參與。決賽結果：6團最佳演繹獎、4團最佳創意獎、2團最佳新銳獎、5團優秀展演獎、4團優秀組織獎，閩南樂府高齡已逾85歲的資深曲腳林卿女士演唱〈望明月〉獲得最佳演繹獎。</p>
<h3><strong>結語</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;險惡的2020年，因武漢肺炎病毒蔓延，疫情擾亂全世界的日常秩序，臺灣因為超前布署、防疫措施得宜、全民配合防疫，才能疫情控制得不錯，讓大家生活如常。傳統的南管館閣不是職業團體，所以在這波疫情之下，並不會因藝文活動延期、取消而影響原本的經濟收入。活動方面，館閣每年固定的郎君祭典、整絃、配合館址所在廟宇的祀神儀式，大多都未受影響，僅有少部分取消或縮減活動規模；拍館、傳習課程部分，亦僅有少部分館閣在2至5月疫情最緊張時期，暫停一小段時間；國際展演、交流，因為活動延期或防疫政策設限，本年度未有館閣團體出國演出、交流。因為疫情關係，文化部今年才擬定推行的「文化部對受嚴重傳染性肺炎影響發生營運困難產業事業紓困振興辦法」政策，促使部分團體申請、獲得補助而錄製南管相關影音資料，雖然紓困政策是非常態的藝文補助，但對館閣而言，卻是非常好的機會，運用該筆經費為館閣保存自己的影音紀錄，未來也能提供後學一份參考資料。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南管薪傳狀況，有憂也有喜。海寮普陀寺清和社方水拋年初病故，祀神儀式能唱《金爐寶篆》的男性曲腳，僅剩2位，1位是年歲稍長的方文賴，另1位是方文賢，但方文賢之前車禍受傷，目前比較不方便久站，祀神儀式又約需40分鐘，所以會比較辛苦；清和社固定參加西港慶安宮三年一科的西港刈香，真的很需要新血的注入。高雄崎漏正順廟正聲社幾位資深館員，隨著年歲增長，40分鐘左右的祀神儀式，體力已逐漸不堪負荷，2020年部分祀神場次已由賜福宮振樂社館員支援。臺中市的文昌公廟南管團目前已無聘請老師指導，團員自行練習；屏東的東港鎮海宮南樂社也因前理事長退休，現今呈暫停狀態。上述這些情況很是令人擔憂。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;雖然薪傳狀況有讓人憂心的地方，卻也有能期許未來的歡欣所在。清和社招生困難，館員方秀連於是教導她的女兒李珮榕以及年幼的孫子王星皓、王星漢、王星辰學習，目前祖孫三代皆能參與清和社活動，3位小朋友每每上場都是吸引注目的焦點。南聲社資深館員黃美美的孫子方秉棟，從嬰孩時期就跟著阿嬤到館閣，長期耳濡目染，目前尚在唸幼稚園中班，11/22隨著阿嬤參加茄萣金鑾宮振南社秋祭曁整絃會奏，一曲〈老爺聽說〉，宏亮的聲勢與自信穩健的臺風，驚艷全場。位於彰化和美的雅月齋，偶爾會應邀出陣、奏唱南管，團長的一對兒女也在媽媽許宜茜的教導下學唱南管，課餘時間，有時會跟著父母出陣、唱南管，2位小朋友也非常可愛。鹿港聚英社2位年輕館員，目前就讀七年級的陳庭萱與五年級的陳昱蓁，是4年多前，父親陳建合重回聚英社時，跟隨到聚英社學習南管，2人是聚英社極力栽培的新秀。南管傳習招生往往會有些困難，如果館閣的館員能將親朋好友帶進來學習，這是最直接也最快速的注入新血方式。</p>
