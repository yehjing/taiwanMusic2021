<h3><strong>貳、政府公部門、藝文單位舉辦的南管活動</strong></h3>
<h4><strong>一、文化部文化資產局</strong></h4>
<p><strong>（一）戲苑．遊藝－南韻悠揚喜迎親</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「戲苑．遊藝」系列活動共有謝平安收冬戲、吟唱吹響在石板屋下、人生之樂看見滾歌仔、南韻悠揚喜迎親、指掌間戲說大稻埕、復刻記憶的廟口亂彈等6個場次，各開放50位研習名額，藉由講座體驗、古蹟導覽、現場演出等方式，帶領學員深刻體驗瞭解傳統文化。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2/29南韻悠揚喜迎親場次報名額滿，本活動於鹿港新祖宮舉行，內容包含南管傳統音樂與戲劇觀賞以及民俗婚禮文化體驗。文化部文化資產局蕭宗煌次長蒞臨致詞，鹿港鎮長許志宏夫婦扮演男方家長，鹿港鎮洛津里長黃文秋夫婦扮演女方家長，新祖宮主委、玉珍齋總經理也參與盛會。現場同時吸引不少新祖宮香客與觀光客駐足觀賞。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;活動首先由林吳素霞老師簡介南管音樂與南管古樂迎親儀式，合和藝苑再演示娶親前一晚的南管整絃與拜天公儀式南管祀神，迎親過程熱鬧喜慶，但由於踩街位於觀光區狹窄老街，加上觀看、拍照人數不少，以致場面動線有稍微擁擠混亂。學員在了解嫁妝古禮用具與觀賞鹿港古蹟建築之後，再欣賞南管戲〈小登科〉演出，最後由林吳素霞老師簡介南管戲，並示範教學〈天星〉，帶領學員體驗南管戲身段動作。整個活動內容非常豐富精彩，充滿了歡樂喜慶。</p>
<p><strong>（二）辦桌．看戲－戲中滋味</strong><strong>在地口味</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部文化資產園區一桌二椅舞臺的傳統表演藝術活動，2020年以「辦桌<strong>．</strong>看戲」形式，讓觀眾一邊看戲、一邊品茶吃茶點，而茶點以「戲中滋味 在地口味」為概念，選擇演出團體所在地的特色名產，安排茶點與茶飲。本系列活動的觀眾席，在座位區擺設9張辦桌的圓桌，每桌約6-8個座位，這樣座位設置，其實拉遠了觀眾欣賞演出的距離。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;活動自6/25開始至10/3結束，32場節目邀請知名團隊以及重要傳統表演藝術保存者帶領其傳習計畫藝生參與演出。8/22公親寮清水寺天子門生陣場次，以「歌詠太平：太平歌曲唱與導聆」為主題，由陳昭瑋主持人開場引言，再由公親寮王金樹里長導聆，介紹祖師爺鄭元和的故事、公親寮清水寺天子門生的歷史以及演奏的樂器種類。9/5清雅樂府「園內花開－南管饗宴」由邱一峰老師主持開場引言，再由黃珠玲團長介紹清雅樂府的歷史、南管樂器種類。9/6合和藝苑「郭華買胭脂<strong>．</strong>入山門」，由邱一峰老師主持開場引言、林吳素霞老師介紹後場樂器「南鼓」與說明南管戲的特色，再接著演出。林吳素霞老師下半場也親自上臺司鼓。現場觀眾不少，和鳴南樂社絃友特地到臺中觀賞清雅樂府、合和藝苑演出。本活動皆有現場直播，提供未能前來的朋友線上觀賞。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其實，現場觀眾都很專注欣賞演出，基本上不會在演出中一邊吃東西、一邊看表演。之後若有類似活動，主辦單位可以考慮直接送報名觀眾外帶點心餐盒，拉近觀眾與舞臺的距離，或許會比較有利於欣賞演出。</p>
<p><strong>（三）技藝傳薪－</strong><strong>2020</strong><strong>重要傳統表演藝術傳習成果展演</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部文化資產局於11/21-11/22、11/27-11/29連續5天，在文化部文化資產園區舉辦「技藝傳薪<strong>－</strong>2020重要傳統表演藝術傳習成果展演」。活動一方面考核驗收各傳習計畫藝生學習成果，另一方面藉由演出推廣、拓展欣賞人口及展現傳統表演藝術的價值。「南管戲曲/林吳素霞」場次於11/22晚上18:30在園區一桌二椅舞臺演出，此次演出南管戲劇目為《郭華買胭脂》第三齣〈入山門〉，由藝生魏秀蓉、郭子瑄與合和藝苑館員張雅茹分飾月英、梅香、郭華。</p>
<h4><strong>二、國立傳統藝術中心</strong></h4>
<p><strong>（一）</strong><strong>2020</strong><strong>共約南管．春．茶席</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3/8「2020共約南管．春．茶席」分上午、下午各1個場次，每場次觀眾40人，觀眾席共擺設6張長桌，1桌7個位置，桌面布置雅緻，燈光微暗柔和，觀眾分區而坐。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本演出為南管音樂、戲劇演出與茶席搭配的活動，時間約120分鐘無中場休息。演出由李易修主持導聆，分為3段節目，第一段南管奏唱音樂、第二段為南管演奏與腳鼓擊奏、第三段是結合日本舞踏的梨園戲「朱文走鬼」片段演出。每段演出後中場休息時間，則由資深茶人趙崑秀帶領6位茶人進行茶席，讓觀眾品茗，時間約20分鐘，期間播放臺灣音樂館出版的《承啟臺灣南管》專輯中的〈荼蘼架〉、《四時景》樂曲，在雅致氛圍中品茶、聽樂，是非常美好的雙重享受。本場次演出有為4/4-4/5《朱文走鬼》演出宣傳的意涵。</p>
