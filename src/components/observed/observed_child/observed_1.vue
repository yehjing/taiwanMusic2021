<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2天活動由於大雨不停歇，不僅限縮戲臺下的觀賞空間，吵雜的下雨聲也影響臺上的演出者演出。本次整絃，台北市閩南樂府管絃研究會的曲腳林卿女士為全場年紀最長者。12/6整絃活動到中午，最後由合和藝苑煞譜《梅花操》，再舉行送神儀式，活動才圓滿結束。</p>
<h4><strong>三、國立中正紀念堂管理處</strong></h4>
