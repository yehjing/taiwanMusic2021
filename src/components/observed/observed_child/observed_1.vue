<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年整絃活動，礙於武漢肺炎疫情之故，上半年影響較大。合和藝苑春祭整絃原本就無對外邀請館閣參與，每次僅邀請兄弟館南聲社參加，因防疫關係，也就未邀請南聲社前來，只有自己館閣團員參與。南聲社春祭也取消對外邀請館閣參加，但有少部分高雄歷史博物館陳嬿朱老師指導的研習班學員與南聲社研習班學員前來參與。南聲社原先在春祭、秋祭整絃，本館只負責「起曲」，接著都是安排與會絃友來賓演唱，本館館員較少有機會唱曲，此次因無對外邀請，所以由南聲社資深曲腳到新生代曲腳輪番演唱，連平常擔任簫腳的林麗馨都演唱一曲，高史博研習班學員宋家慶、臺北絃友黃瑤慧也前來參與唱曲。彰化縣文化局孟府郎君春祭則取消各研習班整絃會奏，祭典奏唱的《金爐寶篆》祀套採用精簡版，慢頭部分唱完直接唱一二拍部分。聚英社春祭也是僅有自己館員參與，並縮短活動時間，祭孟府郎君祀套《金爐寶篆》、祀先賢祀套《畫堂彩結》皆採精簡版奏唱；整絃也僅奏唱－噯子指：〈水月耀光〉，曲：〈山不在高〉、〈碧雲天〉、〈感謝公主〉，譜：《走馬》；最後煞譜是由國小六年級陳庭萱彈琵琶、四年級陳昱蓁彈三絃，姊妹2人能把《走馬》彈奏得撩拍穩定、速度順暢，值得嘉許。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;下半年因疫情趨於穩定，館閣秋祭正常舉行，或許是因為配合上半年防疫措施，南聲社春祭未能邀請大家前來，壓抑了近半年，所以10/17秋祭整絃參加人數暴增，金門浯江南樂社也遠從金門來參加。當天報曲非常踴躍，集斌社還取消原本要唱的〈羨君瑞〉，指、曲、譜曲目總計39首，一直到晚上19:40左右才煞譜結束，相較以往大約多了1個半小時。整絃過程，絃友情誼交流，聊開話題，難免忘記控制音量，所以南聲社也張貼「請輕聲細語 敬重雅音」提醒大家。合和藝苑孟府郎君秋祭整絃也如往常廣邀館閣參加。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;合和藝苑孟府郎君秋祭整絃於10/18舉行，本年度仍以排門頭形式進行，整絃門頭依序為「【山坡】過【相思引】過【玉交】過【福馬郎】」，【山坡】為五六四亻乂管七撩拍曲，【相思引】是五空管三撩拍曲，五六四亻乂管【玉交】、五空管【福馬郎】則是一二拍樂曲。應邀參與館閣有：華聲社、詠吟南樂社、和鳴南樂社、南薰閣、中瀛南樂社、長隆南樂社、清雅樂府、鹿港聚英社、遏雲齋、彰化南管實驗樂團、南聲社、振聲社、廣益南樂社、臺北藝術大學傳統音樂系（以下簡稱北藝大傳音系）以及黃瑤慧絃友，相較2019年，報曲的館閣比去年多了南薰閣、中瀛南樂社、廣益南樂社、北藝大傳音系。整絃曲目如下：</p>
