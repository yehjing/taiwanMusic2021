<h4><strong>二、館閣參與寺廟相關活動</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年農曆新年春節期間，武漢肺炎傳染病訊息剛傳開不久，也還未見疫情緊張，所以大家還是歡欣過新年，海寮普陀寺南樂清和社、茄萣金鑾宮振南社以及鹿港聚英社等依然於初一進行廟前新春整絃活動。之後，因配合防疫措施，不少寺廟活動延期或取消，南管參與寺廟活動也因此受到一些影響，如右昌光安社，每年本廟神明聖誕或隨本廟至交陪境向神明祝壽祀神場次達20多場，今年就取消去交陪境祝壽祀神，只保留本廟部分。不過基隆市閩南第一樂團還是維持每月農曆初一、十五排場，北管組、什音組、南管組除正月初一新春賀正分時段各別排場之外，3組每3個月輪值演奏，南管組於2/23、3/8、5/23、6/6、9/2、11/15、11/29輪值以及7/26朱府千歲聖誕值東至檺林宮整絃。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;館閣參與寺廟相關活動，本年度最特別的是1/1振聲社參與「噶瑪噶居寺元旦供佛祈福護生法會」，以及5/17、5/18福壽宮南管團「先嗇宮神農大帝聖誕－帝王宴南管整絃」與12/25、12/26南管新錦珠劇團參與「中和枋寮威遠壇舉行200週年壇慶－禳災祈安福醮」活動演出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;噶瑪噶居寺是全臺最大的藏傳佛寺，振聲社應邀前往，以南管祀神與整絃，這應該是南管第一次參與藏傳佛寺的活動，有這樣的機會很好。先嗇宮主祀神農大帝，適逢神農大帝聖誕，廟方舉辦帝王宴祝壽，禮聘大龍峒福壽宮南管團整絃排場；大龍峒福壽宮南管團配合帝王宴各進獻餐飲時程演奏，雖然並無南管祀神儀式，但能在進獻神明餐飲時演奏，也是非常好的機會。中和枋寮威遠壇舉行「200週年壇慶－禳災祈安福醮」，林威呈道長邀請南管新錦珠劇團至新北市永和保福宮演出南管音樂，提供信眾欣賞，參與演出人員有南管新錦珠劇團團長陳廷全，以及朱永勝、傅貞儀、黃瑤慧、王議宏、周秀釵、任達康、林家迎、陳美烈、劉雪美等絃友；這是林道長繼2019年「樹林濟安宮建廟230年暨東遷百年五朝清醀醮典」向濟安宮推薦邀請南管演出之後，於今年在他主持的威遠壇慶典活動，再度邀請南管演出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，今年度有部分王醮活動，邀請南管祀神、整絃，如臺南市的佳里金唐殿庚子蕭壠香科五朝王醮、高雄市茄萣區的白砂崙萬福宮庚子科五朝王醮。佳里金唐殿蕭壠香科五朝王醮每三年一科，庚子年香科五朝王醮時程自2019/9/6取王船龍骨木料開始到2020/2/20送王、謝燈篙結束。在送王前一天上午11點左右舉行感謝千歲爺的宴王大典，金唐殿禮聘清和社參與盛典，於宴王大典前先在衙門內整絃，待宴王大典進行，宣傳「御前清客進殿」，清和社男性館員再奏唱〈不帶釵〉步入王府，配合三獻禮儀式進行祀王，奏唱《金爐寶篆》祀套，祀王結束，再唱〈告蒼天〉步出王府。高雄市茄萣區白砂崙萬福宮相隔18年後舉行庚子科五朝王醮，禮聘灣裡慶安堂和聲社參與：恭請新王（請王）、王船出廠、前往出境、恭送新王（送王）等大典；王醮時程自2020/5/24拔主會、黑旗官、紅旗官到2021/1/23謝燈篙結束。7/26請王大典，萬福宮主會與文武陣頭自南萣橋接駕和聲社，再徒步前往茄萣海岸請王地點，萬福宮進行請王相關科儀後，於沙灘上設置香案，和聲社隨即進行祀神儀式，奏唱《金爐寶篆》祀套，祀神儀式結束再十音合奏〈魚沉〉，終於在南管〈魚沉〉樂聲中，新王降臨。萬福宮恭請新王回新王府安座後，和聲社再至新王府進行安座祀神儀式，奏唱《金爐寶篆〉祀套。12/20王船出廠大典，和聲社先至新王府進行祀神儀式、整絃，等待王船添載完畢，再至王船香案前祀神。新王安座、王船出廠，南管進行祀神時，各陣頭也輪流來拜旗，不定時有鑼鼓或其他音樂聲響傳來，南管祀神音量相對較弱，夾雜著喧鬧的陣頭聲響，演奏人員更必須專注聆聽彼此的樂聲，才不致於被影響。</p>
