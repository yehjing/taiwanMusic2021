<h3><strong>肆、南管年度重要紀事</strong></h3>
<h4><strong>一、武漢肺炎疫情影響南管活動</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1/21第一例武漢肺炎確診出現後，臺灣對於這來勢洶洶的病毒更加提升防範，防疫措施讓大家口罩戴好、勤洗手、酒精消毒、量體溫&hellip;，第二學期也延後2個星期至3/2開學，雖然還能生活如常，但隨著疫情緊張，提高防疫措施，許多公共場所出入規定嚴格，藝文活動場所也受到相關約束，演出活動不是延期、取消，就是觀眾席位置調整，如國立臺北藝術大學舞蹈廳演出場次只能有20名觀眾，座位間隔很大。特別是在澳洲音樂家布萊特狄恩（Brett Dean）來臺於2/28、3/1與國家交響樂團演出，返回澳洲後，3/5確診，消息震撼藝文界，使得藝文演出的防疫措施更加謹慎小心，如隔日3/6臺灣國樂團便宣布即日起暫停銷售3、4月份音樂會票券，3/17、3/27演出場次取消。3/21國立傳統藝術中心公告2020臺灣戲曲藝術節延期，13檔節目先取消，其所屬的國光劇團、臺灣國樂團、臺灣豫劇團也自即日起至4月底止室內演出節目皆延期辦理。其中與南管相關的演出取消有4/4-4/5江之翠劇場《朱文走鬼》以及5/9-5/10心心南管樂坊、法國憶古樂團「五湖遊－東西古樂印象光影」。之後，3/31公告江之翠劇場《朱文走鬼》於4/4晚間19:30做線上直播，這場線上直播演出吸引不少觀眾上網欣賞。當時藝文演出活動受到不小影響，不是延期就是取消，僅有少部分改換舉行線上直播演出，如臺北市立國樂團的「台灣加油線上音樂會」，吹管樂部團員用隔板、其他團員戴口罩完成演出。而國立傳統藝術中心臺灣音樂館原訂4/12舉辦臺音講堂「聽聽吳素霞的南管藝術：從韓熙載夜宴圖聽見傳統音樂之美」示範講座也公告延期。此外，也有部分平臺提供以往的藝文演出影片讓民眾在線上欣賞，如「公視+」影音平臺自4月中旬上線漢唐樂府2009年與法國甜蜜回憶古樂團合作演出的「教坊記」以及漢唐樂府2011年與臺北市立國樂團合作演出的「盤之古」，2部影片觀看期限至2022/4/19，而5月初又上線漢唐樂府2012年作品「殷商王．后」，觀看期限至2020/6/30，這3部與南管相關的影片，提供當時未能觀賞演出的南管人一個欣賞機會。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上述是南管藝文活動受到影響的狀況，館閣活動部分，振聲社4月份團練停止、華聲社3/7-6/6有記茶行拍館活動停止、和鳴南樂社研習班自3月中開始休息1個月。圓山福壽宮農曆2月2日福德正神聖誕南管祝壽整絃取消，而右昌光安社2020年祀神場次僅限本廟右昌元帥府與三山國王廟的神明聖誕，交陪境的祝壽祀神活動皆取消。此外，南聲社、聚英社的春祭活動，也取消對外邀請館閣參與，僅自己館員參與。中華絃管2020年第一期研習成果發表音樂會改為成果錄影，於6/13錄製：簫指〈出庭前〉、〈霏霏颯颯〉，曲〈山險峻〉，譜《四時景》、《梅花操》3-5節。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因應武漢肺炎疫情對藝文產業之衝擊，文化部於3/12公告「文化部對受嚴重特殊傳染性肺炎影響發生營運困難產業事業紓困振興辦法」，其他相關文化單位也配合政策提供援助，如中華絃管研究團申請臺北市文化局因應疫情專案補助辦理2020年中華絃管紀念先師暨傳習成果發表會。部分南管館閣、太平歌天子門生陣有申請紓困計畫，如華聲社、合和藝苑、港墘太平歌團天子門生陣、麻豆巷口集英社，以補助經費進行錄音或錄影、樂譜整理。雖然是因應疫情的紓困政策，但能運用補助經費，保存館閣曲目或活動資料，是非常好的規劃。</p>
