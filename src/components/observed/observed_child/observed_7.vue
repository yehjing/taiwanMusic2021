<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上面的資料透露出幾個重要訊息，首先，名稱上，部落族人慢慢擺脫「五年祭」這個以往大部分學者的稱呼，而改成「maljeveq」這個族語名稱，或是「祖靈祭」這個強調神的身分的稱呼。同樣的情形，「六年祭」這個名稱也由族人改成「pusau」或是「送祖靈祭」等的稱呼，顯示族人已經開始意識到，以往學者的稱呼，到了這個時代似乎已有改變的必要，部落族人紛紛朝向「切合祭儀精神」這樣的目標修改，直到現在，「maljeveq」、「pusau」、「祖靈祭」、「送祖靈祭」等稱呼，就比較能被族人接受。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;平埔族的祭儀活動似乎已成為每年的常態性活動，目前，每年持續辦理祭儀活動的族群有西拉雅族、馬卡道族與大武壠族。西拉雅族的活動有「佳里北頭洋平埔夜祭」（臺南市佳里區北頭洋平埔族文化園區）、「蔴荳老祖夜祭」（臺南市麻豆區蔴荳老祖夜祭祭場）、吉貝耍Ta-ai夜祭（臺南市東山區吉貝耍大公廨）、番仔塭阿立祖聯合海祖祭（臺南市七股區大埕里番仔塭阿立祖公廨）、六重溪夜祭（臺南市白河區六重溪平埔文化園區）、隙仔口夜祭（臺南市山上區豐德里隙仔口51號）、番仔田夜祭（臺南市官田區番仔田公廨）、大內頭社太祖夜祭（臺南市大內區頭社太上龍頭忠義廟）等。馬卡道族的活動有加匏朗「夜祭」文化活動（屏東縣萬巒鄉加匏朗部落仙姑祖廟）、老埤部落夜祭（屏東縣內埔鄉老埤社區活動中心）、加蚋埔阿姆寮夜祭（屏東縣高樹鄉泰山村加蚋埔阿姆寮）。大武壠族的活動有荖濃夜祭（高雄市六龜區荖濃公廨前廣場）、小林夜祭（高雄市甲仙區平埔文化園區小林公廨）。</p>
