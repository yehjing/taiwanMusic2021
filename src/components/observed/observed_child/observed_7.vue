<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;疫情如此嚴峻，人類的文化生活也遭受前所未有的衝擊。2020上半年，當政府因為疫情的關係，頒布了室內聚會場所必須要遵守的規範之後，民眾便開始面臨演出節目取消或是延後的消息，而更令愛樂人士扼腕的，是一些出色的國外藝術團隊，因為疫情的關係延後來臺或是直接取消演出，讓很多人的期待落空。最近的例子是莫斯科古典芭蕾舞團（Moscow Classical Ballet），原本預計在2020年12月，於臺北國家戲劇院及高雄衛武營國家藝術文化中心演出，一行於11月29日來臺，不料4名成員確診武漢肺炎，除了原訂於國家戲劇院登場的首演取消外，高雄的演出也確定取消。無獨有偶，號稱「2021全世界第一場演唱會」的年度盛事，是張惠妹在故鄉臺東濱海公園舉行的「BMW M 20/21 aMEI UTOPIA EAST」跨年演唱會，該演唱會雖然沒有取消，但是因為預估將超過5萬人前往欣賞，因此臺東縣政府提出10項防疫重點，主辦單位戰戰兢兢，害怕這個演唱會成為防疫的破口。另外，臺中國家歌劇院藝術總監邱瑗表示，2020年上半年歌劇院演出受到疫情影響，幾乎取消三分之一節目，不少國外團隊無法來臺。武漢肺炎帶來的衝擊有多大，前面的例子都可以清楚的看到。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而慶幸的，要跟國際上動則幾百萬確診個案，單日死亡千人的國家比起來，疫情在臺灣相對緩和。17年前的SARS風暴，病毒與死亡的恐懼，讓臺灣人民養成時時警覺，時時保護自己的習慣。截至2021年1月，武漢肺炎累計的確診病例只有843人，顯示臺灣人在防疫的這個工作上，可說是做到滴水不漏，受到各國人士的讚賞。或許也因為臺灣在防疫上面做出的好成績，我們觀察並統計臺灣一整年的音樂活動，似乎並沒有因為疫情的關係而萎縮，所不同的，可能是進入音樂場所前的必要工作增加，比如個人資料實名制的登記，口罩的配戴，酒精的噴灑等等。某些地方，為了兼顧音樂會的持續進行以及疫情防治，發展出全然不同的音樂會型態，這些新的音樂表演現象，都隨著這次武漢肺炎的風暴而產生。</p>
