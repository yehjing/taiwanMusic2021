<h4><strong>四、傳習活動</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳習活動的實施大致上有2種活動類型，一種是以部落的某個學習空間，進行語言或古謠的學習，另一種是結合動態的活動，所做的傳習活動。第一種的傳習方式，如排灣族ludja古謠學習班（屏東縣安平部落頭目家屋外）、原住民文化學程－魯凱傳薪樂舞（一）（高雄市原住民族部落大學）、拉阿魯哇族語夏令營（高雄市高中部落興中國小）、排灣族鼻笛夏令營（屏東縣泰武鄉比悠瑪部落）、排灣族supaw青年會「文化傳習系列課程」（屏東縣獅子鄉草埔村）、噶哈巫族「樂舞的記憶」歌謠班（南投縣埔里鎮蜈蚣社區活動中心)。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另一種結合動態活動的傳習方式，如佳興社區發展協會「排灣族傳說故事與歌謠」（屏東縣泰武鄉佳興部落）、屏東縣台灣原住民文化研究會「109年傳統藝術接班人駐園演出計畫－排灣族口鼻笛直播演出」（國立傳統藝術中心蔣渭水演藝廳）、「拉森那（日出之地）」尋根之旅（高雄市拉阿魯哇文教協進會）、「109年傳統藝術接班人駐園演出計畫－排灣族口鼻笛」（國立傳統藝術中心蔣渭水演藝廳）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以上2種傳習方式，不論是找某個學習空間，進行語言或古謠的學習，或是結合動態的活動，2種方式都讓文化的傳習得以落實，並獲得最好的成果。</p>
<h4><strong>五、國際與兩岸交流</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以往，國際與兩岸交流的紀錄原本就不多，而且通常是集中在排灣族與魯凱族2個族群，而這次因為疫情的關係，各國紛紛取消遠赴他國演出的計畫，國內原本已經計畫要邀請的國外演出團體，也因為疫情的關係取消。如此一來，整年的國際交流活動就只有3筆，均為排灣族，第一筆是巴賴「藝術共鳴計畫－露水之蒔」與近藤康平共演（臺東鐵花村），第二筆是「2020民族音樂學國際研討會」（國立臺南藝術大學），第三筆是「第一屆ICTM原住民及後殖民語境樂舞研究小組國際研討會－排灣族鼻笛演出」（東華大學原住民民族學院國際會議廳）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018至2019年，排灣族和魯凱族許多優秀的和唱團體遠赴歐洲參加國外的音樂競賽，如排灣族於「2019勃拉姆斯國際合唱音樂節暨合唱比賽」得獎等等，今年都因為疫情無法參加任何比賽，非常可惜。</p>
