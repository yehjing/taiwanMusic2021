<h3><strong>參、族群音樂發展的深入剖析</strong></h3>
<h4><strong>一、武漢肺炎帶來的文化衝擊</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;前言已提及，2020年，全世界陷入一片恐慌與不安，武漢肺炎的傳播幾乎影響了全世界人民的生活。疫情發展的前期，武漢肺炎僅僅是某些地區的病毒感染，但是很快的，疫情迅速擴展，一下子就蔓延到了全世界，疫情一發不可收拾。而在疫情的另一方面，疫情的嚴峻，人類的文化生活也遭受前所未有的衝擊。2020上半年，民眾便開始面臨演出節目取消或是延後的消息，而更令愛樂人士扼腕的，是一些出色的國外藝術團隊，因為疫情的關係延後來臺或是直接取消演出，讓很多人的期待落空。莫斯科古典芭蕾舞團（Moscow Classical Ballet），於臺北國家戲劇院及高雄衛武營國家藝術文化中心演出，以及張惠妹在故鄉臺東濱海公園舉行的「BMW M 20/21 aMEI UTOPIA EAST」跨年演唱會，都受到了極大的影響。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂活動當中展演的部分，若碰到嚴峻的疫情，它的處理方式可以有幾個層次，第一種是直接取消，當表演團體的成員染疫，病毒的擴散與傳染的層面過大，特別是在密閉的空間裡，有可能讓所有的觀眾都染疫，活動因而取消。某些音樂會雖然不在密閉空間，但是因為群聚的因素也會取消，如「拉阿魯哇族2020miatungusu」（聖貝祭祭場）、「108學年度全國師生鄉土歌謠比賽－原住民語系」（桃園市客家文化館）、「《裡面的外面》戴曉君Tai Siao-Chun 野性女力演唱會」（永豐）、「魯凱族2020南島族群婚禮系列活動－開幕活動」（霧臺部落文化廣場）等，這些活動都為了防止病毒擴散而取消。第二種情形是做延期的處理，等到適當時機再恢復辦理，這些活動如排灣族「市集活動 野餐音樂會」（瑪家鄉禮納里部落瑪家穀倉廣場）、排灣族和魯凱族的「斜坡音樂祭」（屏東縣政府）、排灣族的「原住民表演藝術研習課」（屏東縣政府原住民文化會館）、排灣族的「驚嘆樂舞－2020臺灣原住民樂舞饗宴」（國父紀念館）等，這些活動後來雖然均恢復辦理，但辦理時程均在年末，與其他活動嚴重衝突，顯得很匆忙。第三種處理方式是活動照常舉辦，防疫工作卻加強處理。活動當中觀眾嚴守戴口罩、噴灑酒精、溫度過高不得進場等必要規定，除此以外，為了方便後來確診者以及接觸者的追蹤，進音樂廳之前得先填寫本人基本資料。第四種處理方式是室外活動照常舉辦，但是需要有人數的限制以及人與人之間距離的限制，當然，戴口罩、噴灑酒精、溫度過高不得進場仍舊是必要的工作。還有一種方式，是音樂會形式改成線上直播的方式。2020年3月24至27日，排灣族謝水能口鼻笛藝術團在國立傳藝中心蔣渭水演藝廳，參與「109年傳統藝術接班人駐園演出計畫－排灣族口鼻笛」為期5天的演出，第一天和第二天的演出均採觀眾入場的方式辦理，但到了第三天，政府單位因為疫情的關係頒布室內演出的規範之後，音樂會便改成觀眾不進場，全場直播演出的方式。</p>
<h4><strong>二、傳統智慧創作專用權的紛爭</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《原住民族傳統智慧創作保護條例》（簡稱《原創條例》）在2007年三讀通過，該法第一條即明訂：「為保護原住民族之傳統智慧創作（以下簡稱智慧創作），促進原住民族文化發展，依原住民族基本法第十三條之規定，制定本條例。」第三條也清楚的指出法令保護的項目為：「本條例所稱智慧創作，指原住民族傳統之宗教祭儀、音樂、舞蹈、歌曲、雕塑、編織、圖案、服飾、民俗技藝或其他文化成果之表達。」<sup>3</sup>但直到2015年，原民會才頒布詳細作法，並開始受理各個「傳統智慧創作」的審議。截至2020年7月，已經有74項創作通過登記，這當中與祭儀或是歌舞相關的申請案件總計18項。2018/8/1，原民會舉辦論壇，邀請「原住民族文化發展中心」前來進行開幕表演，卻未經同意使用奇美部落樂舞，引發侵權疑慮。原民會強調，這是雙方對法令解讀及溝通認知差異，演出勇士舞是在合理使用範圍內，然而奇美部落卻在臉書發聲明指出，原住民族委員會所屬原住民族文化發展中心（前文化園區管理局）未經奇美部落授權同意，即由其舞者逕行穿著奇美部落勇士（Ciopihay）服飾，表演Ciopihay階級之Pawali祭歌與舞蹈，此舉已嚴重侵害奇美部落之傳統智慧創作專用權。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;儘管，行政院原住民族委員會強調該辦法的立法背景，是基於「近年來，原住民迭有民俗創作遭受侵害，惟依法皆非能獲致保護，這凸顯了原住民民俗創作與現行我國著作權法所指之智慧創作本質上之不同，政府應特別立法保障。」的因素而設，但是排灣族地區仍發生了傳統智慧創作專用權的紛爭。2020年6月4日，行政院原住民族委員會公告核准了排灣族巴格達外家族傳統智慧創作專用權的證書，智慧創作名稱為「排灣族tjaravacalj巴格達外家族歌」（證書號數：排D000069）。不料，該項核准案，卻引發北部排灣族不同家族的疑問，排灣族魯凱族各部落領袖甚至於2020年8月30日，在臺灣原住民族文化園區國際會議廳，舉辦了一場專用權研討會，目的就是研討巴格達外家族所申請的專用權。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;要探討該事件紛爭的關鍵，我們得要從音樂的本質－歌詞與曲調來看。歌詞部分，排灣族善於在歌謠當中敘述家族的豐功偉業，舉凡家族創始、家族遷移經過、家族成員介紹、征戰的戰功、家族成員英勇事蹟、擔任頭目的榮耀等等，歌詞內容有記錄家族史的意義在，但是因為長久以來排灣族並沒有書寫文字的習慣，因此歷經千百年的口述歷史，有時候存在許多的變異，不同的家族也會有不同的說法，所以當某個家族在歌詞當中敘述其家族的事務，並以此歌曲登記傳統智慧創作專用權，就會出現紛爭。再者，旋律的部分也是另一個課題。排灣族與魯凱族大部分的歌謠都有共同的骨幹音，舉〈luljimai〉這首歌為例，該曲幾乎涵蓋了中北排灣所有地區，每個部落都有〈luljimai〉這首歌，所不同的只在歌詞或是旋律轉折的不同，其實就算是歌詞也會有很多相似的用語。再舉〈paljailjai〉這首勇士歌為例，這首歌曲不但風行於排灣族地區，到了魯凱族地區一樣也很普遍，所不同的只在語言，或是旋律的轉折。所以，行政單位要審核歌曲財智權的申請，就要非常謹慎的比對這些歌曲在整個區域分布的狀況。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《原住民族傳統智慧創作保護條例》立意良善，然而從2017年10月25日核准第一件證書以來，僅僅過了3年，許多實務上會碰到的困難還很多，音樂方面的案例常見的爭議有：僅以書面上的文字審核，未考慮是否與其他家族相似；未附上音檔，不清楚旋律為何；歌詞是否與不同部族重疊；申請案是否有部落耆老參與與確認等等，這些都需要部落族人以及行政單位的合作，才能針對條文內部的細節作正確的修訂。</p>
<h4><strong>三、「臺灣原住民音樂館」設立的必要性</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;承上面的主題（《原創條例》），行政院原住民族委員會公告核准了排灣族巴格達外家族傳統智慧創作專用權的證書，引發北部排灣族不同家族的疑問，各部落領袖於2020年8月30日，在臺灣原住民族文化園區國際會議廳，舉辦了一場專用權研討會，目的就是研討傳統智慧創作專用權當前所引發的諸多問題。這個事件正凸顯了2個重點，第一，長久以來，原住民唱歌跳舞，但是從來沒有想過要將這些傳統歌舞記錄下來，歌舞沒有做系統式的記錄與整理，年輕一輩的部落族人無法說出自身部落存在哪些原本就有的傳統歌謠。第二個顯現的重點，是行政單位並沒有積極為臺灣原住民的歌舞找一個家，100年前，日本人就已經對臺灣原住民的音樂的演變現象提出警訊，可是100年後，我們並沒有記取這個建議，以至於原住民歌舞資料分散四處，無法整合。凡此種種，更凸顯了臺灣原住民音樂館設立的必要性。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;筆者於年鑑調查期間，每每遇到任何傳統音樂的問題，想立即找出音樂相關的論述或是資料時，發現音樂資料竟散亂在各個單位，這些單位包括各地博物館、文化園區、中央研究院、大學音樂系資料中心、縣政府文化處、傳統藝術中心、各縣文化處、鄉公所圖書館，甚至是私人的圖書館、耆老的珍藏等等。筆者思索，除了我以外，是不是也有很多的學者，會在找尋原住民音樂文化資料方面，有很大的困擾。因此我認為，我們應該試著參考很多國家的做法，設立一個「臺灣原住民音樂館」，讓這些散亂四處的音樂資料找到一個家。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，因為原住民族的古謠與古典文學緊密連結，因此「臺灣原住民音樂館」的設立應包含音樂與語文2個領域，讓知識更豐富，更博大精深。而若再考量到演出的部分的話，「臺灣原住民音樂館」亦可以設一個表演的部門，而這個部門即是這幾年原住民族委員會原住民族文化發展中心（原為行政院原住民文化園區管理局）極力要籌組的「國家原住民歌舞團」。</p>
<h4><strong>四、國際交流貧乏的因應之道</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年，因為疫情等等因素，各國的表演場館幾乎呈現休止的狀態，國際交流項目的紀錄顯得相當貧乏，除了排灣族的3個紀錄以外，其他族群的國際交流皆為零。面對2021年，疫情並沒有減緩的情況，我們似乎也應該有一些心理準備，讓我們的音樂文化工作，不至於因為疫情的因素而停滯。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;首先，直播音樂會是現在最常使用的方式。幾個國際上知名的樂團，為了不至於讓樂團停擺，紛紛使用直播的方式辦理音樂會。2021新年音樂會「臺灣的聲音 新年音樂會」，元旦下午在國家音樂廳登場，新年音樂會總製作人施振榮表示，新年音樂會精彩可期，並將網路全球直播放送到全世界，要讓臺灣人以臺灣經典音樂為榮。五月天2021跨年演唱會「好好好想見到你Mayday Fly to 2021」，今年因為疫情關係，也改為線上收看演唱會轉播。目前的趨勢，大型音樂會因為聚集過多的觀眾，恐有爆發大規模傳染的疑慮，因此採取線上直播的方式越來越多。至於國際學術研討會或是跨國的音樂論壇，建議改採視訊的方式進行，視訊的方式不會有病毒傳染的危險，是目前防疫當局鼓勵採用的方式。以往音樂教學單位常常實施的大師班演講，或是一對一的大師班學習，亦建議改採視訊的方式教學，師生同樣可以達成學習目標（如國立屏東大學實施的音樂課程線上教學）。最後，善用影視廣播媒體也是很好的方式，希望電視臺與國外演出單位策略聯盟，學習者可以透過電視或是廣播，進行電視的學習以及觀賞國外優質的演出。</p>
<h4><strong>五、口鼻笛進階傳習的豐碩成果</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部的傳統表演藝術傳習工作，經過了幾年的培養，順利產生了第一階段傳習結業的藝生。然而，此階段的結業藝生，結業後因為生計的關係，回復到原來的生活型態，可以發揮其鼻笛技藝的空間不大。再者，2位結業後的藝生想要繼續吹奏口笛鼻笛與歌謠，但是傳統音樂的表演邀約並不多，辛苦習得的技藝因此越來越生疏，狀態打回原點。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部有感於此，乃依據文化資產保存法第九十二條、文化資產保存法施行細則第三十四條，啟動了「重要傳統表演藝術保存者－排灣族雙管口、鼻笛進階傳習計畫」（第一年）這個方案。文化部文化資產局重要傳統表演藝術保存者進階傳習計畫的傳習目標為：「針對完成文化部登錄重要傳統表演藝術第一階段傳習結業藝生，辦理進階傳習計畫，藉以培育掌握該項傳統表演藝術之文化整體知識、藝能項目之藝術表現特色、適合當代社會環境之藝能表演製作能力等全方位之新生代傳承者。」目標清楚的點出，進階計畫是針對已完成第一階段的結業藝生，所做的下一階段專業訓練。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;進階藝生第一年的成果展演是2020年7月26日，於屏東縣泰武鄉武潭國小平和分校舉行。因為該次的演出屬於進階藝生計畫案第一次的演出，因此其成效格外引人注意。筆者觀察現場的表現，我們發現謝水能鼻笛團的藝生確實有備而來，音樂會內容有口鼻笛獨奏、口鼻笛合奏、傳統歌謠與鼻笛演出、傳統歌謠與勇士舞演出、鼻笛與神話故事的組合。最難能可貴的，是節目當中安排了鄭尾葉（謝水能藝師的老師）與藝生隔空合奏鼻笛的畫面，祖孫藉由視訊設備同場演出，非常感人。對於該計畫第一年期末音樂會的評價，B委員說：「<span class="bkt">演奏會型態精心設計，搭配在地耆老、青年會共同演出，說書人細心預備與呈現，讓鼻笛真實回到部落與之共鳴，使在地歌謠樂舞重新組合與詮釋，創造多元音樂型態與在地連結，鼻笛音樂豐富化、意義化與價值功能再提升。</span>」D委員說：「<span class="bkt">音樂會構成也可見設計及用心，不僅有特色，也能確認藝生的成長。</span>」E委員的評價為：「<span class="bkt">今天屏東縣台灣原住民文化研究會製作這場鼻笛結合耆老歌謠之表演活動非常有意義，不僅讓鼻笛傳統音樂得以傳習，並且能應用於部落的日常生活中，更讓排灣族的生命中融入更多傳統音樂，讓傳統音樂成為生活之各種情緒之表達。</span>」</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;期末音樂會固然成功，但是我們仍整理出了幾個有關口笛鼻笛發展上的隱憂，第一個是藝生習藝時的經濟現況，他們需要多一點的經濟協助，讓學習無後顧之憂。第二個是表演團體使用口笛鼻笛的限制，笛子的音律問題似乎成為合奏上常常面臨的障礙。第三個是目前沒有建立口笛鼻笛音樂完整的音樂理論系統，沒有建立完整的系統，口笛鼻笛音樂理論的探討往往是建立在西洋音樂系統之上，跳脫不出。第四個，是口笛鼻笛有關於傳統與非傳統的爭議，在器樂的發展上，這些爭議只會越來越多，值得注意。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;口笛鼻笛的未來發展，從育才的角度上來看，我們認為學術單位應當提供適當的管道提昇藝生的相關音樂知識，這些管道可以是進入音樂學系攻讀大學學位，也可以是現有的傳習方案當中，增加音樂知識學習的時數。從樂教的角度上看，我們認為中小學學校教科書當中，應增加口笛和鼻笛的內容，讓不同族群的學生都可以認識這項樂器，而國內一年一度的音樂比賽，也應納入雙管鼻笛的項目。從表演的角度來看，應積極設立委託音樂家作口笛鼻笛音樂創作的鼓勵辦法，讓這個樂器多一些與西洋管絃樂合奏的機會。最後，是整合的角度上，我們認為，應設立國家級原住民音樂館，藉以整合不同族群的笛類音樂資源，並擴大口笛鼻笛或各種笛類樂器在國際上的能見度。</p>
<h3><strong>結語</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年，全世界籠罩在武漢肺炎的威脅，武漢肺炎的傳播幾乎影響了全世界人民的生活，2020年後期，大家期望疫苗的開發，以及各國隔離政策的嚴格施行，可以有效的抑制疫情的發展，無奈，到了2021年初，更棘手的武漢肺炎病毒變種、各地疫情復甦的消息，以及國內部立桃園醫院的事件，都讓2021年的音樂生活充滿的許多的變數，我們無法掌握現況，只期待疫情能立即緩和，人們恢復以往的生活。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年有許多值得探討的焦點，第一個是武漢肺炎帶來的文化衝擊，展演活動碰到疫情可以有幾種層次的解決之道，第一種是直接取消，第二種情形是做延期的處理，第三種是節目照常舉辦，防疫工作卻加強處理，第四種處理方式是室外活動照常舉辦，但是需要有人數的限制以及人與人之間距離的限制。第二個探討的焦點是傳統智慧創作專用權的紛爭。2020年排灣族地區發生了傳統智慧創作專用權使用上的紛爭，我們也發現申請案件出現的爭議有：1.僅以書面上的文字審核，未考慮是否與其他家族相似；2.未附上音檔，不清楚旋律為何；3.歌詞是否與不同部族重疊；4.申請案是否有部落耆老參與等等，這些都需要部落族人以及行政單位的合作，才能針對條文內部的細節作正確的修訂。第三個探討的焦點是「臺灣原住民音樂館」設立的必要性。2020年排灣族巴格達外家族傳統智慧創作專用權的問題、原住民音樂資料散亂在各個單位的問題、設立應包含音樂與語文2個領域的問題、籌組「國家原住民歌舞團」的問題等等，都顯示設立「臺灣原住民音樂館」的重要性。第四個探討的焦點是國際交流貧乏的因應之道。2020年因為疫情等等因素，國際交流項目的紀錄顯得相當貧乏，幾個解決方式可行：1.善用直播音樂會；2.國際學術研討會或是跨國的音樂論壇改採視訊的方式進行；3.善用影視廣播媒體進行各種教學與傳達。第五個探討的焦點是口鼻笛進階傳習的豐碩成果。文化部的「重要傳統表演藝術保存者－排灣族雙管口、鼻笛進階傳習計畫」（第一年）這個方案，進階藝生第一年的成果展演成果豐碩，國家級藝師謝水能音樂團體的表現搶眼，成績受到肯定。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年是個風雨飄搖的一年，但是全年的活動紀錄並沒有因此而減少，可見大部分的活動雖然延期，後來仍舊辦理完畢。國際交流以及祭儀活動的活動量急劇減少，需要持續關注。官方的音樂出版案件萌芽，值得鼓勵。而展演活動的活動量占比雖然衝到最高，然而筆者發現到展演活動的內容流行化的趨勢明顯，流行與傳統在很多場合上呈現了拉力戰，是否因此反而破壞了傳統音樂原本的樣貌，2021年需要繼續觀察。</p>

