<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以上的紀錄，值得提及的是排灣族的出版品《kinakaian母親的舌頭》。這個專輯由Y31十一音樂發行，演唱人為阿爆（Aljenljeng Tjaluvie）。這張專輯在第三十一屆金曲獎當中獲得極高的評價，共入圍了8個獎項，最後一舉拿下了最佳原住民語專輯、年度歌曲獎及年度專輯獎共3個獎項，成為本屆金曲獎最大贏家。此張專輯，有許多歌曲是阿爆和母親的共同創作，歌曲更是結合了排灣古調以及近代音樂，非常特殊。</p>
