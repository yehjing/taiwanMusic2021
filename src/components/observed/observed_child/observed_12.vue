<h3><strong>前言</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年國人西樂與混合編制創作可分管弦樂作品，室內樂作品，合唱與聲樂作品，音樂劇、音樂劇場與跨界展演，以及學術研討會與出版五個項目討論。管弦樂作品以臺北市立交響樂團和國立臺灣交響樂團為主要製作與演出單位。室內樂作品以中華民國現代音樂協會、亞洲作曲家聯盟台灣總會製作之音樂會和國立傳統藝術中心臺灣音樂館「臺灣音樂憶像」系列較具質量，此外高雄對位室內樂團和薪傳打擊樂團也製作了水準優異的現代作品發表會，展現南臺灣的音樂實力。國人合唱作品方面，2020年仍以台北愛樂文教基金會較為穩健，其他如台大、拉縴人、福爾摩沙、木樓和台北室內合唱團亦各有建樹。聲樂作品以詠樂集製作的陳義芝詩選歌樂創作和游昌發的瘂弦詩歌藝術歌曲發表會較具特色。綜合型態的音樂劇，2020年以國立臺灣交響樂團製作的《妖怪臺灣》較可觀。與國人創作相關的學術研討會，由文化部委託北藝大音樂系承辦的「重建臺灣音樂史－臺灣音樂史研究回顧與發展願景」研討會仍有零星論文發表。因疫情故，2020年中，音樂會多停辦或延期，唯有臺灣音樂館製作的音樂會仍能延後或如期舉辦，並維持優異的製作品質，本文最後乃以公部門功能突顯和老中青作曲家創作不輟來總結2020年的國人音樂創作。</p>
<h3><strong>壹、管弦樂作品</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年國人西樂創作的管弦樂作品仍以幾個公營交響樂團為主，其中以臺北市立交響樂團和國立臺灣交響樂團的演出質量較可觀，國家交響樂團和長榮交響樂團也零星演出了若干國人作品。</p>
<h4><strong>一、臺北市立交響樂團</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年8月22日臺北市立交響樂團於城市舞台的開季音樂會「音樂劇場《燈．影》」，演出作曲家潘皇龍為市交50週年團慶的新作：五樂章管弦交響協奏曲《大燈對》，並搭配「丞舞製作團隊」的舞蹈。依開演前作曲家的說明：除了從北管曲牌【大瓶爵】和【大燈對】借來之旋律動機外，他還從「臺北市立交響樂團」的英文名稱挑出與字母對應的音符作為骨幹音來進行展衍和變化，讓北管旋律和象徵北市交的音符輪替於不同的樂器音色間，再從「線」發展到「面」，「面」發展到「間」的層疊音響。「丞舞」的舞蹈讓人感受到舞者的動作和表情呼應著音樂的脈動與結構，在《燈．影》的劇場框架中伸張了舞團的技術和意志。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;9月20日北市交於中山堂中正廳的「溫情臺北－溫隆信樂展」，曲目多為溫隆信發表於2019年的《臺北交響曲》和《臺灣客家369樂章》部分樂章。此次演出更加入爵士舞蹈。新作《低音管協奏曲》為紀念已故低音管演奏家張龍雲而作，曲風溫暖，作品並置入作曲家當年與摯友共同研究的低音管多聲技巧，以懷思故友。10月8日北市交於國家音樂廳的「賴德和樂展」演出兩部交響曲：《海神家族》和《吾鄉印象》。《海神家族》交響曲以作家陳玉慧小說《海神家族》發想，五樂章各自與小說情節呼應。因小說情節涵蓋了日治時期至戰後，為作曲家提供了臺灣、日本、中國之異質性文化聲響的機會。《吾鄉印象》則以作曲家自身的本土音樂回憶構思，四樂章反映了南管、北管、原住民以及非調性現代感同步存在於臺灣的音景。整體而言，《海神家族》熱鬧易懂，《吾鄉印象》於樂章轉換間也訴說了作曲家對於各族群、時代的親疏情感。</p>
<h4><strong>二、國立臺灣交響樂團</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年8月14日，國立臺灣交響樂團於衛武營國家藝術文化中心音樂廳演出的「2020 NTSO國際青少年管弦樂營成果音樂會」邀請旅美音樂家陳美安擔任指揮。音樂會安排了莫札特、杜卡斯、拉威爾和史特拉汶斯基等西方經典作品，高難度的史特拉汶斯基《火鳥》組曲，樂團演出十分出色，展現了臺灣年輕音樂家的潛力。兩首相對簡易的國人作品：蕭泰然的《來自福爾摩沙的天使》以G大調主和弦的三個音為基礎，先由長笛與木管吹奏出主題旋律，再透過弦樂與銅管的銜接推進音樂，最後於弦樂溫暖的樂音中邁入結束樂段，曲風親切純樸；張菁珊改編自洪一峰歌曲的《寶島曼波》為國臺交委託編曲的民謠與流行歌曲系列之一，樂曲以奏鳴曲式為結構，在適切的管弦樂配器與音樂織體的銜接互動下，呈現出《寶島曼波》輕鬆愉快的特質，曲末藉管弦樂的壯闊音響，於節慶般的歡樂氛圍中結束，為流行音樂古典化的佳例。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;11月22日國臺交於霧峰演出的「山水意境中的白鳥之歌」，由葉政德指揮，首演了楊聰賢的新作《山水舞鶴》。全曲七樂章寫作靈感均源自舞鶴作品，依作曲者，「<span class="bkt">作品各樂章跳脫作家任何單一小說的敘事或情節，以其迄今所有創作中表露的敘述策略之沿革作為音樂敘述上的探索標的，意即探尋舞鶴在各個階段採取的敘述策略於音樂創作上的可能性－從早期揉合庶民鄉土和現代主義，直到晚近純然內觀的創作，期間歷經各式不同面向的實驗&hellip;&hellip;</span>」（轉引自陳安駿，〈楊聰賢，《山水舞鶴》〉，音樂會樂曲解說）。由於作曲者於節目冊中並未給予各樂章以適切的標題，若無作曲者自行詮解，聽眾實無從得知哪一樂章的寫作靈感是來自舞鶴的哪一部作品，但從作品可聆之多處略帶狂放性格的滑奏音響，或可聯想到舞鶴作品某些超現實的狂亂情境或小說人物幽微特異的心理狀態，而作品後幾樂章較寬廣、質樸的旋律片段，也讓人聯想到舞鶴在《餘生》、《思索阿邦．卡露斯》等作品中的原住民觀照。作品寫作企圖龐大，概括呈現了作曲家嘗試透過音樂寫作，依舞鶴和作曲家自身視角，於音樂與文學的相互映照中探尋其對於臺灣當代歷史與人文變遷的理解和體驗。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;12月11日國臺交於衛武營國家藝術文化中心舉辦的「客家音畫」音樂會，曲目難易搭配適中，既有陳樹熙《走馬看花逛老街》、《埤塘》和劉松輝《客家音畫》雙嗩吶協奏曲等悅耳且逗趣的樂曲，也有相對較嚴肅的金希文《客家狂想曲》小提琴協奏曲；穿插其間的黃思瑜編曲《情迷山歌子》和劉學軒《六堆戰紀~1721》，一者結合爵士風，一者刻劃英雄事蹟，風格介於平易與嚴肅之間。可貴的是所有曲目均能呈現出客家傳統之民歌曲調、樂器音色與文化特質而不落陳腔俗套。製作單位的用心值得讚賞！</p>
<h4><strong>三、國家交響樂團與長榮交響樂團</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因疫情故，2020年國家交響樂團取消了多場音樂會，國人作品發表遂成聊備一格。2月22日的名家系列「畫景敘事」演出旅美作曲家王怡雯的《花園集景》，作品以五樂章呈現作曲者於不同國家與城市旅遊參訪，從眾多花景與花藝中獲得之靈感，頗似於穆索斯基《展覽會之畫》的音畫風格。五樂章分別為：〈繁花躍欄〉，描寫春天盛開的杜鵑花及其繽紛世界；〈錫頂鳥屋〉，描寫植物園孩童區內有著錫製屋頂的鳥屋和婉轉動聽的鳥鳴聲；〈光影閃爍之池塘〉，描寫植物園內大池塘的蓮花和白雲倒影交織成的抽象形影；〈花之盆景〉，描寫花園內巨大花盆內的花藝；〈景觀壁毯〉，描寫日落時遠眺花園中的草坪及坡地上的繽紛花床，景觀瀰漫著神祕氣息，如一幅巨型掛毯。音樂流暢，音響細緻豐富，呈現出作曲者別出心裁的藝術觀照。11月21日的名家系列「琴戀．彼得洛希卡」演出客委會委託旅美作曲家陳可嘉創作的《客家故事》，作品嘗試以管弦樂描述客家子弟於世界各地的遷移和融入當地社會的奮鬥過程，音樂頗具生動的節奏和力量感。從聲響營造和音樂推展角度不失為佳作，然而整部作品無法讓人從聲響上聽到任何「客家元素」，則音樂似乎也可適用於任何移民或個體的奮鬥故事，這是略感缺憾之處。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年9月7日由中華民國聖樂促進會製作，長榮交響樂團演出的「2020台灣之聲－管弦樂篇」發表了陳茂萱、連信道、柯芳隆、莊文達、林岑陵的八部管弦樂作品。其中，連信道的《夢之谷》藉音樂詮釋戀人因戰亂而離散、相思的故事，曲風浪漫。琵琶協奏曲《霸王別姬》改編自劉德海的著名武曲《霸王卸甲》，樂曲前段描寫楚漢相爭至垓下酣戰的過程，後段描寫垓下敗陣後項羽的悲壯心情：〈楚歌〉中琵琶以長輪手法奏出的悲切曲調和〈別姬〉以急促旋律和滑奏表現出西楚霸王在四面楚歌中的悲憤與無奈。柯芳隆《哭泣的美人魚》則嘗試以當代音響美學呈現作曲者的生態關懷，作品以簡約的旋律象徵大海中純真的美人魚因生存環境遭受蹂躪而哭泣。陳茂萱的第一號鋼琴協奏曲《一言九鼎》則以幼年聽聞北港義勇軍抵抗日軍入侵的故事出發，藉此作品保留兒時記憶並抒發懷舊情懷。全曲以迴旋曲結構，藉鼓聲、鋼琴與管弦樂的互動呈現出激昂熱烈的音樂氛圍。樂曲主題充滿節奏活力，副題則融入流行語彙，作品整體兼具振奮人心的鼓聲鼕鼕和氣息寬廣的旋律，令人印象深刻！</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;兩位中生代作曲家：莊文達的《八里隨想曲》有感於水筆仔生命力之強韌，描寫其屹立不搖的堅毅精神，作品洋溢著青春氣息。《黑水溝》則呈現先民移居臺灣的艱苦歷程，並以黑水溝的洶湧滔急象徵臺灣的歷史轉折。法國號的激昂音型呈現了臺灣民族在艱困環境下不撓的毅力。中段曲風富於旋律與抒情性，象徵漢族移民與本地平埔族通婚，攜手開創光明前景。樂曲最後以平穩的步伐邁進，於高潮中結束。林岑陵的新作《黑潮蘭嶼》靈感來自許常惠教授1978年於蘭嶼的采風錄音〈達悟捕飛魚豐收回港歌〉。作品的結構與音響思維來自神秘黑潮、飛魚和達悟族三個意象間的對話與相互流轉，透過非調性和音響技法、複調性及古老調式作曲法，輔以多變的節奏、快速轉換的織度及複雜的音樂層次變化，營造出多元色澤音響、音色對比及音響張力的累積建構，傳達出作曲者對自然與生命永續共生的理念。《轉折》則以非調性語法融合五聲音階，傳達東方文人的藝術心靈與天地人三合一的生命境界，並緬懷作曲家盧炎對作曲者的影響。</p>
<h4><strong>四、音契、龍潭愛樂、新北市青少年管弦樂團與巴雀弦樂團</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年1月3日財團法人音契文化藝術基金會於臺中國家歌劇院舉辦的「愛與傷痕－公益音樂會：為泡泡龍病友而演」音樂會，作曲家金希文除指揮多首優美的古典、浪漫名曲，也演奏了金希文本人創作之《寫給小提琴與管絃樂團的綺想曲》，由金昫祈擔任小提琴獨奏。作品旋律來自基督教聖詩〈耶穌愛我〉，採取類似變奏曲的手法和架構，穿插作曲家個人靈感片段，在不同速度、調性上展開溫暖悅耳的綺想曲，表達作曲家感恩耶穌的宗教情懷。7月16日龍潭愛樂管弦樂團於國家音樂廳的「客座英倫」音樂會演出三首國人創作：王柏力的《龍潭之歌》拼貼了臺灣車鼓調、客家平板、《望春風》、《雨夜花》以及《中華民國國歌》等旋律，是一首平易通俗的什錦式樂曲。李哲藝的《龍潭映象交響詩》採用「龍潭映象」徵稿的兩位得獎者徐碧美（客語）和顏肇基（牧也，國語）歌頌龍潭的詩詞譜曲，樂曲前半頗為冗長，引用了鄧雨賢採用長老教會聖詩〈拿阿美〉曲調改編的《月夜愁》旋律，也是一部熱鬧通俗、拼貼風格的作品。下半場演奏的鄧雨賢《望春風》採用日本作曲家若松正司的編曲，配器均衡，樂風溫暖可親。8月9日新北市青少年管弦樂團假新北市藝文中心的「樂音飄揚」音樂會演出郭芝苑的管弦樂組曲《回憶》。《回憶》為郭芝苑1961-1973年作品，原標題為《民俗組曲》。作品描寫作曲家年少時的鄉土與音樂回憶，分四樂章：〈盛宴〉、〈夜曲〉、〈車鼓舞〉、〈迎神〉，標題頗有呼應日本國民樂派作曲家伊福部昭的《日本狂詩曲》（如〈夜想曲〉、〈祭典〉等樂章）和江文也《台灣舞曲》之曲趣。〈盛宴〉描寫古希臘酒神祭的熱鬧場景，以東方吉普賽音階融合五聲音階手法寫作；〈夜曲〉呈現作曲者幼時夜間聽聞民間樂人的笛子或胡琴聲，以及音樂勾起的童年回想；〈車鼓舞〉描繪廟會祭典所見輕鬆詼諧的「車鼓弄」；〈迎神〉則呈現媽祖生的廟會陣頭和鑼鼓喧天，於熱鬧歡快的氣氛中結束全曲。在郭聯昌指揮下，學生樂團的表現中規中矩，呈現了作曲家的鄉土情懷與質樸樂風，音樂內涵且富於教育傳承意義。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由青年指揮家楊書涵帶領的巴雀弦樂團，2020年8月26日於松菸誠品表演廳的「東方音緣」音樂會演出兩位波蘭作曲家帕德瑞夫斯基、蕭邦以及匈牙利作曲家巴爾托克作品，音樂會並委託青年作曲家林佳瑩創作新曲《變形》（Anamorphosen），作品從呼應巴雀樂團演奏過的李察．史特勞斯"Metamorphosen"出發，借用視覺藝術扭曲的透視觀點與錯覺，使觀者須藉特定視角來捕捉圖像之真實意義的概念，將特定素材以重疊記憶方式呈現，藉由緩慢移動而延續的音色變換來表現暈塗繪畫手法，營造出朦朧漸變的視覺延宕效果。作品將李察．史特勞斯"Metamorphosen"的音樂片段轉化為富於能量和象徵自由的音響風貌，讓音樂在想像中釋放，延伸到視線的遠端。作品之陌生化的音響推移，確屬典型的音響風格作品，而對照作曲家的論述或音樂實踐，卻難謂有何「東方」元素或意涵。12月26日於國立臺灣藝術大學的「藝韻薪傳」音樂會，在簡文彬指揮兩部節慶作品：雷哈爾《風流寡婦》選粹和貝多芬《第九號交響曲》第四樂章之前，演出了作曲家江易錚的新作"Transmental"，作品標題有精神或心理狀態的過渡、轉移、超越之意。作曲者自云此作「<span class="bkt">為對於現實生活中所面臨的種種跨心理突破，以不同於傳統創作的思考模式表達。跳躍性脈絡的構思，串連心靈轉變的處境，為生活在他方作為跨越理想精神狀態之烏托邦。</span>」作品為典型的非旋律音響風格，弦樂與管樂多處滑奏聲響或可腳註"transmental"的移轉跨越意涵，整體音響充實而有活力，不失為佳作。</p>
<h3><strong>貳、室內樂作品</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年國人創作室內樂演出以中華民國現代音樂協會的「國際現代音樂節」，亞洲作曲家聯盟台灣總會以及國立傳統藝術中心臺灣音樂館製作的一系列「2020臺灣音樂憶像」音樂會較具份量。高雄薪傳打擊樂團和對位室內樂團也分別製作了主題明晰的作品發表會，展現南臺灣的現代音樂演奏實力。</p>
<h4><strong>一、中華民國現代音樂協會</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2020年的國際現代音樂節之前，現代音樂協會於9月24日與朱宗慶打擊樂團假北藝大音樂廳聯合製作「現打．擊音」音樂會，演出七部國人作品：桑磊栢的《塑造金屬》靈感來自雕塑家鄭宏南形塑海中生物的鋼雕，曲中以馬林巴敲擊出的傳統和弦代表藝術家使用的鋼鐵零碎物件和具體雕塑，無確定音高的樂器音響代表抽象的海浪、氣泡和雨滴等聲響。張婉俞的《凝．動》以金屬樂器殘響，透過打擊樂者的動態演奏連結各打擊樂器。擊樂者於演奏中發出各種有音高與非音高的人聲，與鑼鼓互動，呈現出靜動交替的音響意趣，隱喻生命的變動與休止。林芝良的《邂逅》藉由棒槌、高爾夫球、玩具車在木琴與顫音琴鍵盤上的滑動，木質與金屬材質樂器音色的聲響變換，營造出具節奏感的柔滑音效。同樣表現靜動交替與共存，邱妍甯的《在激情與平靜中搖擺》透過五位打擊樂者以共同使用的小鼓、木琴、古鈸等樂器和個別的雨柱、顫音琴、管鐘、鐘琴、風鈴，交互環繞、交織、流動，聲響由繁複趨於單純。陳立立的《小宇宙》靈感來自陳黎《小宇宙：現代俳句100首》中的三篇短詩。同樣探討動與靜，配器僅用定音鼓與古箏，三個段落分別表現陳黎三闋短詩的快速滑奏、擲骰翻轉和流動性等詩意與聲響。謝宗仁的《因為思念溫特克拉嫩貝伊門貳壹陸房之巢》為作曲者留德期間的生活感懷，作品表現作曲家因住處窗外鳥巢的存在與消失引致生活和心情的變化，音樂以金屬擊樂，小打擊樂器與人聲，鼓類樂器呈現三種情境。曲末以人聲唱出的《一隻鳥兒》片段訴說了作曲者因鳥巢消失而觸動的惻隱之心與思鄉情緒。林桂如的《島語》則以強烈而沉重的聲響，呈現2009年莫拉克颱風重創臺灣前後的情境和作曲家對大自然生態環境的關懷。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年10月底至11月初假國家演奏廳舉行的四場「臺北國際現代音樂節」系列展現了現代音協於疫情下仍努力於國際音樂交流的信念。10月30日擊樂場，邱浩源和陳珩的作品分別表現打麻將的趣味情境和小件樂器的遊戲聲響，頗具新意。那娜擊樂二重奏結合韓國杖鼓擊樂家崔昭理演出韓國作曲家鄭日蓮與現代音協理事長潘家琳的作品，音響趣味與演奏品質均佳。從作品演出間作曲家親自解說的創作理念，可看出國內年輕作曲家流暢的表達論述能力。10月31日下午的室內樂場則呈現了張譽馨寫給鋼琴的《致散佚之光、星體與時間》、蔡承哲寫給中提琴獨奏的《在夢見牛飲一碗香灰水之後》、陳以德的《街景速寫》、羅芳偉的《孤夜》和邱妍甯的《我以我獨特的方式美麗著》兩部絃樂四重奏，以及李建諭的《無聲的告白》等年輕作曲家的作品，展現了臺灣作曲新世代的朝氣。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;11月1日晚上的琵琶作品專場由琵琶演奏家黃立騰與現代音協共同策劃，邀請青年琵琶演奏家王楷涵同臺演出。曲目有馬水龍的《水龍吟》、王正平《天問》和周久渝的《籽聲》，並委託林芝良、鐘儀芳和張婉俞譜寫新曲。林芝良《悟》以琵琶長線條輪指技法為基礎，藉由輪指配速之快慢交替顆粒性長音的聲線堆疊，配合滑音變化，建構出令人耳目一新的音樂畫面；鐘儀芳《聽雨》以旋律取勝，描繪「雨」的多樣姿態與聲響；張婉俞的《烈》則從抽象概念返璞歸真，改變左右手的主從關係，並通過彈奏位置的變化來豐富音色。幾代作曲家的作品映射出當代琵琶語彙的脈絡與新流。11月1日下午的大鍵琴場亦展演七部作品，令人印象深刻者有陳士惠的《敕桃》和蔡淩蕙的《琴想X－賜福》，二者皆融合西洋古典於臺灣傳統。前者以象徵南管「遊於樂」理念的「敕桃」（遊玩之意）為題，演奏者於舞臺上的移位和互動也頗得「敕桃」旨趣；後者嘗試以有限的樂曲長度表現北管扮仙戲的結構，受限於巴洛克室內樂組合，似較難呈現北管的喧囂與熱鬧。大鍵琴演奏家蔡佳璇努力拓展巴洛克古樂的嘗試甚值肯定。</p>
<h4><strong>二、</strong><strong>亞洲作曲家聯盟台灣總會</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年傳藝中心臺灣音樂館委託亞洲作曲家聯盟台灣總會製作了三場「臺灣音樂憶像」委創首演系列，分別是9月26日由台北室內合唱團演出的合唱專場「聲迴臺灣」、11月28日由北藝大音樂系師生與校友演出的西樂室內樂專場「樂盈原鄉」和11月29日由采風樂坊演出的傳統樂器與西樂器混合室內樂專場「琴想里山」。合唱專場我們稍後再述，以下先敘述兩場室內樂音樂會。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;11月28日於戲曲中心小表演廳的西樂室內樂專場「樂盈原鄉」：呂文慈的《油桐花像白紗》源起於客家文學作家葉日松贈與之客語童謠詩歌創作《因為有愛 𢞴介心正保有溫度》第一輯。內容描述代表正月至臘月的花草瓜樹所蘊含的客家文化。作曲家運用西方樂器吟唱東方語𢑥，將臺灣四縣客語的童詩音韻入樂。蔡宜真的《川．音》透過弦樂四重奏描繪臺灣多樣的水姿與聲響：綿綿細雨、潺潺溪流、急湍瀑布等，連結至川流，串起山林與人文，乃至於文明躁動，最終匯流於大海。楊惠婷的《花絮》、《心燈》選用臺灣女性詩人陳秀喜詩作〈花絮〉與〈心燈〉為詞，詩作以直白風格描寫自然與人的互動，既寫景又感懷人生。音樂則以擬聲手法模仿詩中描述的大自然現象，如風聲、鐘聲與靜寂的月夜，依循詩作情緒的轉換起伏，鋪陳音樂的張力與架構。桑磊柏的《歌之戲》靈感來自歌仔戲，樂器的角色互動渲染了音樂的戲劇效果，呈現出歌仔戲舞臺的熱鬧情境。陳宜貞的《銀蟬忽見上林梢》，歌詞選用作曲者祖父陳慶輝的漢詩〈旱溪晚步 其二〉，藉音樂探索詩作潛藏的意涵。馬定一的《原鄉．原想》以屏東排灣族的雙管鼻笛為模擬對象，將兩聲部六音調式的鼻笛音樂與西方當代音樂語言加以匯流，以表達作曲者對傳統排灣音樂的崇敬。賴德和的室內樂版《咬舌詩》以俗諺造成詼諧語境，嘲諷現實生活與動盪世局的無奈。曲中引用的莫札特奏鳴曲、德布西《兒童世界》和柴可夫斯基鋼琴協奏曲片段展現了後現代的拼貼趣味。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;11月29日的混合室內樂專場因多部作品表現對臺灣山林的人文關懷，乃以「里山」為題。其中謝宗仁的《山稜》藉陌生化聲響呈現臺灣群山的層層相疊，各具姿態；林岑陵的《猴子山Kawsan》藉阿美族與卑南族的史實與傳說，巧妙運用傳統古調與歌謠，以傳統樂器融合人聲，描繪出原民世界的活力乃至於族群間的衝突；周久渝的《狂野塗鴉 参》則延續作曲者留英時期探討文化衝突的系列作品，呈現臺灣山海、南島語系與漢文化等多樣生態、文化的碰撞交流，作品運用了媽祖信仰、原住民豐年祭等素材與意象，將聲音揉合為內心塗鴉；莊文達的《白鷺鷥幻想曲》運用了《駛犁歌》、《白鷺鷥》、《留傘調》等旋律片段，描繪臺灣早期純樸的農村與田園意象。下半場蔡淩蕙的《琴想IX》從南管樂器之特性、語法與配器組合出發，借南管「譜」、「和」概念，開發當代技法與嶄新的音色組合；蔡承哲的《盂蘭盆Ullambana》嘗試以音樂描繪盂蘭盆祭儀，藉北管的支聲複音與即興手法開展樂曲。音樂會最後邀請潘皇龍教授發表其於國內開啟此類混合編制創作風氣的代表作品：《東南西北VIII》，作品以遵循個別樂器間的特殊性能與歷史傳統為縱軸，另以「新音列、新調性與新結構」為橫軸，反映吾人身處世紀之交的心聲，曲末的鳥笛聲更呼應了音樂會的「里山」意趣。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;曲盟台灣總會於12月6日假臺中教育大學舉辦的「音樂台灣2020作曲聯展」，主題聚焦於臺中風土人文。除演出已故張炫文教授的《草枝仔》、《土地之戀》等合唱作品，其他如蕭慶瑜的《暗光鳥》和《山的眼睛》分別以臺中詩人林沉默和蔡榮勇詩作譜曲，謝宗仁的《逝水》和《有贈》則以臺中師專校友陳義芝詩作譜曲；曲盟另委託林進祐教授以臺中市花「山櫻花」和市鳥「白耳畫眉」為題譜寫新作《台中頌》，表達作曲家對土地和家園的深摯情感。此外，侯志正的《第八號長笛獨奏幻想曲》以調性語法展現了當代長笛的多樣技巧，王之筠的《來自遠方的吶喊》則充分運用弦樂的艱難技巧與實驗性音響描繪當代人心靈深處的吶喊，陳慶章的《極微主義五重奏》呈現出典型極微主義的簡約風格，羅芳偉的《哀歌》則以弦樂四重奏之織度、節奏與素材變化吐露作曲者年少時的孤獨心境。音樂會由臺中集泉公司贊助林進祐教授的新作《台中頌》也展現了在地企業回饋地方文化的典範。</p>
<h4><strong>三、臺灣音樂館「</strong><strong>2020</strong><strong>臺灣音樂憶像」作曲家專場</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年，文化部傳藝中心所屬臺灣音樂館在艱困的疫情條件下仍持續製作了數場「臺灣音樂憶像」的作曲家專場。首先是3月8日於臺灣戲曲中心，由鋼琴家葉綠娜製作的「來自故鄉的思念」－蕭泰然逝世五周年紀念音樂會。這場疫情初起時少數留存的音樂會展演了蕭泰然多部代表性室內樂作品，作品因大量採用聽眾熟悉的民歌或流行曲調而平易可親，深具感染力，整體風格近於浪漫時期的國民樂派。演奏家如葉綠娜、魏樂富、蘇顯達與作曲者生前均有多年情誼，音樂會且搭配作曲者生前友人莊傳賢的寫真，更添懷思意味。因疫情延後到10月6日舉辦的「來自福爾摩沙的天使」音樂會則由國立臺灣師範大學音樂系演出。曲目呈現出蕭泰然創作的核心概念與多樣性，樂曲類型涵蓋擊樂三重奏，合唱曲如《祢居住的所在》、《我欲攑目向山》、《出外人》，《C調大提琴協奏曲作品五十二》，管弦樂作品《來自福爾摩沙的天使》，以及為獨唱、合唱與交響樂的《啊！福爾摩沙－為殉難者的鎮魂曲》。音樂會聚焦於蕭泰然創作的中心思想「信仰、家鄉與土地」，呈現了作曲家對家鄉的熱愛，安可曲《嘸通嫌台灣》更為音樂會畫下完美句點。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;11月7日於衛武營國家藝術文化中心，由鋼琴家劉富美女士與高雄市音樂教育學會舉辦的「陳主稅樂展」演出多部陳主稅作品。由六首臺灣歌謠：《思想起》、《白牡丹》、《心酸酸》、《望春風》、《月夜愁》與《桃花鄉》組成的《臺灣歌謠鋼琴組曲I》以對位手法鋪陳，鋼琴左右手如對唱或合唱般道出琅琅上口的民謠旋律。鋼琴曲《山之霧》和《楓》使用鋼琴的觸鍵與踏板變化，以多變的音色細膩描繪景色的層次、光影與季節的色彩變化。《冬之聲》的小提琴吟唱出合歡山的冬天景致，鋼琴以音域變化帶出作曲者對雪景的感觸。聲樂曲《小園春暖》描繪春天的綠意盎然，更唱出鴛鴦的恩愛幸福，《飄葉吟》則描繪秋天伴隨著日落、煙霞與鐘聲的孤寂與淒涼。鋼琴曲《敘》和《吹笛女郎》在技法與和聲風格上較為前衛，句法鋪陳與節奏運用也較為複雜，音樂以聲響對比為主，旋律線條較為塊狀。《小小展技》和《練習曲》充分展現了多變的鋼琴技巧。獻給日本畫家佃幸野女士的《菅芒花的聯想》以聆聽友人演唱《菅芒花》的情緒發想，在作品中使用雙音、撥弦等技巧，誇張的力度表現與和聲語言頗受表現主義影響。《我深愛》則以民歌寫作風格呈現，優美的旋律搭配細膩的歌詞音調，情感直率而真誠。題獻劉富美女士的《秋》和敬獻給許常惠的《雲海》均由劉富美演奏，創作始於對周圍景色與氛圍的感動，昇華為藉音樂的流動抒發親友間的綿綿情感與鄉愁。弦樂四重奏《歸途》動機使用Lydian調式，不同段落展現了古典、浪漫、表現主義與巴爾托克的室內樂寫作風格，體現出作曲家在學習西洋音樂手法與本土元素的心路曲折。音樂會集結了高雄多位音樂家與音樂教育家，展現了南臺灣優異的演出水準，更彰顯出陳主稅與劉富美伉儷多年深耕南部地區音樂文化與音樂教育的豐碩成果，令人感動！</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;12月13日由邱再興文教基金會製作，假北藝大音樂廳舉行的「馬水龍逝世5週年紀念音樂會」演出馬水龍早期作品《小提琴奏鳴曲》以及為簫與四把大提琴的《意與象》、為九把大提琴的《水墨畫之冥想》、《琶琵與弦樂四重奏》和為古箏獨奏的《尋》等風格兼具音響、音色營造與旋律的作品。從作品的安排可看出作曲家生前對大提琴的喜好以及作曲家重視傳統音樂，在作品中努力融合中國傳統與西方當代的藝術理念。音樂會選曲略偏向作曲家同質性甚高的中晚期作品，較難呈現一代作曲家的完整創作風貌。</p>
<h4><strong>四、對位室內樂團與薪傳打擊樂團</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;延續2018年的「聽．曲」和2019年的「聽．蟬」，對位室內樂團於2020年6月14日假高雄市文化中心至善堂以「聽．律」為題的音樂會，除演出匈牙利作曲家李給替早年作品《三首歌曲》和《第一號弦樂四重奏》外，另外也展演了多部國人作品。其中如林盈婷的《靜夜思》嘗試運用南管演奏（唱）者以加花方式修飾主線條的支聲複音，呈現典雅嫻靜的曲風；馬定一《消逝的聲音》以〈秘境〉、〈戰舞〉、〈悲歌〉等五首啟發自排灣古調的小品，書寫作曲者對神秘的原住民傳統音樂由好奇到欣賞的過程，及其對此傳統歌謠日漸消逝、沒落的感懷。楊祖垚和邱浩源兩位年輕作曲家的作品則嘗試於物理性音響的探索中表現各自對死亡與夢境的想像。楊聰賢的《日本山水》曲風簡潔，潘皇龍的《東南西北V》則在一貫的層面音響營造中隱含了作曲家曾領導的音樂團體、學院以及熟識的友人和作曲家自身的人物暗碼。音樂會的座談設計更展現了製作人梁孔玲和樂團嘗試與聽眾溝通的努力，頗具藝文推廣之效。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6月25日於衛武營國家藝術文化中心舉辦的對位室內樂團首席林佳霖小提琴獨奏會「D調昇華」，其中許常惠的《五首前奏曲》體現了東西文化的兼容並蓄，第一、二首帶有廟寺祭典的風格，第三首使用歌仔戲素材，具輕快的特色與活潑的節奏，第四首使用恆春民謠《思想起》與《哭仔調》的滑音和自由吟唱風格，第五首急板則象徵光明，總結前四首的滄桑與鄉愁。日本作曲家松下功的《曼怛羅》運用戲劇、宗教意念與極簡元素，整曲以核心音D開展，由靜入動，演奏者在各個譜架不同位置間游移，象徵念頭的反覆與無盡的煩惱，整部作品展現出豐富的聽覺與視覺效果。11月29日於衛武營國家藝術文化中心舉辦的「對位與大師有約」系列，除邀請小提琴家胡乃元演奏布魯赫《第一號小提琴協奏曲》和布拉姆斯《第一號弦樂六重奏》的弦樂版本，另外也演出了潘皇龍寫給古箏與弦樂團的《東南西北．禮讚》，將傳統樂器的彈撥聲響與弦樂交融，營造出獨特的音響色彩。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;薪傳打擊樂團於2020年10月16日假高雄市文化中心至善堂演出的音樂劇場《食相》（自魯迅《狂人日記》的「食人之相」），音響設計由作曲家曾毓忠擔任，演出結合了文學、戲劇、舞蹈與音樂，為樂團極富創意的跨界嘗試。11月27-29日假衛武營中心舉辦的「2020高雄現代擊樂節」，除了回顧擊樂發展史上的經典作品，更呈現了電腦科技結合擊樂的豐富色彩，營造出擊樂的潛在可能。音樂節首日之「A.I.與擊樂的對話」演出的國人作品有曾毓忠的《跨越》、趙菁文的《純化》和謝賢德的《倉．狂》，均為結合擊樂與電子音樂的作品。11月29日則以歐洲經典作品為主，如樂團負責人鄭翔夫親自演出的森納奇斯擊樂獨奏曲《彈跳》（Rebonds），國人作品則有作曲家林梅芳2004年首演於巴黎IRCAM，由擊樂與電子音樂同步演出的"Multiplication Virtuelle"（潛在的繁殖）。</p>
<h4><strong>五、個別音樂家與獨立製作音樂會</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;個別音樂家與獨立製作的室內樂音樂會，長笛演奏家吳正宇召集的「時間藝術工作室」2020年1月5日於十方樂集舉辦的「Parallaxis Ⅱ新作暨台灣作曲家委創作品發表音樂會」，除演奏兩位歐洲年輕作曲家新作，也委託香港作曲家許德彰和作曲家江易錚創作新曲。許德彰的《水圖》由單純的數理現象出發，進展到文化層面和作曲者自身對聲音之消逝的多重思考，以非線性方式匯集呈現，為作曲者以馬遠十二幅《水圖》為題的系列作品之一。依作曲者，這些作品「<span class="bkt">並不是在説水，亦沒有指向特定的圖象。更多的是將水圖當作文化符號， 從最細小的一些個人感觸出發，尋找一絲若離若即的聯繫。</span>」江易錚的《構逅》以管樂、弦樂及打擊樂音色相互融合並行，器樂演奏與電聲如同作曲者自身及其文化背景，並將現實聲響與預錄錄音融匯。作品標題意在將不同樂思、音色、概念和相異的構思解構後以多元並容方式呈現。作品以音色虛實為主軸，藉由音色的各種細微嘗試，堆疊素材，並以多層次織度交互穿插銜接，讓媒材「邂逅」後建構成作品。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台北中央C室內樂團9月27日於國家演奏廳的「大風起C韻飛揚」音樂會演出了潘皇龍、孫瀛洲、董昭民以及維也納作曲家Rienhard Karger和美國作曲家Jacob Sudol作品。其中，孫瀛洲的曲笛協奏曲《風中笛聲》取材自古曲《梅花三弄》，略加擴大發展；董昭民的《風雨中的太陽》有感於香港時事，將事件相關的日期化為節奏動機，並以《馬賽進行曲》、《願榮光歸香港》等素材，在樂器間的拉扯與撞擊中，透過音樂訴說個人理念。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年11月9日，國立臺灣師範大學於國家演奏廳舉辦的「2020師大音樂系教授作品發表會」，演出了錢善華、莊文達、蕭慶瑜、陳瓊瑜、趙菁文和吳佩蓉新作，上半場趙菁文的《於焉》以頑固D音的持續與塊狀和聲堆疊描繪希臘神話中的海平面與飛舞的雙翼，當太陽融化蠟製雙翼而碎裂、墜落，人聲也模仿雨滴落下，掉落在那頑固D音底層的G泛音和聲汪洋中。錢善華的三首作品，"Kyrie"取材自葛利果聖歌，發展成八部男聲合唱；《聖母頌》亦取材葛利果聖歌《聖母經》，藉不同版本、語言與調性的呈現表達音樂最終回歸大三和絃的美好與融合；《落水歌》則取材自客家歌謠，在二者的穿插、互串中表現出熱絡的合唱氛圍。蕭慶瑜的《三首關於山的詩作》近似於聯篇歌曲，巧妙連結了詩意與音聲。下半場陳瓊瑜的大提琴獨奏曲《河流》以大量泛音為主體，在泛音與實音的虛實互動間著重於聲響的轉換，追求非常規的大提琴音響；莊文達的《水筆仔之歌》弦樂四重奏則歌頌水筆仔屹立不搖的精神及其生命毅力，曲風溫暖悅耳。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;同樣任教於師大音樂系的鋼琴家廖皎含在其因疫情延後至11月15日舉辦的「來自臺灣的聲音～臺灣經典鋼琴作品」音樂會，主題聚焦於「臺灣」，除了鋼琴家任教的師大師長之外，曲目也涵蓋了蕭泰然《家園的回憶》、黃輔棠《臺灣狂想曲》和馬水龍《臺灣組曲》。在表達明晰的解說之後，鋼琴家仍氣定神閒的彈奏了所有作品，講解的親和力與彈奏的集中均十分可佩。節目冊中，每部作品更附有該作品在鋼琴彈奏上所運用的手法與寫作特點，讓演奏會更兼具教育推廣意義。所選作品既標榜調性和好聽，整場音樂會自然流露著溫暖和悅的氛圍。在彈奏蕭慶瑜作品《映》之前現場播放了作曲者親自彈奏的早年舊作《土耳其的月亮》，使音樂會更具溫馨氣氛。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，任教於高屏地區多所大學的青年鋼琴家蘇綉雯於11月14日假高雄音樂館舉辦的「幻．變－2020蘇綉雯鋼琴獨奏會」演出了連憲升的早期作品《兩首鋼琴小品》，其中《練習曲》嘗試以無調性語言表現聲音在音響空間裡的各種組合與運動，曲風充滿現代音樂典型的動力、緊張與神經質，音高材料的組織與控制卻不失其嚴密與邏輯性；《夜歌》的樂思來自古琴音調的想像，自在舒放的吟唱風格和《練習曲》的激烈律動迥異。兩首小品一剛一柔，一者動、一者靜，適成有趣的對比。中山大學李美文教授領導的巴洛克獨奏家樂團，12月17日於臺北松菸誠品表演廳的「六人組－給小提琴、鋼琴和弦樂四重奏」則演奏了李思嫺新作《六重奏》，音樂呈現出作曲家於疫情期間的觀察與心境：人們的焦慮不安和因疫情於現實空間產生的距離與移動限制，乃至於網路的便利產生的繁複交流與自由。音樂流暢優雅，樂曲後段溫暖的旋律讓人感受到作曲者對人性的信念與幸福的憧憬。創作風格頗能呼應下半場法國作曲家蕭頌那溫暖懷舊的樂風。</p>
<h3><strong>參、合唱與聲樂作品</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年的國人合唱作品以台北愛樂文教基金會主辦的音樂會演出質量較可觀，其他如台大、福爾摩沙、拉縴人和台北室內、木樓、幕聲等合唱團也在演出中加入了若干國人作品。宜蘭縣五十蘭愛樂合唱團2020年也製作了全場國人作品音樂會，提升了東部地區的音樂風氣。聲樂演出則以詠樂集的「陳義芝詩選與臺灣作曲家歌樂創作」和游昌發的「如歌之必要－瘂弦詩歌」發表會較為突出。</p>
<h4><strong>一、台北愛樂文教基金會</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年夏天，台北愛樂文教基金會於艱困的疫情條件下仍舉辦了數場國際合唱音樂節系列音樂會。7月26日的「合唱．台灣（壹）」首先由幕聲合唱團演唱林明杰的《西江月》，此曲為合唱團委託創作之《紅樓夢－柳絮詞五首》系列之一，作品以曹雪芹《紅樓夢》中薛寶琴所作〈西江月〉譜曲，反映大家族風飄殘絮、落紅遍地的沒落境況。同樣於此系列，林京美則以薛寶釵的〈臨江仙〉譜曲，作品以鋼琴高音區反覆出現的裝飾音型呈現翩翩起舞的柳絮，後段於「<span class="bkt">好風頻借力，送我上青雲</span>」的詩句中呈現出合唱的飽滿張力，表達寶釵志得意滿之態。林碩俊以泰雅族古謠編曲的《思念》將民歌旋律以輪唱與和聲堆疊持續推進、貫穿，表現原民愛情的專一與堅定。金希文的《日出台灣》則以阿里山日出為主軸，表現時光更迭下人們對土地與故鄉的真摯情感。其後，台北愛樂室內合唱團演唱了純真而富於童趣的石青如《一的炒米香》；呂金守的《油桐花》則委請巴斯克作曲家艾爾柏汀（Josu Elberdin）編曲，呈現出編曲者對此歌謠所描述之「滿山白花、繽紛飄落如飛雪」景象的感動；印尼作曲家朱瑋權以馬偕詩作譜曲的《最後的住家》，藉由悠遠的旋律和穩重的和聲表現了馬偕博士對臺灣的護持與眷顧，歌曲末段並加上原民風格的調式與活潑節奏，加強了音樂的躍動感。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8月1日的「合唱．台灣（貳）」首先由育才小學合唱團演唱郭志超以流行音樂改編之"Too Young"、"Hold My Hand"和"When You Turn Old"，三首作品以英、中、臺語交替演唱，並由郭志超本人領唱，搭配兒童合唱應答，輕快悅耳又生動有趣。擅長當代合唱作品的台北室內合唱團則演唱三部技巧艱難的合唱曲：潘皇龍以詩人陳黎詩作譜曲，為無伴奏八部合唱之《夢遊女》曲風平穩，首段音樂遊蕩在和諧、緊張、鬆弛的循環中，次段以不規則的張力與不平衡的動感呈現「夢遊」情境，三、四段接續以朗誦和歌唱塑造戲劇性張力和對比，彰顯詩中隱喻的愛恨情緒，末段倒轉第二段場景，回到夢遊情境，在愛恨糾纏中徐緩結束。賴德和以向陽詩譜曲的合唱版《咬舌詩》，曲中國臺語交替輪唱，並以俗諺造成詼諧逗笑語境，嘲諷現實生活與動盪世局之無奈。曲中並由鋼琴彈奏了三段愛樂者熟悉的莫札特《奏鳴曲K.331》、德布西鋼琴組曲《兒童世界》中的〈黑娃娃的步態舞〉和柴可夫斯基《第一號鋼琴協奏曲》片段，以熱鬧的後現代拼貼風格突顯詩人對現世的反諷與批判。曾葉發的《東山－詩經古懷》以《詩經．國風．豳風》裡的〈東山〉譜曲，描述出征多年的兵士渴望回鄉的心情，隱喻反戰情懷。曲中所用旋律為作曲家高中時即已譜寫，技巧則兼容線性的聲部處理和不協和音程色彩，搭配低音大提琴、擊樂和鋼琴低音的鳴響，樂思繁複而聲響豐富。音樂會最後由台北愛樂市民合唱團演唱錢南章的《合唱劇場－山豬妹》，將音樂會帶回輕鬆卻頗具疫情之現實感的旅遊氛圍。作品幽默地描繪作曲家伉儷2016年夏天參與花東旅行團的趣事。「山豬妹」為旅遊區販售特產的阿美族人，其販售風格生動逗趣。音樂劇以輕快詼諧的曲風、整齊劃一的動作，呈現了樂齡出遊的歡快和原民商販的世故。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;12月8日台北愛樂室內合唱團的「故．事－錢南章＆金承志創作音樂會」演出錢南章的《金包里傳奇》和金承志的《白馬村遊記》兩部大型作品。錢南章作曲、賴美貞填詞的《金包里傳奇》以舊時草山通往宜蘭的「金包里大路」為描繪對象，將傳統雜貨店（𥴊仔店）、磺港漁火、夫妻石等沿路的人文景緻融入作品中，音樂流暢、風趣而不失常民歷史意趣。大陸新生代作曲家金承志的《白馬村遊記》則以音樂描繪世間的桃花源，樂曲包含了民歌、童謠、戲曲、祭祀吶喊等元素，平易近人地呈現了傳統中國文化落於常民生活的雋永滋味。作品雖以調性語法寫作，卻能藉由中文對偶與歌詞的鄉土底蘊傳達出作品的民族風格。</p>
<h4><strong>二、台大、福爾摩沙、拉縴人、台北室內、木樓與宜蘭五十蘭愛樂合唱團</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年2月12日，台大合唱團於疫情初起時如期舉行的「2020冬季音樂會－緣」，上半場以多首具有基督教「祈求上主垂憐」之意的宗教小品開場，之後演唱錢南章以鄭愁予詩〈錯誤〉譜寫的作品，刻劃歸人過客的無奈心境，作品風格平易，音響活潑有趣。周鑫泉以杏林子散文創作的《來不及的愛》，描述人與人間因未及表達真實情意而生的後悔與惋惜，提醒世人及時表達真愛。音樂會最後演唱已故旅美作曲家劉新誠的英文清唱劇《紅樓夢》，作品由陳煒智節選David Hawkes的《紅樓夢》譯本並酌添旁白，再點綴小提琴樂聲，以英文歌詞詮釋東方文學經典。全曲以石頭（賈寶玉）的觀點出發，道出人世間的盛衰興替、悲歡離合，並從《紅樓夢》大觀園的故事情節，譜寫愛恨情仇與貪嗔癡等人類情感。作品雖間歇採用五聲音階材料，也嘗試以音樂劇方式，藉撥算盤的姿勢和聲響呈現某種東方意象，然而，因語言採用英譯文本，音樂整體風格似更近於西方的音樂劇或西方世界的「東方想像」，不易給人鮮明的東方（或中國）色彩和文化情境。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6月19日拉縴人男聲合唱團於國家音樂廳舉辦的「愛與贖」音樂會也始於多部宗教性合唱作品，其中尤以松下耕的《主は私の羊飼い》（主是我的牧者）、《罰與罪》第三樂章和《ほらね》（看吧，伊藤靜詩）在旋律的優美、詞意的深刻和歌唱表現力等方面最具質量。國人作品如吳孟翰的《耶和華是我的牧者》、劉新誠的《如此愛你》和黃俞憲的《深笑》均為平易近人的作品。音樂會以吳孟翰編曲的《約會約會》結尾，詼諧的曲調搭配戲劇性對白，營造出愉快的音樂氛圍而一掃疫情的苦悶。7月18日福爾摩沙合唱團的「返來」音樂會，上半場亦選取多部西方宗教作品。合唱團擅長的本土曲目則演出樂團伴奏蔡昱珊改編的多首閩南語歌謠，曲風平易悅耳。其他如石青如的《禮物》藉詩人李敏勇詩表現父親面臨待嫁女兒的愛與祝福，日本作曲大師三善晃編曲的北海道民謠《ソーラン節》則呈現出北海道原住民身處大自然的質樸情感與旺盛生命力。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年9月26日，由曲盟製作、台北室內合唱團演出的「聲迴臺灣」音樂會呈現了國內三個作曲世代的合唱新作。青壯世代：邱妍甯借助臺灣童謠《白鷺鷥》歌詞禍福相倚的寓意，將若有似無的旋律與和聲動機嵌入曲中，唱出那「駐留在生命中難以抹去的歌」；余忠元的《梅落》則以五代詞人馮延巳的《鵲踏枝》入樂，融南管聲腔與崑曲語彙於一爐，並以支聲複調和塊狀和弦織體對比出纏綿婉轉和鬱情宣洩的詞意。中生代：林桂如選取臺語童謠《丟丟銅仔》、晚清詩人李望洋的漢詩《宜蘭集詠》－〈境比桃源〉和日治時期總督府國語學校助教授宇井英《台灣周遊唱歌》有關臺灣東北海岸的描述，借當代電子音樂之Convolution音聲合成概念加以轉化，引導聽眾穿越時空，進入想像的宜蘭火車之旅。陳瓊瑜的《島》則以書寫自身存在感受的詩作，藉由支聲複調的純淨線條勾勒出超現實的飄浮感，調性色彩的中段回歸溫暖的生活實感，隱約出現的民歌曲調呼喚著聽者的鄉愁。資深作曲家：潘皇龍的《夢遊女》透過流動的聲韻和速度變化所營造的張力與對比，刻劃夢中形影的分合、遊盪與人間情愛的對立、消解。錢善華的《落水歌》以雨聲音效為基底，插入不同的旋律，營造出頗具新意而不失親切感的民歌氛圍。曾興魁的《春天的腳印》則以謹嚴的轉調手法詮釋本土詩人謝武彰刊於《童詩五家》的詩作，樂曲在輕快的小步舞曲樂段後進入行板的結束部，以充滿希望和光明的「<span class="bkt">燕子在天空歌唱：春天就快到了！</span>」結束全曲。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;12月9日，木樓合唱團的「光之境－月光．秘境．傳說」音樂會演出的國人作品有林京美的《我心默然等候》，作品以人聲特質從氣聲與細微出發，逐漸堆疊累積表現張力，以呈現在人生艱難道路上持守的堅定信仰；任真慧的《暮島之聲》則以詞彙的重複與發音，藉由文字本身來構成聲景呈現；趙菁文的《會唱歌的數字們》由十首關於「數字」的小品構成，由一到十，連結聲音、文字與其延伸意義的關連與想像，各自獨立又互有聯繫。張舒涵改編鄭朝方創作的《老屋》音樂深刻雋永。音樂會多部作品突破了傳統和聲限制與對人聲的想像，以五個主題鋪成五幕，融合各類曲風，呈現出不同民族間的多樣音樂語言特色。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另值一提的是2020年10月8日由宜蘭縣五十蘭愛樂合唱團假宜蘭演藝廳舉辦的｢無限蘭芽－台灣合唱創作之夜－太平詩路首演」。音樂會演出了冉天豪的《那一晚》、《願》，陳維斌的《桐花圓舞曲》、《今日哪又閣咧落雨》、《花廳門》，石青如的《世界恬靜落來的時》、《烏罐仔裝豆油證》、《如果明天就是下一生》、陳佩蓉的《相見歡》、曾筱雯的《老母相思五月花》、陳中申的《水果們的晚會》等作品，以及合唱團委託三位作曲家，以李潼詩譜寫的《太平詩路》組曲，包括蘇屹的《山嶺》，余忠元的《半日閒》、《素顏相見》、《心路》和李雅婷的《紅葉》。本場音樂會為東部地區難得的製作，由合唱團指揮高端禾主導的選曲與委託創作為宜蘭地區音樂文化開啟了嶄新的一頁，假以時日必定能為宜蘭與東部地區帶來豐富的音樂饗宴和活潑的文化創作。</p>
<h4><strong>三、陳義芝詩選歌樂創作與游昌發的瘂弦詩歌</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年國人作品的聲樂演出以兩場主題式音樂會較為優異。9月25日由詠樂集製作的「《秋天的故事》陳義芝詩選與臺灣作曲家歌樂創作」演出七位作曲家以陳義芝詩作譜寫的歌曲：蕭慶瑜的《雪地人跡》以德布西鋼琴前奏曲《雪地足跡》的持續音型為背景，襯托旋律歌唱，手法有如古諾以巴赫《C大調前奏曲》襯托《聖母頌》的歌唱；《雪地記事》的鋼琴部份也引用了盧炎《鋼琴前奏曲》的主導音型，並加以展衍，以傾訴愛情。謝宗仁的《有贈》著重詮釋詩中重複出現的「野」字，音樂律動而奔放；《逝水》則以柔和的吟唱對比《有贈》的動態感。陳瓊瑜的《七夕調色》含蓄地描繪柔美的東方美女，《哀歌》則以沉重的頑固音型襯托詩意的孤寂與絕望。上半場壓軸的潘皇龍《一筏過渡》則以作曲家一貫的陌生化音響呈現出世間情感的虛實無定。下半場，羅芳偉的《我們去找我們的海》巧妙地以一種音響異化概念，藉揚聲器唱念出詩中具對偶結構的詩句，頗具新奇和幽默感；《手稿》則素樸地唱出詩中嘗試描繪之作家與手稿間的糾葛與疏離。羅珮尹的《無事－有贈》以較為現代的語法呈現詩中跳躍時空下的各種視覺、聽覺與嗅覺的官能感受；《花事三帖》則採取調性的溫暖語法唱出詩意的惆悵與浪漫。音樂會最後以賴德和的四部作品收尾：《蝴蝶啊，蝴蝶》以蜜蜂與蝴蝶的意象總結前三段詩節所描述之戀人的愛憎起伏；《可不可以，阿勃勒》則以反覆探詢的歌詞吐露忐忑不安的期盼。《有贈》以鋼琴滑音與和聲的擴充來描繪愛情所帶來的充實與昇揚；《秋天的故事》則如實唱出愛情的夢幻與相思，柔美地為整場音樂會劃下句點。10月17日由斐亞文化藝術製作的「如歌之必要－游昌發瘂弦詩歌」演唱了游昌發以十二首瘂弦詩作譜寫的藝術歌曲，展現了作曲家熟稔的調性寫作技術與美學理念，詮釋了瘂弦詩作的豐富面貌。音樂會最後一首作品《瘋婦》雖為調性語言，生動的滑音與唸唱使作品頗具現代音效。</p>
<h3><strong>肆、音樂劇、音樂劇場與跨界展演</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年較值記述的音樂劇和跨界展演，首先為9月26日於鳳甲美術館舉行的「第三屆JPG擊樂實驗室成果發表－潘朵拉幻象」，展演由朱宗慶打擊樂團與財團法人擊樂文教基金會聯合製作，以打擊樂結合電子音樂與新媒體藝術跨域展演。展覽部分有與觀眾互動的聲響裝置，演出則藉環繞喇叭的設計和三面環場的影像圍繞著觀眾，打造出充滿錯置與幻覺的場域，將聲音、影像、燈光、空間等現象納為創作元素，以演算程式帶出精確結構。整場演出藉由音樂的起落、視覺設計轉化、電子音效的鋪陳、舞者肢體和環繞喇叭，使過程富於新鮮與實驗感。觀眾如身置潘朵拉盒中，放大視聽感官去體驗聲音、聲響與視覺間的交織互動。10月10-11日，國立臺北藝術大學音樂系與新媒體藝術系合作的「2020關渡藝術節－後花園：科技與音樂跨域實驗劇場」，展演由新媒體藝術系教授王俊傑、王連晟與音樂系吳思珊、陳立立教授，以及法國擊樂演奏家Jean Geoffroy等人聯合指導，帶領北藝大學生集體創作演出，運用音樂設計、科技影像、自製樂器與聲學音響系統營造出富於實驗性質的科技音樂劇場，挑戰觀眾的聽覺與視覺感受，嘗試表達「超越」的核心概念，探索自我並反思生存環境。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;12月5日由國立臺灣交響樂團與故事工廠合作的《妖怪臺灣》音樂劇假臺中市中山堂舉行。音樂劇製作嚴謹，劇中之妖怪形象雖來自作家何敬堯《妖怪臺灣》系列著作（如蛇郎君、阿里夏蓋、滾地魔），然而劇中的「妖怪」形象不免受到西方著名音樂劇，如安德魯．洛伊．韋伯之《貓》的影響，在本土音樂劇累積製作經驗與創作風格的過程中，尚屬無可厚非。音樂劇的基調和風格輕鬆詼諧，一掃臺灣本土民間傳說之悲憐色彩。劇情結合現代網路寫作與靈異傳說，劇中人物出入現實與虛幻之間，作曲家張菁珊創作、編配的音樂與歌曲流暢悅耳，整體可說是一部老少咸宜，兼具趣味性和藝術性的展演。12月12-13日，捌號會所於臺南涴莎藝術展演中心藝術館和臺北歌德學院藝文活動廳館舉辦的「2020再壹波藝術節」以「精神遊牧」為主題，於疫情間思考「當疾病停止人們遷徙，我們縱使不曾為停止對藝術的渴求，又將如何重整行囊再次踏上旅程？」。嘗試於有限的演出環境中創造跨國連結，以「錄像作品」與「現場演出」呈現國際合作作品。首演之國人創作有林芳宜、高愷怡與劉韋志作品。現場由臺灣新世代傳統樂器演奏家組成的「亞洲當代傳統樂團」和跨域聲響音樂家組合的「她室」，以及舞蹈家郭箏跨界合作，呈現新世代的思維與創作。音樂以自由即興方式實驗中西樂器聲響的拆解與再製，舞蹈則於「迴路」的反思中跨域呈現自我。</p>
<h3><strong>伍、學術研討會與出版</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年與國人創作有關的研討會以文化部委託北藝大音樂系，於10月30、31兩日假臺灣戲曲中心舉辦的「重建臺灣音樂史－臺灣音樂史研究回顧與發展」國際學術研討會較為重要。會中相關主題有〈論臺灣當代古琴創作：以陳立立作品《憂患》為例〉、〈所唱即所信：駱維道之臺語聖樂研究〉、〈聖樂與崇拜的關係：以浸信會懷恩堂近50年的國語崇拜所使用的聖樂為例〉以及〈作曲家郭芝苑的文化傳承：以苗栗縣郭芝苑音樂協進會、郭芝苑室內合唱團、苑裡青年掀海風為例〉等篇。羅芳偉的〈論臺灣當代古琴創作：以陳立立作品《憂患》為例〉側重探討陳立立作品《憂患》（"Hypochondriasis"）對古琴音響之探掘及該作品如何藉電子原音技術開發古琴之各種延伸性新穎音響。兩篇基督教相關論文呈現出本土長老教會與浸信教會之聖樂實踐的差異。王微儂的〈所唱即所信：駱維道之臺語聖樂研究〉透過駱維道的聖樂創作呈現出臺灣教會音樂的脈絡化現象，如駱氏作品以臺語創作表達神學意涵，以旋律對位取代西洋和聲，擷取臺灣素材作為創作元素等。至於研討會的焦點報告：〈作曲家郭芝苑的文化傳承：以苗栗縣郭芝苑音樂協進會、郭芝苑室內合唱團、苑裡青年掀海風為例〉則較偏向地方音樂文化活動的引介，推廣意義多於學術研究價值。而北藝大團隊於2020年3月出版的《臺灣新音樂的歷史見證》則集結了2019年同系列的「重建臺灣音樂史」研討會論文，收有探討金希文和賴德和等作曲家作品的研究論文。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，北藝大與臺灣音樂館共同舉辦之「聽見臺灣土地的聲音－李哲洋先生逝世三十周年紀念活動」亦於11月28日假北藝大舉行，開幕式由張佳韻指揮北藝大音樂學系室內樂團演奏作曲家高約拿的第一號交響詩《夏天鄉村的黃昏》。作品樂譜為李哲洋收藏，在北藝大傳音系李婧慧教授和吳承庭、張惠妮兩位老師協助下得以重建。作品在風格受到德奧古典、浪漫音樂影響，讓人聯想到貝多芬的《田園交響曲》。作品中的曲調也融合了五聲音階，而帶有民族色彩，為日治時期繼江文也、李志傳之後，率先以西方作曲手法寫作管絃樂曲的作曲家。當日下午的紀念論壇則以臺灣音樂史、臺灣民歌採集運動、原住民音樂的採集與研究、「全音音樂文摘」與臺灣音樂史研究、李哲洋文物典藏、修復及展望等主題，邀請顏綠芬、雷驤、林保堯和民族音樂學者呂鈺秀、明立國、范揚坤等人，從不同角度闡述李哲洋對於臺灣音樂文化的貢獻。</p>
<h3><strong>結語</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年因疫情故，民間自辦音樂會受限於經費預算與人員調度，取消或停辦者不在少數。惟公部門製作或委製之音樂會多得以延後或如期舉行，突顯出疫情期間公部門持續推動音樂文化活動的使命感與優勢。如文化部所屬傳藝中心臺灣音樂館委製，2020年以「臺灣音樂憶像」為題的作曲家專場即有3月和10月的兩場蕭泰然紀念音樂會，10月8日的「賴德和樂展」，11月7日的「陳主稅樂展」和12月13日的「馬水龍逝世5週年紀念音樂會」。其他尚有 9月25日的「陳義芝詩選與臺灣作曲家歌樂創作」，9月26日的「聲迴臺灣」合唱音樂會，11月28日的「樂盈原鄉」和11月29日的「琴想里山」兩場室內樂委創首演音樂會，以及11月28日的「聽見臺灣土地的聲音－李哲洋先生逝世三十周年紀念活動」暨高約拿交響詩《夏天鄉村的黃昏》的演出。延續2019年以「臺灣音樂憶像」為題的音樂會，此一系列儼然已成國人作品發表的重要場域。其他如北市交的潘皇龍《大燈對》演出和「溫隆信樂展」，國臺交的楊聰賢《山水舞鶴》首演和「客家音畫」，均為2020年國人作品的重要展演。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以亞洲作曲家聯盟台灣總會製作的三場「臺灣音樂憶像」委創首演音樂會為核心，似可輻射出臺灣老中青三代作曲家創作風貌與多樣風格：台灣作曲家協會暨亞洲作曲家聯盟台灣總會兼容了不同世代的作曲家和創作風格，從調性、非調性到當代音響學派兼而有之，即使同樣以「臺灣」為主題或創作元素，仍可開展出不同風貌的創作；現代音樂協會的國際現代音樂節則鼓勵了更多四十以下年輕作曲家的作品，或音響滿溢、或詼諧遊戲，呈現出更多自由無羈的實驗精神與鮮銳的創作活力。而多位資深作曲家甚至獲得更多資源來發表大型作品，如潘皇龍的《大燈對》、溫隆信樂展、賴德和樂展和楊聰賢的《山水舞鶴》，其他如錢南章的《合唱劇場－山豬妹》和《金包里傳奇》以及游昌發的「如歌之必要－游昌發瘂弦詩歌」也都能在長期合作的合唱團與學生支持下，展現其穩定、成熟的創作質量，更呈現出更多音樂與文學結合的現象。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本文僅就2020年較重要的國人作品演出加以討論，疏漏難免，尤其因疫情故，2020年上半年多場音樂會延後至秋冬天舉行，南北各地音樂會的頻繁密集使駐點工作難於兼顧。整體來說，臺灣藝文界於2020年疫情期間雖展演稍有停輟，然因國內疫情控制得宜，暑期後各項展演多能如期舉行，作曲家們孜孜寫作、匠心營造的作品也多能透過各個展演分享給音樂同行和不同層面的欣賞者，真可說是舉世疫情中的樂土。在此安定環境下，期盼臺灣的作曲家和音樂工作者秉持自由開創精神、文化傳承使命與寬廣的包容胸懷，持續創作出具有深刻思想和真誠感染力的音樂作品。</p>
