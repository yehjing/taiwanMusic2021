    <h3><strong>結語︰</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧2020年臺灣當代音樂的文化現象與生態環境，確實不難察覺諸多特殊現象︰似乎編制大或涉及國際交流事務較多的團隊所遭受的影響較大，應變速度較為緩慢；而個人或編制小的團隊，應變速度較為快捷。音樂展演的版圖似乎面臨重新洗牌，需要重新布局的時刻突如其來，慰為契機。當然區隔異地，視訊音樂會應運而生，解決了舟車勞頓並避開了武漢肺炎感染的風險。所以我相信，未來的音樂創作未必需要遵循傳統的總譜書寫方式，樂曲的編制也可以靈活搭配，展演的形態也可能跨越空間的限制，再透過視訊系統加以統整。將拙作《萬花筒》為任何音樂家的即興音樂系列作品，與《迷宮．逍遙遊》機遇音樂系列作品相結合，參酌《跨越時間與空間的藩籬》，展現另一種音樂創作與展演的可能性，將為音樂生態環境創造無限生機。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;時代在變，潮流也在變，優勝劣敗的循環屢試不爽。我們是否有可能逐步建構自我主體性的音樂生態新環境，除了個人與團隊的努力以赴，在上位者扮演著前瞻性推波助瀾、洞察機先，甚至於是扭轉乾坤的關鍵角色。舉個例來說，國家文化藝術基金會為了鼓勵演出國人作品，在申請補助案格式曲目設計中，增列是否含國人作品，雖非必要條件，卻有部份啟迪的作用產生。老實說，現代音樂節自始至終總是行家的聚集，永遠都是曲高和寡，也有它們的重要性與學術性，展現各地方的音樂特色。但是，如若在例行性的經典曲目間，安排一首國人作品或當代音樂作品，應該是輕而易舉的，它所產生的對比效應，一方面讓演出者更為精確區分時代風格的差異，也為聽眾帶來更為寬闊的視野，與明確的音樂史學觀，相信會是兩全其美的選擇。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在此慎重地呼籲演奏（唱）家與演出團隊，不論公設或私人團隊，在曲目設計上，在作曲家、曲目名稱外，千萬別忽略了創作年代的意義。一方面代表著隨機教育的功能性，另一方面也給作曲家形成某些壓力，讓作曲家直接面對經典作品。倘若一首21世紀的作品，聽起來像18、19世紀的風格，那會是個什麼樣的場景；或明明是一首21世紀的作品，在原創性、前瞻性與專業性上卻讓人無從定位它的風格？內行人看門道，外行人看熱鬧，吾人不可不深思！
    </p>
