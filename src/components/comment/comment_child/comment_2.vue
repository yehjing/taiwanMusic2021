    <p class="firstRow"></p>
    <h3><strong>前言</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020庚子年的確非常特別，武漢肺炎（COVID-19）貫穿了一整年，還持續發難中，而且也席捲了全球五大洲，造成史無前例的重大浩劫。它不僅改變了人們的日常生活方式，也考驗著各國的衛生福利措施，以及國際間各相關組織機構的應變能力。臺灣因為應變得宜，甚至於超前布署，雖然藝文活動在前半年幾乎停擺，後半年已經逐漸恢復正常，值得慶幸。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;也因為半年的幾乎停頓，讓我們有更多思考與反省的機會，我們可以更為深入、更為徹底的針對我們的音樂教育與展演生態，提供討論與建言。比方說我們實施了將近半世紀的中、小學音樂實驗班制度的績效與影響，我們的音樂會展演型態與臺灣音樂的主體性認知是否相吻合，何種音樂會曲目格式設計符合當前的需求與國際潮流，甚至於也應該探討時下將音樂歸屬於表演藝術的利弊得失。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;老實說，將音樂歸屬於表演藝術的問題很大，大到足以消滅音樂的藝術性與原創性。畢竟音樂的養成教育與演出型態和表演藝術有別，嚴格來說，在音樂的演出，也只有歌劇或音樂劇場的演出勉強可以歸屬表演藝術範疇，其他眾多的音樂會演出型態就非表演藝術所能涵蓋。再者，將音樂劃歸表演藝術，音樂終將淪落為配樂，喪失了音樂作為獨立而專門的藝術的可能性，不可不謹慎。試想我們將藝術分成表演藝術與視覺藝術的二分法，遠不如將它劃分成音樂（時間藝術或聽覺藝術）、表演藝術與視覺藝術的三分法來得恰當。當然，歐洲行之數百年的劃分法，普遍暢行於國際間，茲以大學藝術教育為例︰如果一所兼具各種藝術的大學，自然而然的取名某某藝術大學；如果它只有音樂、戲劇與舞蹈，那它的名稱就會是某某音樂暨表演藝術大學，而非單純的表演藝術大學。雖然筆者歷經30餘年大聲疾呼，仍然如同狗吠火車，只可惜我們音樂界沒能體會它的嚴重性，導致音樂生態的發展障礙重重，成為表演藝術的附屬品而不自知，又將如何拓展它的原創性、前瞻性與專業性？記得，筆者曾經應邀擔任第二屆「台新藝術獎」評審委員兼表演藝術類召集人，我就很明確的預言，將台新藝術獎分成視覺藝術與表演藝術，音樂類永遠沒有機會獲獎。不管從它們的藝術性、功能性，音樂演出的抽象性，要即刻引起評審委員或聽（觀）眾的共鳴，談何容易；而且當音樂的展演，抽離它的抽象性，而強化它的功能性，就如同國內當前的音樂生態環境一樣，那就僅僅剩下比較庸俗的功能取向，而失去了它那作為獨立而專門的藝術的可貴了。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;接下來，筆者也想談談當前音樂會製作的種種現象。比方說，有很多樂團，它們多數為了票房收益，以為「國民樂派」風格或所謂的「民俗歌謠」是我們欣賞音樂的最大公約數；甚至於數十年來，都還在執行推廣普及的任務，而絲毫不覺得，還有更為寬廣的多元風格選擇與反映當代精神的可能性。記得前些年，有一個知名的臺灣樂團，到北歐巡迴演出，它們也不能免俗，在歐洲經典曲目之外，刻意安排了臺灣當紅的作品作為代表。演出開始原本讓人刮目相看，驚嘆於臺灣樂團的實力，卻在臺灣作品上場後，逐漸引發聽（觀）眾坐立不安與騷動，甚至於質疑臺灣音樂仿傚俄羅斯國民樂派的奇異現象，讓一場難得的國民外交引發了自曝其短的悲慘下場，誠然始料未及。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;再者，臺灣音樂會曲（節）目表的格式設計，的確也讓人眼花撩亂。主要的有歐式或曰ISCM/WNMD（國際現代音樂協會/世界新音樂節）格式︰依作曲家姓氏、作品名稱、編制與創作年代排列。有美式或曰晚會式，依作品名稱與編制排列，最後才是作曲家姓氏。兩者各有其優點，通常歐式比較屬於專題式節目設計，區塊不多，各區塊時間不會太短而各自成單元，在作品發表或管弦樂團、室內樂團的音樂會使用；作品風格與音樂史學觀非常重要，給人留下比較統合而完整的印象。美式剛好相反，節目數量繁多類似流水帳，個別曲目演出時間短而轉換頻繁，散見於晚會、同樂會中使用；給人留下了五花八門、多元化曲目設計的印象；至於出自那個作曲家，或何種風格流派則無關緊要。排版的方式，有頁面左邊對齊或居中兩種。歐式排列左邊對齊或居中皆宜，美式排版以曲目左邊對齊為主，作曲家擺放在右側；因為居中佔用空間過大，比較不合適。以筆者2011年主持「亞洲作曲家聯盟/亞太音樂節」為例，我們從亞太會員國寄來的400餘件作品中，經由評審委員會選出100首作品，依編制並衡酌各國能見度劃分成15場次，分別假臺北市、臺中市與新竹市演出，節目冊為A4大小規格，依演出日期編排近280頁。為了區分各場曲目表與相關內容（含作曲家簡介、樂曲解說與演出者或樂團簡介）。曲目表採居中編排，內容則依一般編排方式，以茲凸顯場次之間的區隔。當然，在國際化的前提下，特別是國際交流的場景，我們通常會雙語並列，我們不難發現，有以段落為單位，上下中英對照，或以頁數為單位，左右中英對照的。但是，還有另一種可能，方便單一語文的持續閱讀，不至於區分語言的主副關係，而是將兩種語言文字視為同等，如同我們1994年在美國紐約舉辦的「台灣作曲家樂展」的節目冊，只要在節目冊設計編排上下翻轉，兩邊都成為正面，而在中間交界處擺放插圖或留白即可。至於一般節目冊頁碼的設定，右側為奇數（正面），左側為偶數（反面），乃國際通行的慣例，我居然在眾多音樂會評鑑中，偶爾也會發現例外，雖然事小卻讓人驚訝。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;COVID-19肆虐下臺灣當代音樂的文化現象與生態環境，依筆者一年來的親自參與作品發表與聆賞音樂演出的觀察與搜集，以及總計畫辦公室提供的資料，詳加分析、比較，擬分公設樂團、大專院校音樂系所、國立傳統藝術中心臺灣音樂館，與民間樂團與合唱團等四個部份，附上部份音樂會的DM，分門別類敘述如後。
    </p>
