<h3><strong>伍、後記－每區客家八音團永續經營與維生系統的建議</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如前述，東部客家人乃日治時期以來經過二次、三次移民過程而定居下來的移民及其後代，相當在意生存的機會。東部客家人約佔東部居民四分之一，其中鳳林、富里、池上等鄉鎮更佔一半左右。近半世紀以來，東部的客家年輕人多數移居北部、西部打拼，在彼處落地生根，留下壯年老年在家鄉務農打工維生，因此當老輩的北管、八音、子弟班成員逐漸凋零亡故，而新的社會經濟條件卻不利這些傳統音樂文化的發展，它們走向崩落消亡是大勢所趨，目前唯有依賴極少數著意傳統文化的有心人或需要這種演出技藝收入貼補家用的人<sup>23</sup>，在東部縱谷區角落勉強維繫著北管八音樂聲使其不致成為絕響。如果未來還想要持續聽到這種曾經伴隨客家移民日常生活、生命禮俗以及作為客家族群身分認同的文化元素，可能要拿出有效的方案來保存、振興、發揚甚至更加普及。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我們認為可以從幾個方面來做：</p>
<h4><strong>一、保存與扶植</strong></h4>
<p>1.以普查資料為基底，盤整東部北管八音可用個人和團體資源，建請縣政府及鄉鎮公所列入重點保護輔導對象，甚至頒發民間藝人證書，在象徵與實質上，給這些孤單奮鬥的八音工作者持續努力保存發揚傳統文化的動能。</p>
<p>2.以一、兩個體質還不錯的北管八音樂團作為重點對象，給予必要的專業診斷和精進輔導，再安排到各鄉鎮做系列推廣演出。從花蓮縣新城鄉（保安宮）、花蓮市（延平王廟或城隍廟）、吉安鄉（五穀宮）、壽豐鄉（客家文化廣場）、鳳林鎮（受天宮或義民亭）、光復鄉（大富富安宮）、瑞穗鄉（青蓮寺）、玉里鎮（協天宮或舊玉里國小）、富里鄉（東竹義民廟）、池上鄉（客家文化園區）、關山鎮（得篙社區或天后宮）、鹿野鄉（瑞和社區），擇週六傍晚演出，每個月一至兩場，演出內容包含八音、客家唸謠、客家山歌（樂團伴奏）、撮把戲等，活動過程必須有活潑生動的內容介紹，使民眾逐漸認識客家音樂文化極其特殊韻味。活動邀請鄉鎮公所、社區、農會與地方宮廟參與，請各地方公所、農會提供農特產品，作為有獎徵答的獎品（非本鄉鎮）。每場補助5萬元（包含演出費、音響費、主持費、交通費、誤餐費等）。</p>
<h4><strong>二、推廣與教育</strong></h4>
<p>1.與各鄉鎮內客家人為主的社區發展協會協同合作來推廣收效會比較大。</p>
<p>2.建置並分類婚喪喜慶等不同場合所演奏的八音樂曲，同時建置工尺譜、簡譜通用的樂譜，以方便推廣教學。</p>
<p>3.重點扶植至少3至4個北管八音樂團，鼓勵他們主動配合地方社區宮廟年中行事、廟會慶典和生命禮俗，提供表演和服務，並給予適度經費補助。</p>
<p>4.建立媒合網頁平臺，凡通過平臺評鑑的媒合活動，給予適度經費補助。</p>
<p>5.鼓勵藝師進入社區中小學成立北管八音社團，使用淺顯易懂的簡譜來教學，社團稍具基礎後鼓勵參與社區各項文化慶典活動或文化才藝競賽。</p>
<h4><strong>三、融舊與鑄新</strong></h4>
<p>1.鼓勵各學校成立新八音樂團，使用傳統的嗩吶、鑼鈔、大小鼓、胡琴之外，還可加入小提琴、大提琴、爵士鼓等，以增加學生學習意願。</p>
<p>2.舉辦八音樂團演藝微電影競賽，提振推廣效果。</p>
<p>3.創造在流行歌曲中置入客家八音的機會，打開新生代的閱聽之耳。</p>
