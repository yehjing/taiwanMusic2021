<sup>6</sup>位於花蓮縣花蓮市國強里5鄰豐村72號。舊稱「復興宮」，1851年（清咸豐元年）先民黃阿鳳先生始自臺北淡水等地區，邀同十六股東，募集民工東來開墾、行前至臺南恭迎延平郡王鄭成功暨其部下甘輝、張萬禮神像前來，設案崇祀借資景仰，時為先住民騷擾築堡自衛，稱復興庄。期間深承郡王庇蔭，人畜鼎盛靈應事蹟、迨1860年（咸豐九年）建宮奉祀，宮名即是庄名。1890年間（光緒十六年間）首次重建，1951年（民40）10月22日花蓮發生地震，廟宇震毀，地方士紳鄭東辛先生等人募資修建，並更名為「延平王廟」，為保存原有宮貌盡量延用舊材，1953年（民42）修建完成，現廟於1997年（民86）重建完成。<br>
<sup>7</sup>位於花蓮市成功路169號。城隍廟建於1934年（昭和九年），供奉的主神為城隍爺；1937年（民26），日人即著手寺廟改建工作，大肆摧毀臺灣的廟宇，焚毀神像，因此花蓮城隍廟也是日人決定拆除的廟宇之一，但是跟花蓮城隍廟隔幾十尺距離的鄭聖祠因供奉日人血統的國姓爺鄭成功，獲得日人的尊重，所以不在日人拆廟之列；後來，當地人由於鄭聖祠廟宇老舊不堪，於是向日人建議，讓鄭聖祠的主人鄭成功神祈移入城隍廟，取代城隍而為主神。至今，花蓮城隍廟廟貌得以保存，而國姓公爺的廟號「鄭聖祠」，事實上，定沒有將大殿上的城隍爺自主神位置移去，只是把城隍神像的王爺帽改變，使之看起來和國姓爺的帽子相似，使其誤認國姓爺就安坐在主神的位置，為防止日人搗毀原來屬城隍廟的其他神像，居民紛紛把能夠搬動的神像帶走，藏於民家，其中也包括現在安坐在大門旁的七爺、八爺神像，至1949年（民38）國民政府播遷來臺，日人加諸城隍的限制才宣告解除，城隍也就恢復舊觀，而鄭成功神像也從此留在花蓮城隍廟。<br>
