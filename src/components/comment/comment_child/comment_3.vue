    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由臺灣大學學生會與臺灣大學台語文社舉辦的臺灣首場本土語言音樂祭「灣流音樂祭」10月登場，有感於愈來愈少人使用臺語、客語、原住民族語，主辦單位希望透過免費音樂會的舉辦，讓更多人認識與守護本土語言（簡若羽，2020），雖然是小型、非官方、非營利的演唱會，但為非主流語言所舉行的演唱會，「灣流音樂祭」可謂是臺灣音樂祭中的首創。
    </p>
    <h3><strong>肆、臺灣與全球化趨勢</strong></h3>
    <h4><strong>一、線上串流與線上演唱會成主流</strong></h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因為疫情，全球的宅經濟有爆發性的蓬勃發展，根據KKBOX和KKTV與KKIT的共同研究和統計指出，超過60場售票活動被取消或延後，影響超過35萬名觀眾，使用OTT媒體服務（Over-the-Top
      Media
      Service）、聽Podcast、和觀看線上演出是民眾宅娛樂的首選管道；因應措施，過往人工驗票的進場方式被實名/實聯制入場方式取代，驗票時間從平均3小時縮短至平均20分鐘；肺炎疫情中斷藝人演出的行程，許多活動轉而以線上直播的形式舉辦，因為突破了地理的限制，讓國際歌迷也有機會能看到臺灣藝人的演出，售票型的線上演唱會遂成主流（KKBOX，2020）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在中國，抖音因應疫情推出了「沙發音樂會」，以居家環境為場景，在節目中清唱、聊天、甚至吃東西，以平易近人的氛圍與歌迷互動；在美國，Lady
      Gaga和其他藝人與WHO合作，舉行了「One World: Together at
      Home」線上慈善音樂會，以馬拉松式演唱會，穿插訪談並對醫護人員致敬、幫WHO募款；在南韓，防彈少年團（BTS）舉行了付費的線上演唱會「Bang
      Bang Con The
      Live」；在臺灣，5月31日，五月天在YouTube平臺上辦了一場「突然好想見到你」線上演唱會，大型規模且零收費，當天直播人數超過60萬，全球轉播平臺超過4200萬觀看人次，必應創造執行長周佑洋表示，過去線上演唱會的建設是以宣傳和輔助為目的，但經過疫情的催化，線上音樂會的模式可以進化與延續為常態運用（黃亞琪，2020）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據IFPI報告指出，2019年全球唱片市場總營收為202億美元，其中，串流音樂營收114億（佔全球56.4%）、數位音樂下載15億美元（佔全球7.4%）、而實體銷售只有5億美金（佔全球2.5%）（IFPI,
      2020）。以線上串流為聆聽音樂的管道和平臺，是全球的主流。
    </p>
    <h4><strong>二、智財權的守護與區塊鏈</strong></h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1997年後網際網路的興起，MP3與P2P技術衍生的盜版問題，讓流行音樂產業面臨數位科技時代的重大衝擊（MIC產業情報研究所，2016）。其後，隨著聯網環境的持續優化、行動裝置普及與物聯網等新科技興起，消費者對於音樂取得的偏好與消費習慣更衝擊傳統實體的音樂產業，讓過去的提供實體唱片為主的音樂產業轉型提供數位音樂發行與各式串流平臺服務。另外，網路時代層出不窮的盜版問題，也在近年智慧財產意識抬頭後，透過創作者宣導、服務商維護與消費者消費習慣的改變，配合新興區塊鏈技術的興起，逐漸有了全新的風貌。
    </p>
    <p><strong>（一）數位發行</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;現今數位化串流平臺與線上串流音樂儼然已成為現今音樂聆聽的主流，根據IFPI在2019年全球音樂產業報告指出，串流音樂收入佔全球唱片市場總收入達47%，將近一半，數位化的串流平臺所創造的利潤，如今已是音樂產業的重要食糧，付費串流音樂的收入，光是2018年就成長了32.9%（IFPI,
      2019），再加上前述2020年報告，種種資料均顯示，現今的流行音樂產業已與新興科技產生密切的跨界合作，並促使產業升級。而配合串流平臺與數位音樂普及，數位發行也成為業界常態。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;數位發行相較於傳統經紀簽約的唱片發行，是指音樂人透過網站上架個人作品，發布至iTunes、Spotify、iNDIEVOX、KKBOX
      等各大數位通路，例如在田數位發行（Soundscape）、派歌（Packer）等（在田數位發行，無日期；派歌，無日期），有了大型數位通路深厚的行銷資源與廣大聽眾，不僅提高創作的曝光度，也讓創作有機會躍上更大的國際舞臺。其次，數位發行與過去傳統發行最大的差異在於省去實體鋪貨上架的繁瑣程序，並透過網路銷售打破地域限制，由於沒有過去實體銷售難以評估的龐大花費，通常數位發行商也都標榜合理、直接分潤，沒有隱藏費用，讓近年興起的微型工作室、個人YouTuber等更能放心的創作並且獲得合理報酬。
    </p>
    <p><strong>（二）智權管理</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;智慧財產意識的抬頭也讓藝人和唱片公司更重視創作的智慧財產議題，配合近年區塊鏈技術的興起，保護智慧財產權的研究也越來越多，吸引金融業、法律、資工、音樂等領域專業人才共同創建平臺與解決方案，包含SONY已以區塊鏈技術建置著作權的管理平臺（Kao,
      2018）。2019年，Maxonrow陽光區塊鏈登臺，是世界唯一實名制區塊鏈，與遠東航空、國立臺灣師範大學亞洲流行音樂數位科技研究中心、粉絲達克娛樂國際控股集團、沐環球控股集團、音樂霸、ALLN、紅星以及東來寶基金會等學術及產業界簽屬合作協議（張佩芬，2019）。2020年，各方推出區塊鏈與智財守護方案；國內有網擎資訊與新創區塊科技合作推出數位資料存證安全方案（iTHome,
      2020）；師大與清大攜手合作提出的影音智財權管理與區塊鏈帳本結合的方案，使用區塊鏈技術為基底，建立智財權管理資料庫、透明化的分潤機制與帳本、影音著作權的授權與管理、法律智權諮詢等面面俱到的方案，打造智權管理工具，並與安信達國際智權顧問股份有限公司共同舉辦說明會（Liu,
      2020）；資訊工業策進會則與音樂霸共同打造數位音樂內容管理平臺，導入區塊鏈技術與版權認證機制，解決盜版、抄襲、內容授權與分潤等問題（楊又肇，2020）。「區塊鏈應用法律高峰論壇2020」邀請國內銀行業、證券業、法律、會計師事務所、資策會科法所、資策會、金融管理、保險業等領域專業人士與談，從金融科技的角度探討區塊鏈落地應用的種種層面。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而這些平臺所推出的解決方案，主要是透過區塊鏈技術中的「去中心化」、「不可竄改性」、以及「加密安全性」，讓數位發行的音樂能快、更簡易的確認創作者與版權所有人，讓創作者更放心的創作，同時，創作的授權機制透過區塊鏈技術的應用，與傳統複雜的授權機制相比，將大幅降低中介降低處理成本、提高效率，再加上區塊鏈技術可提供透明可信任的金流帳本，讓創作者明確知悉創作所獲得的授權收入，解決過去中介方所衍生的信任問題。
    </p>
    <p><strong>（三）法律維權</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;配合區塊鏈技術應用至音樂的智慧財產權管理，也涉及相當多法律議題，包含如何證明創作的權利歸屬、授權合約的設計、數位資料的證據力，甚至是創作者面臨盜版與抄襲問題時應如何處理可能衍生的糾紛？種種議題都涉及如何「落實」前述的智權管理機制。尤其，現今區塊鏈技術解決的是資料存儲的不可竄改性，但數位資料的證據力問題則需將資料上鏈至公鏈才能有效處理，惟目前資料上鏈至公鏈所耗費的成本與時間過鉅，故少有著墨。關於此點，目前師大與清大攜手合作提出影音智財權管理與區塊鏈帳本結合的方案中，有提出特有區塊鏈技術方案可大幅降低資料上鏈至公鏈的成本，將有效提升數位資料的證據力以及其產生的成本問題。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其次，當這些區塊鏈技術於音樂產業的應用未普及時，尚須進一步研究實際面的限制，包含授權機制實施（例如如何讓創作者確認自身權益與所需授權）、合約內容如何設計、當智慧財產權被侵害時應如何維權等。而此部分，目前師大與清大攜手合作提出影音智財權管理與區塊鏈帳本結合的方案中，有提及透過技術加上法律的專業團隊，實質提供使用者在授權時的諮詢、合約擬定以及涉訟時的律師服務，提供實質協助有效解決前述問題。
    </p>
    <h3><strong>結語</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因為武漢肺炎疫情的影響，2020年臺灣的流行音樂生態變「宅」了，宅經濟與宅娛樂是今年的主題，民眾對線上音樂會與直播的參與大增，線上串流訂閱率升高，但重大的音樂節停辦、頒獎典禮規模縮減、上半年許多音樂會與現場演出取消。國內的論壇轉而以聚焦於國內音樂環境、網路「秒閱聽」與線上串流媒體的影響等主題。整體而言，2020年不若去年度熱鬧與國際化，是安靜與沉潛的一年。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;所幸，許多資深民歌歌手與音樂巨擘回歸到舞臺上與觀眾見面，是今年度樂壇的大驚喜。希望來年，這些堆建臺灣流行音樂能量與創作底蘊的老朋友們，還能持續的與老歌迷見面、與年輕的歌迷互動。流行音樂學界持續進行人才的培育，產業重新檢視過去作法並升級原有的線上音樂會科技，同時帶動產業與演唱會製播的型態變化。AR、VR、MR、SR等技術加入演唱會科技是未來的趨勢，相信隨著5G行動數據技術的成熟與普及，線上演唱會可以更加擬真。此外，為了守護原有的音樂資產，資工與科技、金融、法律、音樂等產學人士投入智財管理與保護的方案與平臺建置。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年，臺灣流行音樂環境與生態並非蕭條而是蟄伏，閉關修練以蓄勢待發。
    </p>
