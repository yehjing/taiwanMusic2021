      流行音樂培訓計畫」分別從歌唱、作曲、填詞、吉他、編曲5方面進行；南臺科技大學流行音樂產業系舉辦「流音之星夏令營」則以高中生以下學生為招生對象，分為創作、歌唱、幕後3組進行。由前述可知，高等教育的人才短期培訓仍以歌唱和創作為主，如有演奏也只有吉他，然流行音樂中有更多其他器樂的運用與搭配，如：爵士鼓、Bass、Keyboard等，且音樂的整體輸出有賴錄音工程的輸出與後製，這兩方面在高等教育的短期培育中，實在少見，但在民間已有行之多年的系統化培訓與分級能力檢定，將人才培育的層面延拓展至更多的流行音樂專業與導入證照制度，使流行音樂教育的訓練更加全面與具有公信力，是當前體制內教育應納入規劃的目標。
    </p>
